<p>
<p>Дістається і воїнам, і маріупольцям запорізького щирого хліба. Так само ж розлітаються й дитячі сувеніри та малюнки. Найстарший з бійців з позивним «Михайлович» навіть не військовий — волонтер, авто-електрик з Кирилівки, що добровільно, без будь-якої зарплатні та надії на якийсь-там «статус» по війні, вже рік у батальйоні допомагає відновлювати зранену техніку , отримавши від земляків величезний торт, тут же, під оплески побратимів, передаровує його дітям-маріупольцям. Взагалі, здається, половина привезених подарунків потихеньку перетікає на «цивільну» половину залу…Такі-то у нас «фашисти».</p>
<p>Кожному народові б таких! По праву господарів пріоритет тут у місцевих. Тож першим виходить на сцену маріуполець — співак і автор пісень.</p><a id='u1'></a><h3>Other cities Zaporizhia</h3><p>Виконує трійко своїх творів. Серед них — і ліричні, і патріотичні, і україномовні, і російськомовні. Голос у хлопця, насправді, красивий, грудний, проникновенний. Видко, що вкладає душу. Тому з помосту бард сходить під заслужені аплодисменти.</p><strong><a id='u2'></a><h2>Знайомства</h2></strong><p>Але наступна учасниця викликає серед глядачів «тихий фурор» — дружне зітхання і зачарування! Коли дівчина доспівала, у залі наче грім гримнув — долонь не жалів ніхто. Од того і я не встигаю підготувати душу до виступу: і досі завжди хвилююсь, виходячи перед людський загал. Хоча, може, те і на краще….</p><img src="https://i.ytimg.com/vi/wZeAQPfMsgg/hqdefault.jpg" alt="Сайт знакомств Intimok: здесь находят лучших" style="max-width: 480px; max-height: 480px;"/><p>На сцені немає столу, тому, аби звільнити крила, доводиться покласти книжки, привезені з собою, просто на підлогу. Горло трохи здавлює хвилювання, отже сказане, мабуть, звучало дещо хрипко:. Сьогодні в Україні чоловіків терзають два почуття — або страх, або муки совісті. Хоча й не ховаюся — при потребі свій бій, від початку і до кінця всіх кінців, відбуду чесно.</p>
<p>На жаль можливості мої і моєї родини невеликі, але ми намагаємося зробити для вас усе можливе. Гадаю, забезпечили ми ними не менше роти… Нині ж привіз вам найдорожче, що є у поета — книгу поезій «Український Порідник». У ньому кожний з вас зранку-раночку зможе прочитати добре натхнення на кожний Божий день і відчути сердечну підтримку — мою, всіх поетів, всіх запоріжців, всієї України. То все — не якісь пророцтва, чаклування… Кожне з натхнень — наша молитва за вас, наше благе напуття вашій долі!</p>
<p>Після концерту всі, кого книга зацікавить, підходьте — подарую кожному! Далі читаю кілька поезій. Відчуваю, що голос тремтить, але не фальшивить. Дивлячись в очі таких людей, брехати серцем неможливо! Маю поступитися місцем іншим вашим гостям! Часу у нас надто мало, а вони теж приготували для вас свої сердечні дарунки. Ітак неждано зал вибухає оплесками!</p><strong><a id='u3'></a><h2>Відгуки про «Поїхали з нами»</h2></strong><p>Хоча, треба визнати, не такими шаленими, як на спів маріупольської Україночки…. Почесному ж, такої краси, як у тієї мавки, мені повік не дібрати… А читав же, здається, незлецько? Та вже, як є…». Овацій та аплодисментів дістається і їм вволю. Далі — офіційне вручення від командування батальйону «Почесних грамот» і «Подяк» солдатам та волонтерам. Отримують відзнаки наші обидві міськрайонні організації журналістів, а також, зовсім несподівано… я.</p>
<p>Багато разів і багато чого отримував я в нагороду за літературну працю, але коли брав з рук начальника штабу цей простенький аркуш «Подяки», відчув — нагороди більшої досі не мав. Хоча й розумію, що нагороду цю ще відпрацьовувати мені й відпрацьовувати… Що ж, вистачило б тільки сил і здоров я. Вручати «Грамоти» солдатам начальник штабу запрошує жінок із зали.</p>
<p>Виходить їх із двадцятеро — і зовсім ще дівчатка, і красуні-дівчата, і красуні-жінки. Тепло і душевно відбулося все — з поцілунками, обіймами, ліричними відступами. Урочиста частина зустрічі закінчується, і кожен знову береться до своєї справи — журналісти розходяться спілкуватися з солдатами, актори спішать перевдягатися, а я беруся підписувати та дарувати книжки. Неждано навіть трішки, збирається людей багато. Що радує — поміж солдат немало місцевих. Першою до мене люди пропускають зовсім маленьку дівчинку.</p>
