<p>Ми й без того все менше науковці, все більше бюрократи. Донедавна то вважалося малоцікавим баластом, але часи міняються: ідеологічно витриманий «соцреал» зараз на піку моди. Є бажання створити окрему велику експозицію, але за нинішніх умов, доки перебуваємо в старому приміщенні, реалізувати ідею можливо лише за рахунок ущемлення класичної, усталеної експозиції.</p>
		
