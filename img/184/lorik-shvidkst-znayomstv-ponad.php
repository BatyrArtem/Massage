<p>А також підтвердження його слів, що бюджет Глухова зріс майже втричі у порівнянні з роком. Серед іншого, йому вдалося економити кошти на державних закупівлях через роботу системи Prozorro, збільшити матеріальну допомогу на лікування за рахунок міського бюджету та придбати кілька одиниць спецтехніки для міста. Але ж для простих людей це непомітні зміни, хоча, можливо, й набагато важливіші, ніж тротуари. Наочні зміни виявилися обіцянками-цяцянками Терещенка", - каже глухівчанка Олена, яка працює в місцевій школі.</p>
<p>Врешті здаються під напливом запитань та пояснюють: "Ми не голосували за Мішеля Терещенка. Просто не бачили сенсу. Він людина з іншого світу - все життя прожив у Європі, йому не зрозуміти нас. У нашій області панує, можна сказати, мафія однієї людини.</p><a id='g3'></a><h3>Історична довідка</h3><p>Усі тут знають його ім'я, тому не дивно, що Терещенко здався", - емоційно пояснює юна глухівчанка, не називаючи свого імені. На прес-конференції мер міста Глухів Сумської області Мішель Терещенко заявив про своє рішення скласти повноваження міського голови й балотуватися на посаду президента України. Спротив з боку старих еліт у регіоні, за словами Терещенка, й стали основною причиною його відставки з посади мера.</p>
<p>Чинне законодавство робить міських голів цілковито залежними від забаганок обласної влади й заручниками депутатського корпусу. А після скасування гетьманства при Катерині ІІ військовий генерал-губернатор П. Рум'янцев-Задунайський домігся в імператриці дозволу на побудову в Глухові Малоросійської колегії на три поверхи, одного з найкращих у місті. До середини XIX ст.</p>
<p>значної забудови місто не зазнало, а всі нові будівлі були другорядними. Проте нового поштовху до створення шедеврів подарував Глухову меценат М. За його кошти в місті було побудовано одну з перлин Сумської області — Трьох-Анастасіївську церкву, чоловіче училище, жіночу гімназію, учительський інститут та багато іншого. У місті встановлено низку пам'ятників, зокрема: композиторові Д. Бортнянському та М. Березовському скульптор — Інна Коломієць , Пам'ятний знак та пам'ятник Тарасові Шевченку , а також, загиблим солдатам у роки Другої Світової війни і багато інших.</p>
<p>Не далеко від Глухова 14 км знаходиться Свято-Різдва Богородиці Глинська Пустинь — чоловічий монастир ставропігійний. На північно-західній околиці Глухова створено заказник місцевого значення Чернечі джерела. Здавна вони славляться як цілющі. За переказами, був у цій місцевості монастир, тому й річечка, яка протікає в мальовничій долині, називається Чернечою.</p>
<p>А про лікувальні властивості тутешньої води слава поширилася далеко за межі Глухівщини. Тож не мілів потік віруючих та мирян, які вирушали сюди з усіх усюд з молитвою на вустах і непереборним бажанням припасти до життєдайної вологи. Кілька років тому у цьому святому місці за підтримки підприємств, організацій міста й району, приватних підприємців, небайдужих жителів було зведено й освячено храм-каплицю, встановлено хрест, облаштовано купальню та споруджено східці й містки для зручного доступу до цілющої води.</p>
<p>Сюди приходять поодинці, родинами, дружними колективами. Але не задля гучного відпочинку правилами, які прикріплено на видному місці, забороняється галасувати , а для того, щоб у молитві просити в Бога здоров'я й благополуччя. У Глухові збереглося старовинне єврейське кладовище, де похований прогресивний єврейський письменник-просвітитель і педагог Лазар Цвейфель — У сучасному Глухові продовжує розвиватися мережа ЗМІ — як друкованих, так і електронних [61] , розвивається Інтернет.</p>
<p>Є своя друкарня в Глухівському національному педагогічному університеті, де виходять у широкий загал видання в галузі «Педагогічних наук».</p>
		
