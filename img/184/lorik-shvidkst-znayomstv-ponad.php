	<p>Більше я не побачила нічого". Пам'ятна композиція на честь Тараса Шевченка справді виглядає чи не єдиним новим об'єктом на весь центр міста. У парку, що прилягає до неї, раніше був Ленін, проте ще у перші дні свого мерства Терещенко розпорядився прибрати скульптуру, пообіцявши спорудити фонтан на цьому місці. Проте й до сьогодні тут лише розбиті доріжки та плити. Основні обіцянки, які давав Мішель Терещенко перед обранням мером, стосувалися саме покращення якості життя. Люди очікували, що зміниться не тільки вигляд міста, але й відчутним буде економічне поліпшення, зокрема збільшення робочих місць та прихід іноземних інвесторів.</p>
