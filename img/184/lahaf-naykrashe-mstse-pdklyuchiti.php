<p>В начале 19 столетия Яворов был уездным городом 2-й категории, Перемышльского округа. Он оставался городом с типичным к тому времени планом: четырёхугольник с равными узкими улочками и деревянными домами. Главное место занимал королевский замок. На центральной площади — ратуша и рынок. Площадь Рынок лежала посреди города, вымощенная камнем.</p>
		
	<p>Все мажорчики жили на Ростиславской. В  году королевич Вацлав основал яворовский католический приход, а русинский православный приход был создан на столетие позже. Первым сакральным сооружением в Яворове был деревянный приходской костёл Наисвятейшей Госпожи Марии, основанный в  году и существовавший до  года.</p>
<p>В этом костеле в  году был крещён сын короля Яна Собеского Константин.</p>
		
			
