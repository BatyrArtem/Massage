	<p>Per C. DahIke Goloneilum cor. Regni et Prim: Praefect: Archit; Militaris. Anon Domini MDCCLIII. Нижня брама мала чотири вежі поєднані між собою будинком зі стрільницями. Дві башти були біля скали, одна біля річки Смотрич і навпроти через річку ще одна башта. На Руській брамі було дві кам'яні таблиці з гербами: Св. Георгій і інший, не зовсім чіткий можливо вовк. З південного боку міста був третій в'їзд — так звана Руська брама. Ця брама коло річки внизу була збудована в XVI  ст.</p>
		
