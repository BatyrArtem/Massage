	<p>Кам'янецький староста Матвій Влодек пом. Вітряна брама була основним в'їздом в місто. Польська Брама була поєднана з Вежею Баторія Королівською. Складалася брама з будинку в якому був в'їзд до міста.</p><strong><a id='k4'></a><h2>Кам'янець-Подільський</h2></strong><p>Будинок мав три башти: одна на міській стіні, одна посередині для в'їзду й інша — біля річки. В Ляській брамі була каплиця, в якій був образ св. Російський уряд за часів губернатора Горохольського наказав підірвати бастіон на якому стояла Вежа Баторія і розібрати Ляську Браму. Від нижніх башт дорога йшла на гору узбіччям під захистом муру, що був з боку провалля, а на верху гори треба проїздити через ворота, так звану Вітряну браму. Біля тих воріт стоїть величезна башта Баторіева, або «Семиетажна».</p><img src="https://ukrainer.net/wp-content/uploads/2019/11/0_cut_min-84-768x768.jpg" alt="Навігаційне меню" style="max-width: 480px; max-height: 480px;"/><p>Над Вітряною брамою з боку міста прибито таблицю з таким написом:. BATORY R. Sub avspicio Serenissini as Potentissimi Principis Augusti III Regis Poloniae Magniq: Ducis Lithuaniae Ac Principis Haereditarii Saxonlae Electoris illustrissimus Dominus Losephus A Potok Potockl Casteianus Cracoviensis Supremus Exercitium Regnl Dux Hoc opus incerpit erigere Postquam Desiit vivere Filius iIlustrisim, Dns: Stanisia A Potok Potockl Palatinus et Generalis Terrarum Kioviae Tam ductus Gloriae Parentis sul Quam Amore Patriae Hoc opus finivit.</p>
		
			
