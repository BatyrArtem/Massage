	<p>і складалася з чотирьох башт, що з'єднувалися стінами та фортифікаційними будинками. Проїзд ішов через башту і закривався дерев'яною лядою, що спускалася зверху башти. Руські ворота за потребою закривалися дубовою дерев'яною решіткою, яка опускалась за допомогою блоків з простінків другого поверху башти.</p>
<p>Механізм цей існував до кін. Ця ляда була ще недавно до  р. але тепер її немає. Коло проїзду на стіні будинку з боку міста була кам'яна таблиця з таким написом рельєфними літерами:. НЕС PORTA REGIE MAIE STATIS IMPEN SIS IN VIGILIA TRIVM REGVM ERECTA  Ці ворота реставровано королівським коштом напередодні трьох королів Водохреща  р. Коло тої таблиці було вмуровано 2 кам'яні таблиці з рельєфними гербами: на одній св. Юрій на коні який перемагає Змія герб міського магістрату , а на другій ніби герб «Задора».</p>
<p>Ці три таблиці тепер зберігаються в Кам'янецькому Історично-Археологічному музеєві. В іншому місці брами на таблиці вибито герб Польщі — одноголовий орел і напис: A. Верхні ворота Руської брами. На горі, куди йшла дорога від Руської брами до міста, були ворота в стіні, як видно з плану p. Ці ворота певне знищено, коли в XVIII  ст. будували великі будинки для військових казарм «кошари» , де потім містився військовий шпиталь. Від Руської Брами йшла вулиця оточена по боках мурами зі стрільницями до міста. Брама була між стінами. На даний час непогано збережена. Над Руською брамою на скелі стоять руїни чотирикутного бастіону, що охороняв ту браму.</p>
<p>Від того бастіону по-над скелею йдуть міські стіни — з одного боку до в'їзних воріт та Турецького мосту, з другого боку — до «кошар». В половині 18 століття деякі фортифікаційні роботи провадилися під керівництвом інженера Дальке. В — pp. значні поправки замкових і міських оборонних будинків робилися під доглядом Стефана Маковецького будування стіни коло Баторієвої башти та стін у замку.</p>
<p>В  р. король Станіслав Август пожертвував із своїх коштів чималу суму на ремонт Кам'янецьких фортифікацій, про що на пам'ять було вибито навіть медаль. За часів російської влади зруйновано: Верхню, Нижню i Середню Польську браму, Скальські та Польні ворота, Браму Станіслава Августа, Червоні ворота, Міські ворота. Кам'янецьку фортецю скасовано в  р. В XII  ст. входив до складу Галицького князівства. В Мстислав Мстиславович син Мстислава Хороброго і Володимир Галицький воювали з волинським князем Ростиславом Мстиславовичем взяли в полон багато людей в околицях Кам'янця.</p>
