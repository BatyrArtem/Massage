<p>
<p>В півд-зах. стіні розташована поярусно опалювальна система. Перекриття не збереглося. З півдня та півночі біля башти знаходяться залишки міських оборонних мурів.</p><strong><a id='k1'></a><h2>Кам'янець-Подільська фортеця — Вікіпедія</h2></strong><p>Місто мало три брами: Ляську Польську з Вітряними воротами, Руську, Замкову з Красними воротами зі сторони замку. Замкова брама складалася з Міських воріт та Красних воріт кругла башта на мості зі сторони замку , Тріумфальних воріт Брама Станіслава Августа. В'їзд з боку замку був забезпечений самим замком, але при в'їзді до міста було ще над скелями виведено мур з баштою, через який треба було проїжджати, як вже сказано. Зі сторони міста стіна була пофарбована в червоний колір.</p>
<p>Ці ворота мали залізну решітку, яка опускалася зверху. Ворота також були пофарбовані в червоний колір. За Красними воротами дорога розходилася: ліва дорога вела до замку, а права до Замкових воріт Брама Станіслава Августа , які могли би називатися Тріумфальними воротами.</p><strong><a id='k2'></a><h2>Екскурсії у Кам'янець-Подільський</h2></strong><p>За замковими воротами дорога знову ділилася на дві частини: одна вела в передові укріплення між високими земляними валами обкладеними тесаним камінням, а інша на Підзамче. Являли собою чотирикутний бастіон з бійницями на всі сторони. А проти самого мосту та в'їзду до міста стояв бастіон, або велика й ширша башта.</p>
<p>Цей бастіон притулявся до верхньої площі гори, де стоїть місто; побудувала його кам'янецька вірменська громада, як це зазначено в напису на плані  р. Коли Кам'янецьку фортецю було скасовано, то той бастіон засипали землею і зробили з нього терасу так званого Старого бульвару. Проти мосту з боку міста накреслено п'ятикутний бастіон, що про нього в експлікації сказано: "насип для військових машин гармат , що збудувала вірменська нація з тесаного каменя. Комплекс Польської брами складався з: Нижньої брами п'ять башт і аркадний міст біля річки Смотрич , Середньої брами, Верхньої брами Вежа Баторія, Вітряні ворота.</p>
<p>Від Нижніх до Верхніх воріт йшов мур. Внизу переїзд через річку захищали п'ять башт , що стояли коло річки: три на правому березі, з боку міста, і дві на другому боці річки.</p><a id='k3'></a><h3>Останні новини</h3><p>На плані Кам'янця  р. видно, що в Лядській брамі через річку був збудований на мурованих арках якийсь ніби коридор для переходу — шлюзова система. Через р. Смотрич йшов аркадний міст, на якому був змурований перехід зі стрільницями, що мав ще одну башту. Під Нижньою брамою сиділи в минулі часи лірники та оспівували славні часи на Поділлі.</p>
<p>Від цих воріт збереглися два стовпи, на них були гербові таблиці.</p>
		
