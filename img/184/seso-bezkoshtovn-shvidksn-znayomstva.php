	<p>Контактна мережа остаточно демонтована у січні року [17]. Добропільська центральна районна лікарня. Добропільська санітарно-епідеміологічна станція. Приватний лікувально-діагностичний центр "МЕДСЕРВІС" [18]. Перша двокласна земська школа на території Добропілля була відкрита в році на території 11 колони, школа розмістилася в одному з бараків, вона була розрахована на 28 учнів. Після Української революції років в школа переїхала в більш просторе приміщення, яке належало колишньому шахтовласнику. У році у Ерастівському селищі побудували двоповерхову школу, на другому поверсі викладали українською, на першому російською і татарською.</p>
