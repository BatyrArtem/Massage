<p>у році було 44 мешканці поміщика Албанського, в році — Ерастівський рудник, на правому березі річки Бик  — Святогорівський рудник. З року працює залізнична гілка. До ради увійшли Є. Агеєнко, А. Іванов голова , І. Лактіонов, І. Орлов, С. Стебні та інші шахтарі. В зв'язку із загрозою наступу на Донбас загонів Каледіна, на заклик Центрального штабу Червоної гвардії Донбасу були сформовані загони Червоної гвардії на Добропільських рудниках.</p>
<p>Місто і рудничні селища були визволені у квітні року військами Гетьмана Скоропадського. Місцева адміністрація була розігнана, а її голова був розстріляний.</p><a id='g1'></a><h3>Навігаційне меню</h3><p>Місцеву адміністрацію представляли Н. Булавіна, С. Опроков, І. Серіков, П. Орлова та Н. Костіна розстріляли; машиніста підйому, члена Ради І. Лактіонова та інших побили шомполами; заарештували робочих І. Духа, А. Литвинова, П. Севастьянова, М. Червоного козацтва під командуванням Віталія Примакова остаточно захопили місто і навколишні селища рудників. До року в Парасковіївці було 47 господарств і мешканців, ще жителів у селищі Святогорівського рудника.</p>
<p>Восени  р. Махно з невеликим, але добре озброєним загоном бійців проходив з півночі Луганщини на Дніпропетровщину через територію нинішнього міста Добропілля по дорозі, яка проходила навпростець від Ганнівського містка до ст Добропілля. Григоренко зі своєю бандою влаштував засідку в густих терниках вздовж дороги і за насипом залізниці. Як тільки колона махновців ступила на територію між нинішньою вулицею Івана Франка і водонапірною баштою, банди відкрили по них кулеметний вогонь з-за залізничного насипу, а з тилу, з балки, де нині ставок і міський парк, напали більше півдесятка сотень вояків Григоренка на конях.</p>
<p>Махно талановитий командир, зі своїми трьома сотнями досвідчених бійців, зумів розгромити банду, що мала до тисячі грабіжників, яка після цього припинила своє існування. Ерастівський і Святогорівський рудники були об'єднані в один, який став носити назву «Червоноармійський». Проте його функціонування мало бути припинене через недостатність коштів і відновилося лише у другому півріччі року.</p>
