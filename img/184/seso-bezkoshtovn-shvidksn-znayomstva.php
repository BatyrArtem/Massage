<p>Того ж року на проспекті перемоги біля магазину «Бамовський» спорудженно храм-каплицю в пам'ять гірників, які загинули в шахті. У році урочисто відзначено річчя з часу надання Добропіллю статусу міста у  р. На місцевих виборах в жовтні року мером міста був обраний Андрій Аксьонов.</p><a id='g3'></a><h3>Додати новий коментар</h3><p>Перший випадок використання РСЗВ БМ «Град» у війні на сході України стався 13 червня року о в Добропіллі. Російські терористи спробували обстріляти блокпост ї механізованої бригади на в'їзді до міста. Один снаряд влучив у блокпост, але не розірвався, проте інша частина залпу дала переліт — і накрила овочебазу, що перебувала метрів за блокпостом. Одна цивільна особа загинула. Вогневі позиції терористів розташовувались неподалік від населеного пункту Мерцалове [14] [15].</p><strong><a id='g4'></a><h2>Погода у Добропіллі на місяць</h2></strong><p>Терористи спробували втекти, однак машину довелось залишити. Військовослужбовці ЗСУ виявили в ній таблиці для стрільби з РСЗВ, на якій вказаний номер військової частини. Місце дислокації — станиця Калинівська і аеропорту Ханкала Чеченської республіки. Командир батареї — гвардії капітан Дмитро Афанасьєв [16]. Видобуток кам'яного вугілля ДХК «Добропіллявугілля»  — одне з найбільших підприємств галузі  — видобуток вугілля в році — 5 тис.</p>
<p>Одне з найкращих вугледобувних підприємств — шахта «Добропільська». В останні роки за виробничі досягнення, технічні нововведення її колектив відзначений міжнародними призами: «За найкращу торгову марку» рік — Женева, рік — Мадрид , призом Європейського торгового дослідного центру «Євроринок» рік — Брюссель.</p>
		
			
