	<p>Мовляв, так він показав слабкість і нерівність власниці. У Норвегії саме дівчата частіше ініціюють знайомство із сексуальним підтекстом, бо якщо так зробить чоловік, то це можуть розцінити як домагання. І довести справу до суду. У бік Заходу нам далеко ще й тому, що коли заходить мова про знайомства, то ми досі однією ногою в заляканому СРСР. Водночас за тисячі кілометрів від колючих дротів чоловік помічає жінку в кафе, на вулиці чи у транспорті, підходить, знайомиться і тут же називає час і місце зустрічі.</p>
<p>Ця культура далеких х досі притаманна Європі та США. Та жителі Східної Європи й колишніх радянських республік не встигли її засвоїти, узявшись похапцем за онлайн-інструменти. Майже половина аудиторії на пікап-курсах Вольфа — молоді чоловіки, які не знають, як поводитися. Єдиним джерелом інформації, за словами тренера, залишалось кіно — з його ілюзорними картинками. Тепер так само невідомо, як поводитися на сайтах знайомств. Те, чого Вольф навчає цих молодих людей на своїх курсах, він сам називає «вінегретом з психології, етикету, соціології, стратегій поведінки та сприйняття».</p>
<p>А ще є багато практики — це коли хлопці — поодинці або ж разом, але на певній відстані — ідуть знайомитись у місто. Дівчата реагують по-різному.</p><strong><a id='g4'></a><h2>5 найкращих додатків для знайомств під час карантину – Lustrum</h2></strong><p>З досвіду Вольфа, здебільшого — огризаються, бурмочуть «я на вулиці не знайомлюсь», ігнорують чи перелякано проходять повз. Іноді хлопці виявляють наполегливість та кидають репліку на зразок: «Тобі важливіше місце чи емоції, які в тебе викликає людина? Деякі дівчата зупиняються. Одній норвезькій першокласниці сподобався хлопчик. Вона діловито взяла його за руку та почала скрізь із ним ходити. На якійсь перерві вчителька покликала хлопчика на бесіду:. Але правильніше зауважити, що тобі не до душі поведінка дівчинки.</p>
