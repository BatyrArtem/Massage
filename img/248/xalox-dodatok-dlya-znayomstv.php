	<p>І уявлення не має, де у заплутаних провулках знайти цей невідомий йому паб. І що столик там треба замовляти завчасно, інакше сидітимеш на якомусь ослінчику, з одного боку затиснута куртками, а з іншого — людьми. Та було весело — коміки виступають, люди заливаються сміхом. У когось дебют. Симпатичний молодик поруч розповідає мені, хто є хто.</p>
		
	<p>Зрештою, він знає, що чекаю. Маленький, невпевнений, лисуватий. Стоїть менше ніж за метр від мене.</p><strong><a id='g2'></a><h2>Я фанатію від мобільних знайомств, але на побачення не ходжу</h2></strong><p>Хлопець вставляє у вуха навушники і починає нервово походжати з боку в бік. Зиркаю в телефон: жодного дзвінка чи смс. Але ж він знає, що я чекаю! За кілька хвилин усі сумніви розвіюються. Скидає навушники й біжить на сцену:. Вибачте, я так хвилювався перед виступом, що десять разів прослухав його щойно в навушниках. Та Валерій, задоволений собою, вже сходить зі сцени й поспішає натягати куртку та шапку. Стоячи біля мене. Я не з гордовитих, просто було цікаво спостерігати за ним і його поведінкою — а що ж буде далі? Він іде геть. І я теж — із відчуттям найгіршого «побачення» в житті.</p>
<p>Слідом вибігає мій симпатичний співрозмовник, щось розповідає про себе. Не слухаю — невже я настільки краще виглядаю на фото, що в житті йому навіть не захотілось підійти? Хто ж винен: я, бо не нагадала про себе дзвінками, чи він — бо чи забув, чи засоромився витягати мене з компанії або бодай дати про себе знати?</p>
<p>Сходив на кілька побачень і зрозумів: усе було заради того, щоб випити каву чи поїсти на дурняк. Якось подруга скинула їй скриншот повідомлення, де іноземець розписував, що хоче знайти собі українську подругу або навіть дружину, проте постійно зіштовхується з меркантильністю. Моїй подрузі-стюардесі такі історії теж знайомі. Буває, скупчуються дівчата разом на борту — і нумо розповідати про свій досвід у «Тіндері»: а він мені те купив, а я його на ось це розвела. Доходить до комічного: «Слухай, а можеш мене зараз підмінити?</p>
<p>Там «мій» сидить, а я йому відмовила! Ольга Мельник не помічала такого, щоб дівчата в інших країнах були настільки меркантильними. Фахівці погоджуються, що глобальні тенденції, які ставлять під сумнів жіночу залежність та чоловічу ініціативність, з роками все ж почнуть проявлятися в Україні.</p><strong><a id='g3'></a><h2>Свайпни мене, якщо зможеш</h2></strong><p>Та принаймні до Норвегії, де часто працює Дмитро Вольф, нам іще далеко. За його словами, там дівчата настільки емансиповані, що серйозно налаштовані подати на хлопця в суд лише за те, що той допоміг зняти валізу з полиці в літаку.</p>
		
