<p>Схематично креслить мені на снігу їхню класифікацію та властивості — весело. Ще Діма пише книжку — насправді талановито. Жвавий, розумний, комунікабельний — із дівчатами не мало би бути проблем. Якось в універі пішов на марафон «сліпих побачень», де за кілька хвилин мав фантастичний успіх — отримав усі десять можливих телефонів дівчат.</p>
<p>І нікому не подзвонив. Просто граються і не шукають там нічого. Тренер з особистісного росту та соціальний технолог Дмитро Вольф, який, з-поміж іншого, десять років проводить у Києві пікап-курси для чоловіків, висловлюється різкіше:. Ось сподобалася дівчина ти хлопцям. І їй зовсім не хочеться з ними спілкуватися, пізнавати їх, ходити з ними на побачення.</p><a id='g1'></a><h3>Довіритися додатку?</h3><p>Того, що її лайкнули, достатньо, аби закрити додаток і знову зайти туди через три дні, коли знову захочеться почухати своє е ґ о. Щоб із кимось познайомитись, більше не треба довго збиратись, фарбуватись, кудись їхати та усміхатись. Зараз це можна робити у шкарпетках, на дивані, з піцою. Наситившись кількістю, люди тут же, пропустивши етап пізнавання одне одного, вимагають якості. Виникає ілюзорне відчуття, що всі навколо якісь поверхові, «недороблені», — пояснює Вольф. А досліджувати — бояться.</p>
		
