<p>Це триває місяці. Згодом починається процес знайомства з дитиною під супроводом психологів. Усиновлення — за рішенням суду. Щодо деяких довго тривають певні юридичні процедури. А частина дітей, очевидно, ніколи не будуть усиновлені з різних причин — підлітковий вік, важкі хвороби. Якщо дитину протягом року не усиновили громадяни України, її можуть усиновити іноземці. Тоді це вже проблема непрофесійної роботи в процесі взаємодобору, знайомства.</p>
