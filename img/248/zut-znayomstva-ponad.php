<p>
<p>Альтернативні родини ми для них швидко не знайдемо, довелося б створювати якісь інституції, а це не вихід. Тому діємо різними шляхами. У столиці сьогодні функціонує вже 27 дитячих будинків сімейного типу, до кінця року плануємо відкрити ще 3 нових. На жаль, цього все ще недостатньо, щоб кожна дитина-сирота, чи дитина, позбавлена батьківського піклування, потрапила на сімейне виховання.</p>
<p>Щороку в столиці набувають статусу близько дітей. Але не всі діти мають родичів, які мають право чи можливість опікуватися дітьми. Міська влада підтримує їх всебічно. У столиці є міська цільова програма «Діти.</p><a id='z1'></a><h3>Пошукова форма</h3><p>Столиця» на роки, вона передбачає фінансову допомогу дитячим будинкам сімейного типу для вирішення різних матеріально-побутових проблем. Кожного року з міського бюджету на це виділяється від 1 до 1,5 млн грн, завдяки чому в батьків-вихователів все більше можливостей створювати для дітей належні умови проживання, навчання і відпочинку. Дитячим будинкам сімейного типу надається також у користування службове авто, і за останні 5 років за кошти міськбюджету придбано вже 19 нових автомобілів.</p>
<p>Проте є й проблеми. Навіть при такій підтримці міської влади — бажаючих стати батьками-вихователями не багато.</p><strong><a id='z2'></a><h2>Бог і Україна понад усе</h2></strong><p>На додачу, треба знайти таку лінію поведінки, щоб і власні діти не ображалися на батьків, і сироти відчували себе серед своїх. Мотивуємо, як можемо. З року місто почало виплачувати одноразову соціальну матеріальну допомогу батькам-вихователям у розмірі тисяч залежить від кількості дітей. На кожну дитину-сироту та дитину з інвалідністю місто щомісячно надає додаткову фінансову допомогу опікунам, прийомним батькам та батькам-вихователям у розмірі грн.</p>
<p>Окремі фахівці супроводжують дітей, що знаходяться під опікою родичів. Періодично навідуються і роблять висновки, добре дитині живеться чи ні, раптом що — вживаємо заходів.</p><strong><a id='z3'></a><h2>Родинське — Вікіпедія</h2></strong><p>Загалом у Києві 27 ДБСТ та 30 прийомних сімей, в них зростають дітей. Щороку в столиці усиновлюють дітей. На жаль, і тут є проблеми. Це стосується дітей підліткового віку, дітей з інвалідністю синдром Дауна, ВІЛ, гепатит С, ДЦП та інші або великих сімейних груп рідних братів та сестер дітей. Сімей, готових прийняти таких дітей на виховання, одиниці. І тоді постає питання про влаштування дитини до інституції, де життя колективне, а увага та позитивні емоції — в дефіциті. Починається те, що звуть «знеособленими переживаннями». У дитини немає дорослого, який виділяв би її серед інших, речей, які належали б особисто їй.</p>
