<p>
<p>У році за Зборівським договором місто відійшло до козацької території, а після адміністративно-територіального поділу України на полки і сотні, Ямпіль увійшов до складу Брацлавського полку як сотенне прикордонне місто та вважався значним торговим пунктом.</p><a id='m1'></a><h3>Навігаційне меню</h3><p>У р. На жаль, картограф не позначив на карті укріплень Ямполя замок, який без сумніву вже існував на той час і від якого до сьогодення залишилось зовсім мало. Але історичні відомості того часу стверджують, що у тому ж таки році козацьке військо під приводом Богдана Хмельницького вступило до міста та розташувалось в Ямпільському замку. Хмельницький перетворив місто в свою тимчасову резиденцію. У Ямполі відбувались прийоми закордонних посольств, проходили міждержавні переговори. Тут Богдан Хмельницький приймав російських послів П. Протасова і Г. Богданова, а також послів кримського хана.</p>
<p>У Ямпіль до Хмельницького прислав своїх послів коронний гетьман польсько-шляхетського війська М. Звідси він вирушив у похід на молдовські землі, що завершився переможно для козацького війська, і сюди ж повернувся після походу. Тут, у одній з церков він призначив весілля для свого сина Тимоша та молдовської принцеси Розанди. Удару процвітанню міста було завдано в р. Внаслідок цих спустошень Ямпіль довго залишався обезлюдненим і відродився вже як незначне село.</p>
<p>З тих пір Ямпіль довго знаходився в повному запустінні. За даними року в Ямполі налічувалось всього хат. На початку XVIII століття нові власники міста, Потоцькі, намагалися відновити його і влаштували в ньому пристань і склади для товарів, що доставляються по Дністру з Чорного моря.</p>
<p>В році повітові установи перевели у містечко  Цекинівку, проте у  році повернуті в Ямпіль. Ямпіль був одним з найбідніших повітових міст Подільської губернії, схожий на велике село, з небрукованими вулицями і великими площами. У містечку налічувалось дворів, 6 мешканців чол.</p>
		
	<p>На початку ХХ століття заселена територія Ямполя становила десятин. Тут було 27 незамощених вулиць і 5 майданів. Містечко освітлювалось лише 62 гасовими ліхтарями.</p><img src="https://www.yampil.info/wp-content/uploads/2021/02/1111-4-290x290.jpg" alt="" style="max-width: 480px; max-height: 480px;"/><p>Після лютневої революції в Росії на території району влада переходить з рук в руки, а саме: Центральна Рада, Гетьманат, Директорія, Радянська влада. У році діяло споживче товариство, з року в Ямполі виникла перша промислова артіль «Пролетар». За кілька років у Ямполі виникло 11 кооперативних об'єднань, переважно виноградарських. Будували й державні підприємства.</p>
		
