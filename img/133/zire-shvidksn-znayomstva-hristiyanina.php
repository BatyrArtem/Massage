<p>Це був найбільший дзвін Галичини й один з найбільших у Європі, тож недивно, що з часом він став головним дзвоном Львова. У цьому ж соборі приймали папу римського Іоана Павла ІІ. Якщо пощастить і буде сонячно, із Замкової гори можна побачити навіть Карпати. Це свідчить про зростання в країні популярності барної культури. На її вістрі поряд з Києвом знаходиться і Львів, у місті чимало вишуканих закладів із цікавою коктейльною картою.</p>
<p>Беринди, 2 У самому серці Львова варто завітати до бару з магічним числом у назві — Rest. Bar  Привітний персонал завжди створить гостинну атмосферу. The Cult — заклад, в якому тебе огортає атмосфера затишку та вишуканості, де поєднані історія й сучасність. І, звісно, авторські коктейлі, серед яких, наприклад, Mandarin Bulleit Old Fashion. Бартендери закладу проходили навчання та практику в кращих барах світу, а коктейльна карта налічує велику кількість авторських напоїв. А ще заклад зарекомендував себе як чудовий майданчик для клубних виступів джазових виконавців з України та всього світу.</p><img src="https://p.calameoassets.com/130824154848-9e7a988c5db0f57d72fac3dd5bd69328/p1.jpg" alt="" style="max-width: 480px; max-height: 480px;"/><p>Також тут представлено великий вибір наливок, горілок та коктейлів, як ось, наприклад, Boulevardier. На старовинних картах саме Єрусалим зображений у центрі світу, саме тут відбувалося багато подій, описаних у Біблії. Сучасний Єрусалим ділиться на дві частини: Нове й Старе місто. Нове місто, зведення якого розпочалося у  році, давно випередило Старе і за розмірами, і за чисельністю населення. Тут Ісус востаннє зібрався зі своїми учнями, тут він був узятий під варту римськими центуріонами.</p><strong><a id='j3'></a><h2>Лозова (Кегичівський район)</h2></strong><p>Тут збереглися фрагменти стін XIII століття до н. Звідси, за переказами, Мухаммед вознісся на небеса. Собор прикрашено скульптурами та іконами, а єдине джерело світла у ньому — чисельні лампади, підвішені до стелі. Нині це друге за чисельністю населення місто країни, яке вважається її економічною столицею й являє собою агломерацію більш ніж десятка населених пунктів — справжній мегаполіс на березі моря. А ось кафе з традиційною єврейською кухнею, як не дивно, не так уже й багато.</p>
<p>Діють три театри «Габима», «Гешер», Камерний театр , кілька балетних ансамблів і багато музеїв. Музей Землі Ізраїльської. Тож не варто дивуватися, що прийом у тутешніх провідних фахівців розписано на декілька місяців наперед. Ми відібрали 10 найвизначніших локацій, які залишать по собі гамму неперевершених емоцій і допоможуть скласти повне уявлення про країну.</p><strong><a id='j4'></a><h2>Лозова (місто)</h2></strong><p>Не існує іншого такого місця в світі, яке поєднувало б стільки віх історії, релігії та культури. ЯФФА Один з головних портів стародавнього Ізраїлю й одне з найдавніших безперервно населених міст світу — цього опису достатньо, щоб кожному мандрівникові закортіло побувати в Яффі. Саме тут колись Ной побудував ковчег, а Персей звільнив Андромеду. В Яффі свого часу побували хрестоносці та Наполеон. Раніше ця назва належала всій долині біля підніжжя Оливної гори. Сад є популярним місцем паломництва християн, адже саме тут відбувалися описані в Біблії події — регулярні молитви Ісуса зі своїми учнями і взяття його під варту після зради Іудою.</p>
<p>В саду досі ростуть вісім олив, посаджених ще в I столітті н. Його узбережжя є найнижчою ділянкою суші на Землі, а рівень води на  м нижче рівня моря і до того ж падає зі швидкістю приблизно 1 м на рік. Через велику концентрацію солей у Мертвому морі його вода у вісім разів щільніше за воду звичайних морів, тож ні потонути, ні навіть пірнути неможливо.</p><a id='j5'></a><h3></h3><p>Зате можливо полежати на морській гладі з книжкою в руках, відчувши повний релакс! Свою назву він отримав завдяки розташованому неподалік місту, руїни якого вважаються одним з найпривабливіших місць для археологічних розкопок. Всього налічується 19 терас, які величезним каскадом спускаються до узбережжя. У центрі цієї природної дивини розташований величний храм, увінчаний позолоченими банями. Грандіозні рукотворні сади, які відкрили свої двері перед численними туристами та паломниками в  році, стали частиною Всесвітньої спадщини ЮНЕСКО.</p>
<p>Ейлат давно став своєрідною Меккою для аквалангістів з усього світу. Місто відомо ще й своїм незвичайним аеропортом, розташованим у декількох десятках метрів від кромки води. Неприступні мури і башти на висоті понад  м збереглися майже недоторканними з 25 року до н. З усіх боків її оточують круті скелі, а з боку Мертвого моря на саму гору веде вузька Зміїна стежка. Найвражаючий епізод в історії фортеці — трирічна оборона гарнізоном зелотів, які у І столітті повстали проти римлян.</p>
<p>Зрештою фортеця пала, та ніхто з її захисників не здався ворогу живим. Відтоді Масада є одним із символів волелюбності єврейського народу. До заповідника належить і оздоровчий комплекс, що славиться мінеральними ваннами та цілющими грязями. Місця сили — це особливі райони Землі, в яких фіксується сильна енергетика.</p>
