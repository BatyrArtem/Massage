	<p>Правобережжя Берди стало російським у році, після приєднання Кримського ханства до Росії. У році біля коси було засновано місто Бердянськ , що згодом розширилося на територію коси.</p><a id='e3'></a><h3>Додаткові переваги замовлення таксі в Дніпрі у нас</h3><p>Латинську назву, що, ймовірно, належала Бердянській косі, — Promontorium Agarum  — отримав мис у Морі Криз на Місяці [7] [8]. Тут особливу цінність складають водно-болотяні угіддя — гирла Берди, Солодкий лиман , Дальні Макорти , острови — Великий 10 га і Малий Дзендзик 15 га. Коса є заповідником державного значення. Загальна площа абсолютно заповідної території на кінцівці коси становить га, включно з ботанічним заказником і акваторією — 5  га. Рослинний світ дуже різноманітний. Зустрічається більш ніж видів вищих рослин , деякі занесені до Червоної Книги осока колхидська , синеголовник приморський.</p>
<p>Рослинний покрив представлений рослинами з потужним корінням. На захід від зони дюн характер рослинного покриву міняється, де зустрічаються переважно представники степу. Всього тут росте понад видів вищих рослин [9]. Хоч коса досить багатолюдне місце, на її території живуть чи колись жили зайці , їжаки , куниці , лисиці , ласки , степові коти.</p><strong><a id='e4'></a><h2>Бердянська коса</h2></strong><p>Мешкає велика кількість птахів: куликів , лебедів , чапель , качок , сорок , чайок, бакланів, очеретянок, і ще більше їх стає під час перельотів. Острови Великий і Малий Дзендзик , малі острови архіпелагу Астапіха  — слугують місцем гніздування птахів. Всього на Бердянській косі зустрічається понад видів пернатих [9].</p>
<p>У морі мешкає більш ніж 70 видів різних риб , серед яких колись білуга , осетер , севрюга , тепер камбала , піленгас , кефаль , тюлька , хамса , тараня , шемая , різні види бичків.</p><strong><a id='e5'></a><h2>Бердянськ — Вікіпедія</h2></strong><p>Ще Азовське море називають морем молюсків. Це важливе джерело живлення риб. Найважливішими представниками молюсків є серцевидка , сіндесмія , мідія. У прибережних водах з'являються дельфіни. На косу прокладена автомобільна дорога з твердим покриттям. Але автомобілям заборонено паркуватися на піску — лише на спеціальних стоянках, обладнаних майже на всій довжині коси. Маршрутні таксі влітку рейси кожні 5 хвилин пов'язують косу з центром. На Середній косі з боку Бердянської затоки збудована пристань.</p>
<p>Влітку між нею і портом курсував пасажирський теплохід «Генерал Кунгурцев». Кайтсерфінг на Бердянській косі.</p>
		
	
