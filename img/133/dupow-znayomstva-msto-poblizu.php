<p>
<p>На території аквапарку встановлено 28 атракціонів-гірок для дітей та дорослих, 7 стартових комплексів, створено незалежну зону для найменших відвідувачів з батьками. На площі 2,5 га у просторих вольєрах під відкритим небом живуть близько 70 видів тварин, серед яких уссурійські тигри, амурські леопарди, африканські зебри та страуси, рідкісні карликові поні аппалуза тощо. На території висаджені більше семи тисяч рослин. Міський парк імені П. Шмідта — улюблене місце відпочинку бердянців, яке займає територію більше ніж 3,5 га.</p>
<p>Його батько, начальник порту та міста у роки, багато зробив для благоустрою, розвитку та перетворення Бердянська у торговельно-промислове місто, він же і став ініціатором створення міського скверу. Саме це місто було рідним для червоного лейтенанта. Колекція картин Бердянського художнього музею могла б стати прикрасою навіть столичної галереї. Це особлива культурна гордість бердянців. Він подарував місту робіт із своєї колекції. А 7 листопада року у Бердянську відкрився художній музей.</p>
<p>Зараз у його фондах нараховується більше чотирьох тисяч полотен. У рік річчя Бердянська художній музей відчинив свої двері у новому сучасному приміщенні по вулиці Карла Маркса, 29 — воно вдвічі більше попереднього та знаходиться практично у центрі міста. Цікаві експонати з давніх часів та до наших днів представлені у Бердянському краєзнавчому музеї.</p><strong><a id='e1'></a><h2>Служба таксі 838 в Дніпрі</h2></strong><p>Він відкрився у році та нараховує сьогодні більше 60 тисяч музейних експонатів. У краєзнавчого музею є ще три філіали. Шмідта та відчинений у вересні року музей історії Бердянська.</p>
		
			
