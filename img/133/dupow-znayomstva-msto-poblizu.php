	<p>Бердянськ — місто фестивалів. Уподобали Бердянськ і байкеры. Вони щорічно збираються на Азовському узбережжі. Усього з квітня по вересень у Бердянську проходить до двох десятків різного роду крупних культурних та розважальних заходів. Але, мабуть, найвидовищним та найбільш очікуваним є Бердянський міжнародний кінофестиваль.</p><a id='e2'></a><h3>Пошукова форма</h3><p>Незабутні враження про відпочинок у Бердянську на все життя залишить прогулянка містом. У нашому місті добре розвинена індустрія відпочинку та розваг — є велика кількість кафе, барів, дискотек. Влітку до Бердянська періодично приїжджають луна-парк, циркові трупи з гастролями. По пішохідних частинах проспекту Леніна, Приморській площі та набережній можна покататися на велорікшах, справжніх конях та прикольному паровозику. Увечері можна відпочити у барах, ресторанах, більшість з яких мають літні майданчики, що розташовані прямо на березі моря.</p>
<p>Бердя́нська коса́  — коса на півночі Азовського моря , що відокремлює Бердянську затоку від моря. Одна з найбільших серед 27 кіс моря: її довжина — 23 км, а ширина лежить у межах від 60 метрів у середній частині до 2 кілометрів у кінці. Площа — 7 км². Своїм виникненням коса зобов'язана річці Берда , що впадає в морі на схід від її початку. Умовно її ділять на три ділянки — Ближня, Середня і Дальня. Ближня коса починається гирлом річки Берди і закінчується біля солоного лікувального Червоного озера на території Бердянського курорту.</p>
<p>На цій ділянці розміщено шість відносно великих озер: Бердянський або Солодкий лиман , озеро Кругле і Мазанкове, Довге, Краснопере, Велике і Красне , які є джерелом лікувальних грязей. Сучасна екологічна ситуація на косі дуже гостра, бо вона майже повністю забудована — в основному будівлями курортно -рекреаційного призначення. На Бердянській косі розташовано 70 оздоровчих установ: 8 санаторіїв, 17 дитячих і спортивно-оздоровчих установ, 45 баз відпочинку. Внаслідок робіт з берегоукріплення відбувається руйнація берегової лінії, зокрема з осені року озеро Красний лиман з'єдналося з морем [1] , що призводить до безповоротної втрати грязів [2].</p>
<p>Також з початку х років мис Великий Дзендзик був розмитий та перетворився на острів [3]. Бердянська коса входить до переліку Водно-болотних угідь України , що охороняються Рамсарською конвенцією з  Найдавніші відомості про дану місцевість датуються V століттям до н. У той час Бердянська коса [4] [5] за іншою версією, Федотова коса [6] носила назву «Агарський мис» можливо, від грец. Агрія  — «надлишкова, багата».</p>
		
