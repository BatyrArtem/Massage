<p>Він знаходився на місці теперішнього кафе. В ньому виступав духовий оркестр, який складався з місцевих жителів. Навчання селянських дітей в містечку Райгород роз­почато з року, а в році відкрита церковно-приходська школа, для якої було збудовано спеціальну будівлю, яка збереглася до цього часу. Приміщення школи були малими, тому в році добудували корпус для молодших класів, та хімічний і фі­зичний кабінети, столову й майстерню, а в році побудували спортзал. На даний момент в школі навчається учнів, працює 34 педагогів та технічних працівників.</p>
<p>В році населення в Райгороді становило душі. Після революційного перевороту селян почали об'єднувати у товариства по спільній обробці землі. В році тут було таке товариство під назвою «Червоний борець» Ситковецького району,товариство в тому році обробило 22,39 десятин орної землі. Всієї землі в колективі було 25,09 десятин. Так на роки було заплановано посіяти 6 десятин цукрових буряків.</p><strong><a id='a2'></a><h2>Вінниччина</h2></strong><p>В році було товариство бурякове під назвою «Червоний шлях», в якому на початку року було селян-плантаторів. В цьому ж році в Райгороді була створена артіль кооперативів по переробці продуктів садівництва, городницт­ва і бджільництва. Ця артіль належала до Ситковецького садово-городнього кооперативного товариства.</p>
