<p>В південній частині села є великі поклади граніту. В році розпочато видобування каміння і пізніше пере­творився в передовий Самчинецький щебеневий завод. Спочатку всю роботу виконували вручну. На вагонетки, що йшли до залізничної колії, носили каміння носилками. З р. Райгород та с.</p><a id='a3'></a><h3>Сільську мультимедійну бібліотеку відкрито в Іллінецькій міській ОТГ</h3><p>Семенки, де на даний період працюють робітники. На початку х років почали прокладати заліз­ничну колію, а згодом збудували станцію і депо. Залізнична станція працює і до сьогодні, де працюють 28 чоловік. У році було організовано колгосп, першими ор­ганізаторами якого були Мельник Прокіп Денисович, житель села Райгород, який і був першим головою. В році була організована Рада Робітничих і се­лянських депутатів. Першим головою був Міченко Тимофій Тимофійович. Колгосп села Райгород з року в рік розвивав своє го­сподарство, а разом з тим збільшував і економіку.</p>
<p>Підвищувався і культурний рівень населення села. До скла­ду Райгородської сільської ради і колгоспу увійшло невеличке село Слобідка, де була створена окрема бригада. Чорною хмарою насунувся на наше село рік. Трактами, широкими і вузькими шляхами, стежками та межами на Поділля пробирався голод. В селянських засіках, спорожнілих до зернини, не було навіть мишей. Люди дої­дали картоплю і буряки, гарбузи і кваснину.</p>
