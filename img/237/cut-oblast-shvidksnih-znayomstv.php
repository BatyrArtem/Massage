<p>Серед них було і Полонне, недарма літописець називає його «святої Богородиці град Десятинний». Археологічні дослідження — років, проведені в Полонному археологічною експедицією Кам'янець-Подільського педагогічного інституту під керівництвом академіка Винокура І. Укріплення знаходилося в районі сучасної ветеринарної лікарні, на природному підвищенні, яке оточене водами річки Хомори і фактично є півостровом. Під час проведення міжнародної науково-практичної конференції червня року «Місту Полонному — років» академік Іон Винокур заявив таке: «Що ж стосується історії Полонного епохи Київської Русі, то, крім літописних згадок про місто, ми маємо на основі археологічних розвідкових робіт — років матеріали, які датуються від Х до ХІІІ століть… …Саме з роком можна пов'язати найдавнішу згадку про Полонне».</p>
