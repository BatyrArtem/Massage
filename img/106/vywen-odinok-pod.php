<p>Також є збагачувальне виробництво — дезінтеграція та обезшламлювання розсипу, гравітаційне, електричне та магнітне збагачування, металургійне виробництво — хіміко-металургійні процеси переробки концентратів із виділенням сполучень цирконію, сульфату цирконію, чотирихлористого кремнію. Сьогодні комбінат випускає понад 50 видів продукції, яка експортується за кордон.</p>
