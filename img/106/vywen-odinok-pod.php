<p>У заході взяли участь ветерани-афганці Сергій Черниш та Ігор Миронюк, учасник бойових дій Андрій Волков. Всі вони працюють у коледжі. Разом зі студентами, викладачами та бібліотекарями навчального закладу вони говорили про патріотизм, про мужність та звитягу наших воїнів, про сучасну суспільно-політичну ситуацію в країні та світі, ділились спогадами про пережите.</p><img src="https://p.calameoassets.com/130824135517-0cf71d9aacf42bdf8bda4a8f9ad7e411/p1.jpg" alt="" style="max-width: 480px; max-height: 480px;"/><p>ЗВЕРНЕННЯ МІСЬКОГО ГОЛОВИ ВОЛОДИМИРА ВАСИЛЕНКА З НАГОДИ ДНЯ ВШАНУВАННЯ УЧАСНИКІВ БОЙОВИХ ДІЙ НА ТЕРИТОРІЇ ІНШИХ ДЕРЖАВ. Ми віддаємо данину поваги мужності та героїзму воїнів-афганців, їхніх побратимів, які виконували свою непросту місію в більш як двадцяти країнах світу. З кожним роком все далі історія віддаляє нас від вогняних років афганської війни. Вони не з чуток знають справжню ціну війні та миру як тоді, так і зараз, усвідомлюючи всю відповідальність за долю прийдешніх поколінь. Низький уклін всім миротворцям, всім нашим героям.</p>
<p>Лебедина вірність… Про неї співають, передають через покоління легенди, складають вірші. Тема добре відображена і в художньому мистецтві, і в народних ремеслах. З давніх давен лебеді є й на наших вишитих рушниках як символ чистоти відносин та вірної і вічної любові. Саме гарній і важливій для людських стосунків темі любові присвячена виставка у бібліотеці Вільногірського коледжу Національної металургійної академії України.</p>
<p>Організатори втілили ідею як р аз напередодні Дня всіх закоханих. І студенти коледжу, і учні Західно-Дніпровського центру професійно-технічної освіти, які із задоволенням долучились до культурно-виховного заходу, знайомляться з раритетними та сучасними статуетками лебедів з кераміки, кришталю та скла. Декілька виставкових експонатів виготовлено у техніці оригамі. Вражають красою і картини, написані фарбами та розшиті бісером. Лебедина пара, створена із звичайного пінопласту, стала дійсно прикрасою виставки, настільки майстерно автор передав у ній спектр піднесених почуттів і краси.</p><strong><a id='s4'></a><h2>Вільногірськ — Візіком карти</h2></strong><p>А книжкова тематична виставка спонукає молодь до знайомства зі світовою літературою, відкриває світ сили слова і краси душі. Не випадково ми говоримо тут «любов», а не «кохання», адже любов має більш широкий і ємний зміст. Саме за цю велику любов, відданість справі та невтомну працю з молоддю вдячність всім, хто працював над цією виставкою: бібліотекарам, викладачам, студентам, батькам. лександр Слобожан, член Правління АМУ Дніпровський міський голова Борис Філатов, виконавчий директор Дніпропетровського РВ АМУ Олена Молошна, голови міських, селищних, сільських рад - члени Асоціації.</p>
