	<p>Шевченка під керівництвом Миколи Бандрівського та Романа Сулика виявило сліди поселення в межах рубежу XI —середини XIII століття , що підтвердило припущення про існування поселення в Стрию в княжі часи. Одним із науковців, які припускали постання Стрия як міста в цей період, був Лев Шанковський.</p>
<p>Він вважав, що «місто мусіло мати вже велике значення в оборонній системі князя Ярослава Осмомисла… Водночас місто Стрий мало теж велике економічне значення» його торговельний характер підтверджує герб, що на ньому зображений мандрівний купець , адже було розташоване на перетині шляхів Київ-Угорщина, Галич-Угорщина та Коломия-Перемишль-Шльонськ [15]. Перша офіційно визнана письмова згадка про Стрий датується роком, коли місто уже було центром волості.</p>
<p>А перша відома згадка про Стрийське староство датується роком. Король Владислав Ягайло на початку XV століття уступив Стрийський повіт своєму братові Свидригайлові , а той незабаром передав ці землі князеві Федору Любартовичу. В руках князя Стрийська волость залишалась до року. Між та роками місто було повністю знищено монголо-татарами , тому своїм привілеєм 21 жовтня року Владислав II Ягайло дозволив шляхтичу-лицареві Закліці Тарлові з Щекаровичів [16] наново заснувати місто і розвивати його на Магдебурзькому праві.</p>
<p>Північно-західний кордон міста проходив по сучасній вулиці Т. Шевченка біля парку ім. Нижанківського , а за цією межею пізніше було закладено міський цвинтар. За привілеями наданими місту Ягайлом було визначено будівництво різних споруд, зокрема «суконниць» на Ринку, встановлення конкретних днів для торгівлі та звільнення міщан на 10 років від усіх податків і данин. Після смерті Владислава ІІ Ягайла у році у Стрия відібрали Магдебурзьке право , а поновлене воно було у році королем Казимиром IV Ягеллончиком під час перебування його у Львові.</p>
<p>Стрий почав розвиватися як потужний торговельний центр на перехресті двох шляхів — найважливішого через Карпати Львів  — Мукачеве та поздовжнього — на Передкарпатті. Особливо інтенсивно розвивались торговельні зв'язки з Угорщиною. Запорукою цього було те, що цим привілеєм міщани звільнялися від юрисдикції воєвод , каштелянів , старостів та інших королівських урядників. Міщан було піддано судові війта , який був підзвітним тільки королівському судові.</p>
<p>У році Стрий вкотре був знищений набігами татар. Для оборони від набігів у місті в XVI столітті побудований Стрийський замок , як оборонна споруда він проіснував до XVIII століття, також був земляний вал , що оточував місто і передмістя, частокіл , що відокремлював передмістя від центральної частини. Правами міщан користувались тільки ті, хто володів нерухомістю у самому місті юридично це була площа Ринок і прилеглі до неї вулиці. У передмісті Лани жила громада, що не мала прав міщан, але підпорядковувалась магістрату , на Підзамчі біля замку мешканці були підвладними королівського старости , в передмісті Війтівство — стрийських війтів, на Шумлявщині -українських шляхтичів Шумлявських.</p>
<p>Саме в середині ХVI століття Магдебурзьке право в Стрию набуло класичної форми. Влада в місті належала магістрату , який складався з ради, на чолі якої стояв бургомістр , і судової лави, очолюваної війтом. Резиденцією старост та їх намісників був Стрийський замок , а магістрат урядував у ратуші. До компетенції ради входила посередницька діяльність між державою та міщанами, поліцейський нагляд у місті, розподіл земель, здача в оренду міського майна, встановлення мита, опіка над вдовами і сиротами, затвердження цехмістрів тощо.</p>
<p>Однією з важливих функцій магістрату було надання міського права, себто прав стрийського міщанина. Воно надавалось на підставі рекомендаційних листів і не було спадковим. Навіть народжені в місті по досягненню повноліття мали формально набути прав міщанина, а неодружені молодики такого права не отримували взагалі. У ХVI столітті розвинулись склярський, бондарський , кушнірський , кравецький і ткацький, солодовицький цехи, кілька млинів, які мололи зерно та дробили дубову кору для вичинки шкіри. У стрийському музеї «Верховина» збереглися два унікальні документи, датовані та роками.</p>
		
