	<p>Для оборони від набігів у місті в ХVІ ст. побудований замок. Як оборонна споруда він проіснував до ХVІІІ століття. В часи середньовіччя Стрий знаменитий як торговий центр. Це спричинило розвиток ремесел. В місті виникають і діють цехи кушнірів, гончарів, ткачів, шевців. Передмістя Стрия займається сільським господарством.</p><strong><a id='d1'></a><h2>На фестивалі у Миколаївському районі пропагуватимуть відпочинок без алкоголю</h2></strong><p>У середині ХV ст. серед населення Стрия переважають українці. У місті в ті часи було 5 українських церков і 4 братства; діють братські школи. У р. Стрий знищений ще одним спустошливим набігом татар. Внаслідок поділу Польщі у  р. Стрий разом з цілою Галичиною потрапляє від владу Австро-Угорщини.</p>
<p>Під впливом революційних подій  р. в Європі у Стрию організовується українська інтелігенція і утворює окружну Руську Раду. У роках у Стрию будують залізницю. У місті створюються і діють паровозоремонтні майстерні, деревообробні підприємства, фабрика сірників "Ватра", підприємства для виготовлення і обладнання нафтодобувної промисловості. Громадське життя українців Стрия пожвавлюється аж під кінець ХІХ ст. Зручне розташування на перехресті міжнародних залізничних та автомобільних шляхів, а також близькість курортів Моршина , Трускавця і Славсько та рекреаційної зони Карпат відкриває перспективи для розвитку міста як торговельного та рекреаційно-туристичного центру.</p>
<p>Стрий є центром Стрийської агломерації , яка разом із Дрогобицькою агломерацією утворює Дрогобицько-Стрийську конурбацію [5]. Клімат  — помірно-континентальний. Головними чинниками формування клімату є сонячна радіація , атмосферна циркуляція та характер місцевості.</p><strong><a id='d2'></a><h2>Стрийський район</h2></strong><p>У Стрию в середньому налічується на рік всього 50 сонячних днів, хмарних і днів [6] із перемінною хмарністю. Зима порівняно тепла, з частими відлигами , середня температура січня  — 4 °C, але в окремі роки бувають морози і поза 30 °C. Річна кількість опадів у середньому сягає — мм, найбільше опадів у літні місяці , найменше — взимку. Колись Стриєм протікало декілька водяних потоків, що впадали у річку. На них працювали водяні млини. Для відпочинку жителів міста облаштований пляж на річці та діє відновлене озеро. Найбільшою річкою Стрийщини є річка Стрий загальна довжина — км.</p><strong><a id='d3'></a><h2>СТРИЙСЬКА РАЙОННА ДЕРЖАВНА АДМІНІСТРАЦІЯ.</h2></strong><p>Течія річки швидка, часто змінює русло, а також рве дамбу у Стрию вздовж об'їзної автомагістралі. Особливо небезпечна річка у паводкові дні. Тоді найвищий рівень води становить 4—5 метри. Паводки тривають 5—15 днів.</p><a id='d4'></a><h3>Графік роботи:</h3><p>Найбільші повені були у , , , роках. Зокрема повінь року стала однією із причин голоду року на Стрийщині. Остання велика повінь сталася в році. Друге археологічне дослідження Ланів Долішніх відбулося 22 травня року. Його проводила Археологічна комісія Наукового товариства ім.</p>
		
			
