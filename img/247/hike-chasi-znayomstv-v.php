	<p>Найприкріше те, що навіть якщо королева не помирала під час пологів і дарувала країні і чоловікові спадкоємця, то на цьому її поневіряння не закінчувалися. Немовля відразу ж показували народу і урочисто хрестили. Потім на честь радісної події влаштовували лицарські турніри, театралізовані дійства та народні гуляння. На святі міг бути присутній останній жебрак, тоді як головна винуватиця урочистості була змушена ще шість тижнів ховатися від сторонніх поглядів і поверталася до своїх королівських обов'язків лише після спеціального "очищення" в церкві.</p>
<p>Втім, треба визнати, процес пологів був таким брудним і неприємним, що очищення дійсно зашкодити не могло. У це важко повірити, однак аж до середини XVII століття жоден лікар англійським королевам при пологах не допомагав. Для цього існувала невелика армія повитух, няньок, фрейлін - і взагалі будь-яких жінок, які могли хоч якось допомогти.</p>
<p>Головну роль, звичайно, відігравали повитухи, які мали відповідати найсуворішим вимогам. Вони мали довести, що володіють достатніми знаннями і що ніколи не були помічені ні в чому поганому або протизаконному.</p><a id='k2'></a><h3>Other cities Zaporizhia</h3><p>Як ви розумієте, не могло бути й мови про те, щоб довірити народження майбутнього монарха будь-кому. Крім того, кожна з них мала присягнути на Біблії, що не спробує потайки поцупити пуповину або плаценту, оскільки і перша, і друга були абсолютно незамінними компонентами для виготовлення чаклунського зілля. Найстарша донька королеви Вікторії і принца Альберта, також Вікторія, була першою королівською дитиною, при пологах якої - року - був присутній батько. Першим чоловіком королеви, який був присутній при пологах, став чоловік Вікторії, принц Альберт.</p>
<p>Сама вона не заперечувала і навіть написала про це в щоденнику: "Не можна було знайти добрішої, мудрішої, розважливішої і спокійнішої сестри милосердя". На той час лікарі-чоловіки вже допускалися до королівських пологів, проте їм було суворо заборонено дивитися на пацієнтку. Торкатися можна, а от дивитися - ні. Автор фото, Wikimediz Commons.</p><strong><a id='k3'></a><h2>Online Dating in Polohy, Zaporizhia</h2></strong><p>Марія Моденська з сином, який так і не став королем Яковом III: країна просто відмовлялася вірити, що він є королівською дитиною. Після громадянської війни, правління Кромвеля та відновлення на престолі сина страченого Карла I до монархів в Британії перестали ставитися з належною повагою. Навіть "плід королівського шлюбу" перестав прийматися на віру.</p>
<p>Тобто ми, звичайно, бачимо, що це дитина, а тепер доведіть, що вона дійсно королівська! Таке скептичне ставлення до репродуктивності монарха було дуже корисним, якщо не подобався сам монарх.</p><a id='k4'></a><h3>З чого почати? Зі знайомства з листом</h3><p>У чому на власному досвіді переконався останній з Стюартів, Яків II, якого багато підданих дуже не любили за те, що він був затятим католиком. Коли його другій дружині, Марії Моденській, прийшов час народжувати у короля вже було дві доньки, але хлопчик отримував пріоритет престолонаступництво перед сестрами , країна була переповнена чутками, що королева зовсім не вагітна, а народ хочуть обдурити, потайки протягнувши до її опочивальні якесь стороннє немовля. Автор фото, Hulton Archive. Якова на престолі змінили його донька від першого шлюбу Марія і її чоловік Вільгельм Оранський.</p>
<p>Щоправда, всі їхні 18 дітей померли ще в дитинстві. Інші авторитетні джерела запевняли, що їм добре відомо: королева вже народила мертве немовля, і тепер готуються його підмінити. Для того щоб покласти край чуткам, вирішили запросити до палацу 70 шанованих людей, які змогли б підтвердити, що своїми очима бачили, як це дитя народилося у цієї королеви.</p>
