<p>І я повірила», — розповідає жінка. Стосунки поновилися, але дедалі погіршувалися. Чоловік постійно ревнував, скандалив. І сталося так, що майже відразу після цієї розмови я завагітніла». Дізнавшись про це, офіційна дружина зробила свій крок. Вони почали їздити по ворожках, які це «підтверджували». Він приходив додому і бив мене. Казав: ти мене приворожила», — розповідає Вікторія. Після цього вагітна жінка остаточно пішла. Чому не поїхала з міста? Гадала, що він врешті заспокоїться. Але так не вийшло». Чоловік позбавив Вікторію абсолютно всього: забрав усі гроші, кредитні картки, золоті прикраси.</p>
