<p>Живеш з батьками, значить, все, свабодєн, йди нафіг.</p><strong><a id='e3'></a><h2>Київстар в м. Вознесенськ, вул. Київська, 3-А</h2></strong><p>Одна мені чітко сказала, що в неї є машина, вона мобільна і хоче теж зустріти такого мобільного. З Києва дівчата, як правило, при машинах, всі наворочені, в дорогих шмотках. Але вони чотко пишуть — "да, якщо ти щедрий, з баблом, то приїжджай хоч зі Львова, хоч з Ужгорода, мені пофіг". Ще з однією спілкувався і вона потім зізналася, що на сторінці фото не її власне. Вона, типу, заміжня і поставила якусь крадену аватарку з метою конспірації. Але, каже, "я подібна на ту, хто на цьому фото". Я її попросив показати своє справжнє обличчя, вона виставила реальну фотографію і відразу ж стерла. Сказала, що їй байдуже чи я одружений, чи ні.</p>
<p>Вона шукає просто чоловіка для приємного проведення часу. От мені вдавалося познайомитися з жінкою з малого містечка на Львівщині.</p><strong><a id='e4'></a><h2>Гола правда про сайти знайомств та їх відвідувачів</h2></strong><p>Приїхала на машині, вся така на понтах. Ну і зразу зрозуміла, що я ніякий не багатий. Я теж зрозумів, що кастінг не пройшов, ну добре.</p>
