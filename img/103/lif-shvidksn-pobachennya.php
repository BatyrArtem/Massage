<p>Під час подій Першої світової війни і громадянської війни у Нікополі неодноразово змінювалася влада. Після Жовтневого перевороту року в Нікополі у січні року створюються органи влади під керівництвом партії більшовиків. За Берестейською угодою влада УНР дозволила увійти на свою територію німецько-австрійським військам і у квітні року австрійці зайняли місто Нікополь. З листопада року внаслідок революції у Німеччини австрійці покинули місто і владу захопили більшовики. У червні року в місті відбулося народне повстання Троїцьке проти комуністичної влади, яке було потоплено у крові угорцями і китайцями.</p><strong><a id='u2'></a><h2>Нікопольський факультет (м. Нікополь)</h2></strong><p>У серпні року Нікополь окупували війська Добровольчої армії під командуванням генерала Антона Денікіна. Але у вересні в місто увійшла Українська народно-повстанська армія Нестора Махна. До січня року Нікополь був центром анархістської республіки. В січні року знову захоплений Червоною армією і встановлено радянську владу. У жовтні того ж року до міста підійшли війська під командуванням Петра Врангеля і на декілька днів захопили місто.</p>
<p>Біля села Шолохове відбувся найбільший кінний бій Громадянської війни між 2-ю Кінною армією і кінним корпусом генерала Бабієва. Білі програли і 18 жовтня року з Нікополя їх витіснили війська більшовиків. Після входження до складу УРСР року Нікополь став центром однойменного повіту, з року — районним центром у складі Криворізької округи. Голодомор — років забрав життя тисячі нікопольців, фіксувалися випадки людожерства, лише допомога американської благодійної організації АРА допомогла вижити нікопольцям.</p>
<p>Станом на 1 січня року в місті проживало 9 жителів. Під час організованого радянською владою Голодомору — років помер щонайменше житель міста [10]. На його будівництві люди рятувалися від голодної смерті, але все одно у місті померло більш як 2 тисячі осіб. У квітні року почав діяти Нікопольський південнотрубний завод , який згодом став одним з найбільших виробників прокатних труб у СРСР. Значно розширили виробництво інші 23 промислових підприємства.</p>
