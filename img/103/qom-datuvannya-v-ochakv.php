<p>З історією Очакова пов'язане ім'я козацького ватажка Семена Палія  — керівника вдалого набігу на Очаків запорожців  року. Російська імперія розглядала Очаків як стратегічний форпост на північному узбережжі Чорного моря. Російські війська фельдмаршала Б. Мініха в році обложили й захопили місто, але рік потому змушені були покинути — за умовами Белградського договору року Очаків залишився за Османською імперією.</p><strong><a id='a3'></a><h2>Каборга — Вікіпедія</h2></strong><p>Після російсько-турецької війни — року Очаків став головною базою турецького флоту на північних берегах Чорного моря. Остаточно Очаків був захоплений 6 грудня року, коли Чорноморське козацтво під орудою кошового отамана Сидора Білого за допомогою війська під командуванням князя Потьомкіна штурмом захопили фортецю. Цьому передувала перемога Олександра Суворова , що під його орудою діяли козаки отамана Антона Головатого, звитяга яких прискорила перемогу Російської імперії в війні, коли козаки з меншими силами знищили турецький десант, який 1 серпня висадився на Кінбурнській косі.</p>
<p>У битвах при форті Кінбурн, у Дніпровсько-Бузькому лимані, при облозі й штурмі Очакова своєю безстрашністю й героїзмом відзначилися загони козаків Чорноморського війська , очолювані кошовим отаманом Сидором Білим , а після його загибелі — Захаром Чепігою. Загальна кількість Чорноморського козацтва під час здобуття фортеці Очаків та оборони Кінбурнської фортеці сягала 20 осіб. У році на місці зруйнованої турецької фортеці було закладено місто Очаків, яке стало портовим містом, губернським центром Новоросії , району, що охоплював землі на північ від морського узбережжя між річками Південний Буг і Дністер.</p>
<p>Під час Першої світової війни на укріплення Очакова й Кінбурна покладалося прикриття Дніпро-Бузького лиману з моря. У листопаді  — січні року Очаків перебував у складі Української Народної Республіки. У радянські часи з по рік під керівництвом генерал-лейтенанта інженерних військ Дмитра Карбишева в Очакові було побудовано низку військових об'єктів: військовий порт, аеродром «Очаків», гідроаеродром «Каборга», у військову інфраструктуру було включено фортифікаційну гідротехнічну споруду на острові Первомайський.</p>
<p>Під час організованого радянською владою Голодомору — років померло щонайменше жителів міста [12]. У -х роках розпочалося будівництво Дніпро-Бузького гідровузла : насипано Східну гідротехнічну споруду площею 27 га , що викликало численні протести громадян, побудовано порт, виконано упорядкування узбережжя, побудовано Східна промислова зона. У місті Очаків на обліку перебуває 10 пам'яток архітектури та 30 пам'яток історії й одна — монументального мистецтва.</p><a id='a4'></a><h3>Місто Святого Миколая</h3><p>На його місці 23 серпня того ж року до Дня Державного Прапора України відкрили перший у Миколаївській області світловий фонтан. Зінченка під патронатом А. Лучки, військовослужбовців Очаківського військового гарнізону за кошти волонтерів та небайдужих мешканців міста Очакова відкрито меморіальний комплекс пам'яті загиблим воїнам АТО. Виконав роботу майстер Сергій Коршун. Вартість робіт склала приблизно грн.</p>
