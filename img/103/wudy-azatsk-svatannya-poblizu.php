<p>
<p>Однак працювали школи в цей час нерегулряно — більшість дітей мучилося голодом, організованим владою СРСР. Наприкінці ліквідували неписьменність серед дорослих. Цього ж року на базі вечірнього робітфаку, який створили , відкрили вечірню середню школу. Своє дозвілля жителі райцентру проводили у 4 клубах: 2 залізничних — імені 1-го Травня та імені Т.</p>
<p>Шевченка, при фабриці імені Петровського та при бурякорадгоспі. Водночас були закриті та зруйновані православні церкви, релігійні погляди людей нещадно висміювалися, а самі священнослужителі переслідувалися. У місті виходила районна газета «Прапор комуни», газета політвідділу Бахмацької МТС «Більшовицький шлях» та газета бурякорадгоспу «За соціалістичний врожай».</p>
<p>Всі вони обслуговували владу під час геноциду українського народу — років, старанно приховуючи факти масової мученицької смерті людей від недоїдання. Станом на рік населення міста Бахмача становило осіб, при чому значна частина — втікачі із сіл, знищених голодомором. Вони працювали на низькооплачуваних та некваліфікованих роботах, що було вигідно комуністичному режиму.</p>
<p>Друга світова війна одразу ж позначилася на житті міста. При міськраді було встановлене цілодобове чергування. Була утворена й комісія з надання допомоги евакуйованим, яких ешелонами вивозили на схід. Через Бахмацький залізничний йшли ешелони з біженцями, пораненими солдатами РСЧА , військовою технікою. Йшли ешелони, навантажені заводським устаткуванням, призначеним для евакуації.</p>
<p>Таких ешелонів було доволі на станціях Бахмач-Гомельский та Бахмач-Пасажирський. Ешелони не могли вирватися з вузла оскільки частина колій на об'їзних шляхах була пошкоджена нищівними авіанальотами німецької авіації. Зранку тут скупчилися ешелони з військовими, пальним.</p><strong><a id='f1'></a><h2></h2></strong><p>Поїзди з евакуйованими, біженцями, з заводським обладнанням, чекали відправки в тил. О другій годині німецька авіація розбомбувала це скупчення поїздів, перетворивши станцію Бахмач-Київський у руїни. Цистерни з пальним на рейках не горіли, а зривалися зі своїх колісних пар, горіла земля навкруги, місцями шпали колій повигорали вщент. Під час цього, пам'ятного для Бахмача, бомбардування постраждали й навколишні вулиці. На вулиці комуніста Петровського зараз Даньківський Шлях , яка проходила паралельно з коліями станції Бахмач-Київський не збереглася жодна оселя, усі будови вигоріли вщент.</p>
<p>Так само постраждала й вулиця Поштову, з іншого боку станції — було знищено поштове відділення, базарна площа й прилеглі садиби приватні та державні. Під час нальоту були розбиті також й інші міські приміщення: міської ради, електростанції, їдальні, деякі житлові будинки. Вдруге німецька авіація бомбардувала місто 21 серпня року. Цього разу бомбували станцію Бахмач-Пасажирський й прилеглі висілки. Під час цього авіанальоту також постраждали державні та приватні об'єкти.</p><a id='f2'></a><h3></h3><p>Але втрати були набагато меншими, ніж від бомбування 14 липня. Так прямим попаданням бомби було знищено щойно збудоване двоповерхове приміщення залізничної пошти, згоріли три великі державні будинки, які стояли навпроти станції Бахмач-Пасажирський. Бомба влучила також у автостоянку військового містечка, де вивела з ладу чотири автомашини і вбила двох солдат батальйону.</p>
<p>Були поранені й убиті мешканці. З цього дня Бахмач бомбували майже щодня.</p>
		
