<p>Сьогодні, 3 травня, в місті Вараш сталась дорожньо-транспортна пригода, повідомляють у місцевій спільноті. Повідомлення про загорання на території промислового ринку на вулиці Садова надійшло до поліції о другого травня. За інформацією штабу РАЕС з проведення карантинних заходів для запобігання захворюваності на COVID, на роботу вдома переведено працівників атомної станції, у відпустках — особи. У Рівненській області на контрольно-пропускних пунктах на в'їздах до села Тинне та міста Вараш продовжують дезінфекцію автомобілів.</p>
<p>На контрольно-пропускних пунктах міста Вараш і села Тинне продовжується цілодобова дезінфекція автомобілів. Комісія з питань техногенно-екологічної безпеки та надзвичайних ситуацій у Вараші прийняла рішення утриматися від відновлення роботи ринків у місті атомників. Принаймні поки що. За минулу добу на в'їзді до КПП міста Вараш та села Тинне Сарненського району рятувальники продезінфікували 79 одиниць транспортних засобів.</p>
<p>Заступника начальника Вараського відділу поліції ГУ Нацполіції в Рівненській області звинувачують у одержанні неправомірної вигоди за вплив на прийняття рішення особою, уповноваженою на виконання функцій держави ч. Петро Сидорчук зареєстрував петицію в Єдиній системі місцевих петицій із проханням відловити безпритульних собак у місті Вараш на Рівненщині.</p>
