<p>У дівчинки виявили сколіоз три роки тому.</p>
		
	<p>Український теле- і радіоведучий, автор і співпродюсер гумористичних шоу Сергій Притула розповів цікаву історію, яка сталась із ним та юним хлопчиком із міста Вараш, що на Рівненщині. Житель Вараша повідомив надзвичайників про те, що у його помешканні в технічний отвір каркасу пральної машини просунуло голову цуценя і не може звільнитис.</p>
<p>Враховуючи підвищення рівня захворюваності на грип та ГРВІ, відповідно до рішення комісії з питань техногенно-екологічної безпеки та надзвичайних ситуацій, у школах Вараша та Заболоття з завтрашнього дня призупиняють навчально-виховний процес. Близько 19 години 22 січня в місті Вараш на вулиці Соборна сталась дорожньо-транспортна пригода — річний водій легковика «Daewoo Lanos» наїхав на річного пішохода.</p>
<p>Геннадій Назарець зареєстрував на сайті Єдиної системи місцевих петицій звернення, у якому просить облаштувати в місті Вараш високу залізничну платформу. Працівники Вараського відділу поліції розшукують осіб, які ухиляються від правоохоронних органів та органів суду:. Учора, 30 червня, в місті Вараш сталася дорожньо-транспортна пригода за участі мотоцикла та автомобіля «Ауді»,.</p>
<p>Рівненський апеляційний суд скасував ухвалу місцевого суду про обрання річному жителеві Вараша запобіжного заходу у вигляді цілодобового домашнього арешту і постановив нову, якою задовольнив клопотання слідчого про взяття підозрюваного у вчиненні умисного вбивства під варту строком на 60 діб без визначення розміру застави.</p>
		
