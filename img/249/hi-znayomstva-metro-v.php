<p>Фото туру 4. Найближчі до вас офіси "Поїхали з нами". Тури на інші дати. Екскурсія до столиці Буковини - «Чернівці - маленький Париж». Ми потрапляємо в місто - яке не поступалося Відню, місто - де тротуари підмітали букетами троянд і де книжкових магазинів було більше, ніж кав'ярень.</p>
<p>Ви прогуляєтеся вуличками О. Кобилянської, М. Емінеску та Вірменською, які зберегли романтику колишніх часів, відвідаєте 5 площ, побачите міську ратушу, театр, будинок колишнього Ощадбанку, Католицький костел і Кафедральний собор, Вірменську і П'яну церкву. Далі нас чекає справжня перлина міста - резиденція митрополитів Буковини і Далмації, яку побудували з до року. У цій дивній за своєю красою споруді видно візерунки української вишиванки, буковинських килимів та гуцульської писанки, легко проглядаються елементи різних архітектурних стилів, які створюють органічну цілісність неповторного ансамблю.</p>
<p>Саме в цьому містечку починається Трансільванія. Ми познайомимося зі Старим містом, з головною площею і Ратушею, район Шей, міська брама і Чорна церква. Середньовічне місто, оповите гірськими схилами з усіх боків справляє незабутнє враження і залишає яскраві спогади одразу після першого знайомства. Сьогодні наша ціль — Національний парк Пятра Краюлуй, що в перекладі означає «Королівська скеля» і є найстарішим національним парком Румунії. Його природний ландшафт являє собою унікальний мікс з гірських ущелин, льодовикових озер і мальовничих долин.</p>
<p>На території парку ростуть понад видів різних рослин, багато з них занесені до Червоної книги. Ми вирушимо однією з гірських стежок до вершини Piatra Mica м. Гадаємо, зайве більше писати про красу навколо, адже ви самі все побачите! Масив Бучеджі, розташувався в південній частині Карпат в формі велетенської підкови. Готові до зустрічі з Сфінксом? Ні, в Єгипет нам не потрібно летіти! Скульптура Сфінкс, створена завдяки впливу вітру на крихкий піщаник, занесена в список 7 чудес Румунії. Адже це найвищий хрест 39 метрів , розташований на вершині гори.</p><img src="https://12dz.com/sites/default/files/styles/wery/public/avse_f/ava5/08448-4687-8586-47-4749.png?itok=TRUPhv0A" alt="Це не зливова каналізація" style="max-width: 480px; max-height: 480px;"/><p>Гайда наповнювати життя чудесами! Заявка на підбір туру. Ваші контакти. Куди поїхати. Отримуйте найсвіжіші гарячі тури на пошту. Мережа туристичних агенцій «Поїхали з нами». Заповніть форму і менеджер турагентства "Поїхали з нами" зв'яжеться з вами найближчим часом. Додаткова інформація. Підписатися на розсилку.</p>
		
	<p>Дякуємо за заявку! Поки менеджери з туризму обробляють ваш запит, підписуйтесь на сторінку «Поїхали з нами» в Facebook. Виберіть станцію метро. Мечникова, 16, офіс 1. Українки 28 Метро Печерська. Дякуємо за підписку! Для підтвердження перейдіть за посиланням, висланим вам на пошту. Найближчим часом менеджер компанії, «Поїхали з нами» передзвонить Вам.</p>
<p>Виявляється, з початку вагітності можна говорити з жінками про таку проблему, розповідати, як з цим боротися.</p><strong><a id='i1'></a><h2>Тиждень пригод – Україна & Румунія</h2></strong><p>Є достатньо спеціалістів, літератури. Організаторка провела багато освітніх заходів, запартнерилася з пологовими будинками, дитячими поліклініками. Попри те, що тема специфічна, для вузької аудиторії, ініціатива вдалася і була корисною. Проект з основ програмування для підлітків запропонував хлопець, який з 12 років вивчав програмування самостійно. Через неповнолітність його не зараховували на курси — він вирішив цю проблему самостійно.</p>
<p>Зібрав школярів, підлітків і вони разом навчалися онлайн і офлайн. Деякі ініціативи підтримали на національному рівні. Авторка фестивалю сучасної української академічної музики запартнерилася з Міністерством культури і отримала фінансування. На місцевому рівні органи влади не підхопили жоден проект. Випускники інкубатору продовжують свою діяльність навіть без системної підтримки.</p>
		
