	<p>Кінотеатр «Україна» у Черкасах розташований у центрі міста, колись він був класним і популярним, але з появою нових мультиплексів став втрачати позиції. Органи влади зрозуміли, що він не приносить прибутку і вони не можуть його фінансувати. У невеликих містах, як Черкаси, багато перспективних людей їдуть у Київ. Культура зможе зробити місто більш привабливим і конкурентоспроможним. Не тільки для туристів, але і для тих, хто тут живе.</p><strong><a id='i2'></a><h2>Місто можливостей: як «Промолодь» змінює Черкаси | Mistosite</h2></strong><p>Ми дуже зацікавлені, щоби такі простори не вмирали і отримували нове життя. Хочемо розробити нову концепцію, подумати, чим ця будівля може бути, якщо не просто комунальним кінотеатром. Працюватимемо з urban curators з Києва, а також місцевими архітекторами та експертами. Читайте також: Гейміфікація партисипації. Гра як інструмент залучення. Громадські організації затикають дірки, які держава не може закрити.</p>
<p>Наприклад, з кінотеатром міська адміністрація бачить два варіанти: здати приватному підприємцю не вказуючи призначення, щоб отримувати гроші за оренду і позбутися цього тягаря. Інший варіант — залишати його тягарем, щороку видавати гроші з бюджету і казати: «Що ж він такий не прибутковий, що ж ми його так довго фінансуємо». Ми знаємо, що є третій сценарій. В українських містах є приклади трансформації державних театрів чи музеїв. Мені дуже подобається Театр імені Лесі Українки у Львові.</p>
<p>Прийшла нова команда управлінців, змінився стиль роботи. У Черкасах є проблема з відкритими громадськими просторами. Немає достатньо приміщень для проведення заходів. Не звичайних конференц-залів, а чогось більш неформального. Деякі мешканці хочуть, щоби такий простір був у кінотеатрі.</p>
		
	<p>Дехто каже, що варто змінити репертуар кінотеатру. Є запит не лише на тип фільмів, які показують у мультиплексах. У березні плануємо провести воркшоп, зібрати ідеї онлайн і офлайн. Поки що рано говорити, що буде на місці «України», все ще в розробці. Органи влади не можуть знати все самі.</p><a id='i3'></a><h3>Зливова каналізація</h3><p>Ми намагаємося не йти на конфлікт і не доводити, що хтось розумніший, а хтось дурніший. Всі мають різні функції. Міська адміністрація — розпорядник, її завдання — утримувати місто в більш-менш нормальному стані. У нас, оскільки ми багато читаємо, їздимо, обмінюємося досвідом, завжди є ідеї.</p>
<p>Наша мета — побудувати діалог, підсилити міську адміністрацію там, де можливо. З перетворенням «України» так і відбувається. Нещодавно директорка кінотеатру написала у фейсбук і попросила скидати їй на пошту пропозиції щодо «України». Це відповідальний та інноваційний крок. Це максимум партисипації, на який вони зараз здатні, але добре, що так.</p>
<p>Тепер ми кажемо: у нас є ресурси, досвід, знайомства, давайте ми залучимо людей ефективніше. Вони відповідають: «О, давайте». Представники органів влади, комунальних підприємств готові до діалогу. Потрібно ставитися до них як до партнерів. Чи готова міська адміністрація фінансувати відновлення кінотеатру — невідомо.</p>
