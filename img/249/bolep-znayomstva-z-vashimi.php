<p>Более того,  руководители государства в Канаде  выступают  с трибуны  и в парламенте,  и перед народом,  как правило,  на двух языках. Половину речи  он  произносит  на  английском  языке,  а половину - на французском. Если вы помните, совсем недавно у нас был премьер-министр Канады Малруни,  который выступал  у  нас  по телевидению  и,  объяснив  тем,  что  он не хочет обидеть ни одну группу своих сограждан и поскольку его  речь  транслировалась  на Канаду,  он  половину  речи  произносил  на  английском языке,  а половину - на французском.</p>
<p>Кстати, французов там гораздо меньше, чем  у  нас  россиян. И  поэтому  кто бы ни пришел в это высокое кресло,  я думаю, что наш будущий Председатель должен обязательно провести линию,  чтобы подобная тема в нашем парламенте больше не звучала. Благодарю вас за внимание. С удовольствием  отвечу  на любые ваши вопросы.</p><a id='y2'></a><h3>Навігаційне меню</h3><p>Родигін В. Уважаемый Александр Сергеевич,  всех  волнуют  вопросы  повышения заработной  платы  работникам  социальной  сферы,  имеется в виду медицины,  культуры. Кроме того,  такой же вопрос стоит и в деле повышения    обеспечения   малообеспеченных   семей,   студентов, инвалидов. Как бы вы могли решить эту проблему или,  по  крайней мере, какие бы наметили пути?</p>
<p>И второй  вопрос. Если я вас правильно понял,  нужно сейчас уже форсировать выход на мировой рынок. Не значит ли это, что мы должны уже сейчас выйти из состава Союза? Первый вопрос - один из острейших, и нас уже упрекали по "горячему телефону", что мы его не касались. Спасибо, что вы его задали. Я лично считаю,  что мы должны отдавать себе отчет,  что эту проблему из центра не решить.</p><strong><a id='y3'></a><h2>Регіон. Білогірські історії.</h2></strong><p>Точно так же,  как  ее  невозможно было  решить  из  Москвы,  так точно не решить ее здесь,  в нашем прекрасном  здании,  в  этом  прекрасном  составе   и   с   нашей великолепной  исполнительной властью. Решение этой проблемы надо отдать на места, уважаемые товарищи.</p>
