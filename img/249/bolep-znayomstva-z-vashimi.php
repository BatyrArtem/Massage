<p>Я старался высоко нести знамя Украины, представляя ее на различных форумах в мире. И я не приемлю,  уважаемые, когда меня хотят  сделать  ущербным,  неполноценным  пасынком моей неньки по языковому  признаку. Это  негуманно,  неблагородно,  нечестно  и неконституционно. От  языкового  признака до любых других - один шаг. Его допустить нельзя. У  нас  в  парламенте  нет  языковых препятствий ни в работе,  ни в кулуарных общениях, ни в дружеских встречах. Наш гуманный украинский закон  предусмотрел,  что  все должностные лица со временем должны владеть украинским языком.</p>
