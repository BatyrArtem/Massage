	<p>Але наука — це не просто віра в красиві казки. Описуючи цю давно улюблену картину, навіть священнослужителі не хочуть помічати її абсурдності, хоча це саме так. Подібного не могло бути, оскільки жоден візантійський священик не міг здійснити такого порушення канонів, не накликавши цим гніву Константинопольської патріархії. Русь хрестилася не в 1, а в Х ст. Обряд хрещення вже тоді був добре розробленим, він здійснювався індивідуально і тільки в храмі. І лише далеч віків не дає змогу побачити гнів християнських ієрархів усіх церков, якби вони почули про подібне охрещення цілої країни.</p>
<p>Невипадково візантійські джерела мовчать про таку важливу подію, як хрещення народу, з котрим у Візантії були давні й тісні контакти. Жоден візантійський історик не відзначив такої важливої перемоги: залучення до своєї віри давнього й неспокійного сусіда. Чи випадково це? Адже ще в 1Х ст. І так розгорілося в них бажання та ревність до віри, що прийняли вони єпископа й пастиря і поважають християн з великою пильністю та усердям» Огієнко І. Українська Церква. Хоча Фотій відніс Русь до підданих і друзів, але «піддані», в свою чергу, не відзначили цієї події.</p><strong><a id='n2'></a><h2>Хрещення русі та створення київських митрополій. Реферат</h2></strong><p>Наш літопис описує похід перших руських князів Аскольда й Діра на Константинополь саме за часів патріарха Фотія, але нічого не говорить про хрещення, і лише зазначає, що на могилі Аскольда поставлено церкву, тобто опосередковано засвідчує, що Аскольд був християнином. Літописець не відмітив ані будівництва християнських храмів, ані хрещення частини русичів за часів Аскольда та Діра, не оспівав належним чином діянь на честь Христа першої княгині — християнки Ольги, і весь тріумф перемоги християнства над язичництвом віддав Володимиру. Чи випадкове це тенденційне тлумачення подій?</p>
<p>Отже, перший давньорусьський історик був церковним діячем і, здавалося б, мусив оспівати шляхи проникання «світла істинного вчення» з найдавніших часів Та літописець навмисне ігнорує навіть відомі йому події і викладає не стільки факти, скільки певну, домінуючу в Х1 — Х11 ст. Це була версія київського двору, оскільки візантійські хроністи, які віддали данину навіть найдрібнішим деталям тогочасної історії, нічого не повідомляють про хрещення Русі князем Володимиром.</p><img src="https://img01.obyava.ua/classified/0/1159/2375/200-kuz3rcovmhtvi3pj.jpg" alt="Опитування" style="max-width: 480px; max-height: 480px;"/><p>Лише на тлі соціально — економічного та політичного розвитку давньоруської держави можна правильно оцінити наявну суперечність. Русь під час князювання Володимира — це могутня держава, що характеризується розвитком феодалізму. Русь часів Володимира — найбільша з ранньофеодальних держав того часу, а за своєю територією переважала навіть Візантійську імперію.</p>
<p>Часи князя Володимира — це кульмінаційний момент розвитку Київської держави. Тому Русь приймає нову релігію не на засадах вірнопідданства, а на засадах самостійної, рівної всім іншим церкви. З огляду на свій самостійний та незалежний соціально — економічний стан Русь приймає християнство, не підпадаючи початково ані під яку політичну або ідеологічну залежність. Це стало предметом гордості Русі і київського літописця, але це не влаштовувало ані Рим, ані Візантію. Русь приймає не візантійський і не римський варіант християнства, а свій, власний.</p>
<p>Академік В. Греков зауважив, що «християнство на Русі… виявилося в кінцевому підсумку не візантійським і не римським, а руським» Греков Б. Киевская Русь — М. Деякі історики української церкви, як наприклад К. Панас, зазначають, що з самого початку «Українська церква стала частиною Вселенської церкви» Панас К. Історія Української Церкви. Розвиваючи цю теорію, М. Чубатий висунув припущення, що церковна життя Русі з самого початку може бути охарактеризоване як синтез.</p><a id='n3'></a><h3>Переваги знайомства онлайн</h3><p>Історія християнства на Русі — Україні. З патріотичних мотиві один літописець з гордістю пише, що апостолів на Русі не було, оскільки в той час молода церква вважала допустимим стверджувати: «Вихваляє ж похвальними словами Римська земля Петра й Павла, від них же увіровали в Ісуса Христа, Сина Божого; Азія та Ефес та Патм — Іоанна Богослова; Індія — Фому; Єгипет — Марка. Усі країни і міста, і люди шанують і прославляють кожне своїх учителів, які навчили їх православної віри. Похвалимо ж і ми по силі нашій малими похвалами велике й дивне створившого — нашого учителя й наставника, великого кагана нашої землі Володимира, онука старого Ігоря, сина славного Святослава» Іларіон.</p>
<p>З часом церква зміцнюється, прилучається до складних церковних стосунків, усвідомлює помилковість таких поглядів і шукає більш ґрунтовних своїх коренів. От тому учителем словінського народу є Павло. Від того народу — і ми, русь; от тому і нам, русі, є учителем апостол Павло» Там же.</p>
<p>У контексті цієї розповіді є такі слова: «Словіни-бо жили охрещені» Там же. Літописець послідовний у своїй розповіді і не його вина, що ми бачимо лише те, що хочемо бачити в його викладі. А хочемо ми бачити ми, що Русь прийняла християнство від Візантії, і не помічаємо того, що літописець торує інші шляхи.</p><a id='n4'></a><h3>А Ви вже підготувалися до локдауну?</h3><p>Це була довга й вперта боротьба за право почути про «велич Божу своєму мовою» Там же. Вся земля Руська складала одну митрополію, причім митрополит київський підчинався царгородському патріарху і то стільки, що патріарх ставив митрополита на Русі, часом навіть не питаюгися ні єпископів, ні правительства.</p>
<p>Такий порядок, очевидно, був недопустимий, і князі наші пробують часом обійтися без патріарха; так, князі Ярослав висвятив митрополита Іларіона у Києві самими єпископами, але патріархи постаралися, щоб це не увійшло у звичай, і присилали митрополитів і далі. Митрополити були спершу все греки. Те ж саме більш-менш було і з «крилосом» священиків при епископі. Він пише працю, в якій викладає погляди нових християн на їхнє місце серед християнських церков. Кулінарія 8. Література 3. Особистісний ріст 1. Масаж 2. Мода 1. Мотоцикли 3. Музика 5. Нумізматика 1.</p><strong><a id='n5'></a><h2>Гортання праворуч</h2></strong><p>Зброя 1. Полювання 1. Покер 0. Поезія 1.</p>
		
			
