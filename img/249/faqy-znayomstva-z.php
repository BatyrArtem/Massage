<p>Але як би там не було, а від Володимира Русь уже виступає християнською.</p><strong><a id='n1'></a><h2>Християнські знайомства</h2></strong><p>Хоч і не всюди певно християнство прийнялося зразу; спочатку, мабуть, тільки по великих містах. І ця картина є настільки близькою для нашого серця, що, можливо, навіть і гріх руйнувати цю ілюзію.</p>
		
