<p>В клубе верят, что не за горами времена, когда футболисты покорят самые высокие вершины. Что в такие минуты чувствуете? Я благодарен болельщикам за то, что они относятся ко мне с уважением и всячески поддерживают. Хотя я прекрасно понимаю: заслужить это уважение можно лишь благодаря максимальной, я бы даже сказал, запредельной отдаче в каждом матче.</p>
<p>Чувствую, что я в долгу перед ними. Тренеру  всегда нужно оправдывать доверие результатом. Понимаю, что на фан-секторе баннер может исчезнуть. На этот праздник как раз пришелся домашний матч с «Динамо». Мы облачились в вышиванки. Очень красивая и запоминающаяся акция получилась. Праздник не испортило даже поражение от динамовцев.</p>
<p>Болельщикам всегда интересно приходить на стадион, если они знают, что их ждет интересное зрелище. Без них в «Карпатах» наверняка не обошлось? В прошлом году мы заключили пари: если «Карпаты» завоюют право играть в Лиге Европы, один из наших тренеров Тарас Беззубяк сбреет усы. И что вы думаете? Был еще договор: в случае выхода команды в групповой раунд турнира тренер вратарей Владимир Васютик и тренер-селекционер Вячеслав Левчук и вовсе побреются налысо. Что и было сделано. К сожалению, нужного результата в Лиге Европы мы не добились.</p>
		
