<p>А еще подготовить квалифицированных игроков, способных громко заявить о себе в футболе. У каждого из них есть что почерпнуть, чему-то научиться. Пропустив несколько матчей в чемпионате и Лиге Европы из-за травмы, летний защитник «Металлиста» к матчу с «Черноморцем» готовится в общей группе. В интервью «СЭ» он рассказал, кто из игроков ЦСКА ему больше всего запомнился во время контрольного матча в Ватутинках, а также признался, что не теряет надежду получить вызов в национальную сборную Украины.</p>
