	<p>Наверное, выставится, когда в официальном матче забьет два гола. Жалко, что в матче с «Черноморцем» он не сможет сыграть из-за перебора желтых карточек, полученных в играх за дубль. Готовы к тому, что этот отрезок может стать определяющим для «Металлиста»? Если мы дважды обыграем шведов, то, по сути, гарантируем выход в плей-офф Лиги Европы. В чемпионате каждая игра тоже будет определяющей.</p>
<p>Считаю, нам по силам одинаково успешно играть сразу на двух фронтах. Собираетесь составить ему конкуренцию за место в основе? На тренировках мы с ним одинаково стремимся доказать, что достойны играть с первых минут. Ну а главный тренер доверяет тому, кто лучше готов.</p>
