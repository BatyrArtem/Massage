	<p>Інший військовий експерт, Ігор Козій , серед причин трагедії виділяє також відсутність чіткого плану виходу з оточення як документа , розірвані та перемішані бойові порядки різних підрозділів, що ослабило лінію оборони, незрозуміле поводження з резервами: зокрема те, що для деблокування українських частин двічі поспіль надсилали менші сили, аніж чисельність російських військ на конкретному напрямку. Утім, були і вдалі епізоди.</p>
<p>Так, артилерійські підрозділи 51 бригади, які в кінці серпня після втечі розвідки і піхоти ЗСУ опинилися сам на сам, замаскували артилерію просто на дорозі та з надблизьких дистанцій нищили техніку супротивника, а також взяли в полон декілька кадрових військових Росії, яких встигли передати СБУ.</p>
<p>А вже у квітні року головний військовий прокурор, заступник генпрокурора України Анатолій Матіос повідомив , що «слідством підтверджено, шляхом отримання документальних матеріалів, загибель під Іловайськом чоловік, поранення ». Раніше Матіос говорив про загиблих, частину з яких вдалося ідентифікувати. Андрій Сенченко , колишній народний депутат, у минулому скликанні парламенту очолював тимчасову слідчу комісію Верховної Ради з розслідування Іловайської трагедії. За його словами, останні два роки тривають маніпуляції довкола втрат під Іловайськом у році.</p>
<p>А ми, розслідуючи цю трагедію, враховували всі епізоди. І те, що передувало і зумовило трагедію — події в секторі «Д» протягом місяця. Далі — бої за взяття Іловайська. Дві спроби деблокування і прорив з Іловайського котла. Міністерство оборони найбільше замовчує якраз ці дві спроби деблокування, оскільки по суті спочатку перший батальйон, а потім зведену батальйонну тактичну групу ї бригади послали на вірну смерть — штурмувати позиції значно переважаючих сил ворога, — повідомляє Сенченко.</p>
<p>Їм військовому командуванню треба було зімітувати спроби деблокування. І це зробили ціною людських життів». За даними ТСК щодо Іловайської трагедії, понад людей загинули під час спроби взяти місто та в момент виходу з оточення, а також від ран у шпиталях. Жертв було би набагато менше, якби керівник Генштабу Віктор Муженко адекватно реагував на повідомлення про вторгнення підрозділів російської армії, стверджує Сенченко.</p>
<p>Вони ігнорували доповіді підлеглих про вторгнення. Наша комісія похвилинно встановила, хто, коли і про що доповідав, хто приймав ці доповіді та якою була реакція.</p><a id='j2'></a><h3>Звичайна операція</h3><p>Кадрові військові доповідали з передових командних і спостережних пунктів про склад і пересування російських колон. Входження російських військ було і до цих подій, але серпня це було масове вторгнення російських регулярних військ, колонами по сто одиниць бронетехніки, які не помітити було неможливо. Реагуючи на численні заяви депутатів, ветеранів бойових дій на Донбасі та громадських активістів про необхідність прискорити слідство і оприлюднити всі подробиці подій серпня року, генпрокурор Юрій Луценко ще місяць тому запропонував депутатам оформити доступ до державної таємниці, щоб мати можливість ознайомитися з результатами розслідування та експертизи щодо Іловайської трагедії.</p>
<p>Таким чином через депутатів можна певною мірою ознайомити з матеріалами справи суспільство, пояснив свою думку Луценко. Чинна влада навмисне приховує подробиці Іловайської трагедії, бо великою мірою відповідає за неї, стверджує журналіст та військовий оглядач Юрій Бутусов , який веде хроніку війни, що фактично триває між українськими та проросійськими силами на Донбасі.</p>
		
