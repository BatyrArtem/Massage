	<p>Це, втім, не заважало молоді цілуватися та милуватися, проте свою цноту дівчата берегли. Колись уся громада зневажала дівчину, яка давала волю нестримному коханню. Траплялося, таких дівчат били мотуззям від дзвонів, намоченим у соляній ропі. В західних районах України майже до XX ст. побутував звичай фізично карати хлопця і дівчину за перелюбство. СВАТАННЯ сватанки, змовини, брання рушників, рушники, згодини, слово.</p>
<p>Починалося весілля сватанням, коли посли від молодого старости, свати, сватачі, посланці йшли до батьків обранки укладати попередню угоду про шлюб. Свататися було прийнято у вільний від польових робіт час на М'ясниці та від Паски до Трійці. Зі старостами до дівчини йшов парубок, на Поділлі — ходили і його батьки, а на Закарпатті — ще й брат або сестра. Досить складний обряд сватання вельми стисло описав Т. Шевченко: "Покохавши літо чи два парубок до дівчиного батька й матері посила старостів, людей добромовних і на таку річ дотепних.</p>
<p>Коли батько і мати поблагословлять, то дівчина, перев'язавши старостам рушники через плечі, подає зарученому своєму на тарілці або крамну, або самодільну хустку". Бували, однак, випадки, коли дівчина не давала згоди на одруження. На знак відмови вона повертала старостам принесений ними хліб або ж підносила молодому гарбуза чи макогона. Тоді про хлопця казали, що він ухопив гарбуза або облизав макогін.</p><a id='f2'></a><h3>Содержание</h3><p>Щоб уникнути сорому, часом посилали "розвідника", котрий мав довідатися про наміри дівчини та її батьків, або йшли свататися пізно ввечері, аби люди не бачили. СВАТАННЯ ДІВЧИНИ. Прийміть мене, мамо, я ваша невістка — такими словами, як не дивно, два століття тому дівчина починала сватання. Згідно з існуючими звичаями, вона мала на це таке саме право, як і хлопець.</p>
<p>А раніше хлопці лише подекуди наважувалися свататися до коханої. Сватання дівчини як відгомін матріархату здавна побутувало у різних народів і не суперечило традиційним шлюбним ритуалам.</p><img src="http://izium-cbs.edu.kh.ua/files2/images/2021_rk_robota_tsbs/tsentr_bbloteka/unnamed.jpg?size=11" alt="" style="max-width: 480px; max-height: 480px;"/><p>Але вже за середньовіччя цей звичай почав сприйматися як небажана форма сватання. Певний виняток становили українці. В їхньому середовищі він, у цей час при забутий, зажив новим життям. Цьому сприяло, зокрема, те, що через постійні війни чоловіки надовго йшли з рідної домівки, а сім'ю очолювали жінки.</p>
		
