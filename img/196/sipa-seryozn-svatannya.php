	<p>Дівчина пропонувала хлопцеві руку, не заручаючись його згодою, причому їй дуже рідко відмовляли, бо це могло накликати нещастя. Про силу цієї традиції свідчить звичай, що існував у XVIII ст. Проте в міру того як зростала роль чоловіків у суспільній сфері, такі звичаї поступово забувалися.</p><strong><a id='f3'></a><h2>Українські важкі ударні безпілотні комплекси: між "Соколом" та "Горлицею"</h2></strong><p>І вже наприкінці XIX ст. ініціатива у сватанні цілком перейшла до парубків, а роль дівчини звелася до сором'язливого колупання печі та подавання старостам весільних рушників. УМОВИНИ оглядини, розглядини, обзорини, переглядини — знайомство з господарством молодого, яке здійснювалося невдовзі після успішного сватання. Природа цього звичаю пов'язана з укладом селянина-трудівника, для якого господарство було основою його життєдіяльності. В ньому проглядається серйозність ставлення до утворення сім'ї: якщо молодий не мав свого господарства, то він не мав і права на створення родини. Батьки парубка намагалися якомога краще представити міцність господарства свого сина, демонструючи його так, щоб умовини завершилися шлюбною угодою.</p>
<p>Бувало, навіть вдавалися до хитрощів: позичали у сусідів коня, волів або сільськогосподарський реманент. Проте не в усі часи і не скрізь умовини сприймалися так однозначно. В цілому ж критерії оцінки обранця чи обраниці в середовищі українців базувалися на іншому грунті. З погляду тогочасної дівчини суджений мав бути передусім хазяїном, опріч того — врівноваженим, спокійним і непитущим, а суджена, з погляду хлопця, — працьовитою, справною господинею, а ще й чепурною та привітною.</p>
<p>З моральних якостей найбільше цінувалася доброта. Отже, сімейне щастя ототожнювали передусім із доброзичливими взаєминами між подружжям: Не потрібен і клад, якщо у чоловіка з дружиною лад. Про хиткість цього звичаю може свідчити довільність його дотримання: у "класичному" варіанті умовини передували заручинам, але могли поєднуватися з ними, проводитися після них або й зовсім не проводитися. ЗАРУЧИНИ полюбини, хустки, рушники, сватанки — заключний етап сватання, обрядове закріплення згоди на шлюб. Водночас це перший передвесільний обряд, що набував законної чинності.</p>
<p>На заручинах до молодої приходили разом із молодим його батьки та родичі. Всі сідали до столу, а молодих виводили на посад. Старший староста накривав рушником хліб, клав на нього руку дівчини, зверху — руку хлопця і перев'язував їх рушником. Після цього ритуалу молода перев'язувала старостів рушниками, а всіх присутніх обдаровувала хустками, полотном або сорочками.</p>
<p>У Карпатах цей ритуал здійснювала мати дівчини: вона обсипала молодих пшеницею та білою вовною і подавала їм мед, котрий символізував єдність молодих та їхніх родичів. Після усіх церемоній дівчина і хлопець вважалися зарученими і відтепер не мали права відмовлятися від шлюбу. Спроба відмовитися вважалася за безчестя, за відмову ж відшкодовували матеріальні витрати та ще й платили за образу. На ознаку того, що дівчина й хлопець засватані, вони отримували певні атрибути: наречений — барвінкову квітку, наречена — червону стрічку у косах або квітку на Буковині — траву. В західних районах більш поширеними були вінки, їх плели у так звані барвінкові дні.</p>
<p>Повна назва організації: Комунальна установа «Одеський художній музей». Міста реалізації: Одеса,.</p><strong><a id='f4'></a><h2>Изюм (город)</h2></strong><p>Опис проекту: Персональна виставка Валерія Басанця, одного з яскравих представників одеського нонконформізму — є продовженням виставкових проектів Одеського художнього музею присвячених українському мистецтву ХХ століття. Це також можливість продовжити діалог про опозицію офіційного та неофіційного мистецтво радянських часів. Мета виставки — дослідити складний історичний досвід неофіційного українського мистецтва радянських часів через оптику персональної виставки Валерія Басанця.</p>
