<p>
<p>Післявесільний цикл присвячувався вшануванню батьків молодими, прилученню невістки до родини чоловіка. Це обряди хлібин, свашин та гостин. ДОЛЮБНЕ СПІЛКУВАННЯ. Весіллю передували дошлюбні обряди, важливим компонентом яких була ритуалістика молодіжного спілкування. Українці кажуть: Як склався у людини шлюб, так піде і все її життя. Ця народна мудрість виходить з того величезного значення, яке надавали шлюбу й сім'ї, особливо у минулому, коли одружувалися на все життя і тільки з однією людиною.</p>
<p>Тож момент вибору судженого був дуже відповідальним. У виборі шлюбного партнера перепліталися різні інтереси. Дівчина прагнула зустріти обранця, котрий припав би їй до душі, батьки жадали помічника у домі, громада — голову міцного господарства.</p><strong><a id='f1'></a><h2>Изюм (город) — Википедия</h2></strong><p>Система дошлюбного спілкування передбачала певні норми залицяння та сватання, які майже до кінця XIX ст. грунтувалися на патріархальних засадах. Молодь спілкувалася переважно в межах свого села, а в містах — у межах вулиці, навіть свого "кутка". На рубежі XIX—XX ст. цей принцип був порушений і молодь нерідко шукала собі пару не тільки у своєму селі, а й у навколишніх. Хлопці та дівчата зустрічалися на вулицях, ігрищах, толоках, вечірніх сходах, ярмарках. Найпоширенішими формами дошлюбного спілкування були вулиці, забави, годенки, вечорниці тощо. На забавах молодь розважалася, співала, танцювала, водила хороводи.</p>
<p>У холодну пору збиралися в приміщенні на досвітки та посиденьки.</p>
		
			
