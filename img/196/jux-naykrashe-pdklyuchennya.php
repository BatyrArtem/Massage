<p>Корюківка з висоти. Автостанція у Корюківці є, але вона не працює. Сюди можна зайти погрітися, від дощу і снігу сховатися, почекати автобус. Однак, тут ви не придбаєте квиток. Уся інформація щодо рейсів автобусів у напрямку Чернігова і Києва з контактними номерами водіїв розташована на віконному склі будівлі. У Корюківці діє лише одна офіційна служба таксі — «Радіо-Таксі», де у диспетчера можна зробити заявку на поїздку. Приїжджають вчасно, працюють добросовісно. Поїздка містом обійдеться від 40 до 50 гривень.</p>
<p>За межі міста — 8 гривень кожен кілометр. До населених пунктів району по бездоріжжю — ціни за домовленістю з таксистами. Гостям міста пропонують свої послуги і приватники, які чекають на пасажирів на площі, частіше - на автовокзалі: Олег , Руслан , Слава , , Олександр. Міський автобус. З 15 жовтня по 15 квітня він курсує містом. Вартість проїзду - 3 гривні.</p>
		
