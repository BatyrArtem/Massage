<p>
<p>Від 1 червня в Україні поступово відновлюється рух потягів - і приміських, і  міжобласних. Про всі особливості функціонування залізниці розповів в. голови правління "Укрзалізниці" Іван Юрик під час брифінгу на щойно відкритому залізничному вокзалі. Після перемовин молодик здався правоохоронцям. Замість вибухівки мав муляж. На деякий час "підривник" паралізував рух мостом та курсування метро.</p>
		
			
				<div>Советский город Доброполье</div>
				<div><iframe width='560' height='315' src='https://www.youtube.com/embed/1KroSOntM7s' frameborder='0' allowfullscreen></iframe></div>
			
		
	<p>За фактом відкрито кримінальне провадження за ч. Міст Метро - це одна з переправ, якою рухається столична підземка. Як працює метро, чи можна зараз ним скористатися, і наскільки постраждали нерви пасажирів — у прямому включенні. Торік у вересні колишній військовий на ім'я Олексій Белько зупинився серед мосту Метро на легковому автомобілі і почав викрикувати, що планує підірвати міст.</p>
<p>Стріляв у повітря. Тоді також розгорнули масштабну спецоперацію. Чоловіку дроном доправляли рацію, на мосту чергували снайпери та броньована техніка. Розслідування справи триває досі. Надзвичайна подія у Києві: пообіді до поліції подзвонив чоловік, і повідомив, що планує підірвати міст Метро. Туди стягнулися значні сили правоохоронців: вибухотехники, кінологи та слідчі.</p>
<p>Міст закрили для руху транспорту, у столиці ввели спецоперацію. Зловмисника затримали.</p><a id='g1'></a><h3>Навігаційне меню</h3><p>Більше про війну на Сході України. Правоохоронці заявляють, що в результаті досудового розслідування зібрано достатньо доказів для оголошення підозри у вбивстві Шеремета та обрання запобіжних заходів Кузьменко, Антоненку та Дугарь. Що робити тим, в кого немає домівки? Проблема безхатченків і так завжди поза увагою влади, але тепер вони опинилися в над скрутних умовах. В усьому світі за останні роки рівень психічних захворювань невпинно зріс, а психологи та психіатри стали найбільш потрібними професіями в медицині. Однак в Україні щодо цього свого думка, а саме - другий етап медреформи, який передбачає масове закриття психіатричних клінік.</p><strong><a id='g2'></a><h2>Архів 1 червня року - </h2></strong><p>Будинки, які будували 60 років тому, розраховувались лише на 25 років і в дев'яності роки їх повинні були масово зносити, щоб побудувати нові. Але цього ніхто не зробив. Їх термін життя подовжили ще на років і проблему відклали до року. Езотерикиня Анастасія Лисицина каже, що якщо ваш коханець народився під знаком Близнята, то балакати у ліжку він буде чимало.</p>
<p>У Києві та області запрацювали камери автоматичної фіксації порушень. Поки що вони працюють лише на перевищення швидкості, хоча здатні фіксувати інші порушення. Загалом кількість підтверджених випадків інфікування в Україні перевищила 24 тисячі, 10 тисяч вже одужали. Більше про коронавірус в Україні. Спецоперація на мосту Метро у Києві: правоохоронці вже порушили кримінальне провадження за статтею "неправдиве повідомлення про замінування".</p>
		
	<p>Затриманий — річний житель Київської області. Небезпечних речовин при ньому не виявили. Американські астронавти вперше в історії прибули на МКС на приватному космічному кораблі. Його розробила компанія SpaceX винахідника Ілона Маска. Після 19 годин подорожі шатл «Crew Dragon» пристикувався до МКС. Американські астронавти проведуть на МКС місяці. З точною датою їхнього повернення на Землю у NASA ще не визначилися. Загроза теракту у Києві, викрадення українського військового, відновлення руху потягів та інше, - у випуску ТСН. В Україні дозволили роботу спортзалів та басейнів. Втім, клубам довелося піти на суттєві зміни у своїй роботі.</p>
<p>Потрапити на групове заняття можна лише за попереднім записом, і так, щоб на кожну людину в залі було не менше 10 м2. На вході всім відвідувачам міряють температуру, тренажери працюють через один. Обладнання мають дезінфікувати після кожного відвідувача. У Києві замінували міст через річку Дніпро. Як і торік, на цей раз мінер погрожував підірвати міст та висував свої вимоги. Проте, якщо торік, мінер вимагав зустрічі із дівчиною, то нинішній піротехнік взяв вище — забажав зустрічі з міністром.</p>
<p>Що ж сталося на Дніпровському мосту у столиці, поки кияни були на робочих місцях, дивіться про це в наступному сюжеті. Підозру оголосили 25 учасникам. Двом — заочно, їх ще шукають. Ще одному стрільцю запобіжний захід обрати не встигли, бо, як каже прокурор, сплив час, визначений на це законом. На засідання підозрюваний не прийшов, тепер його розшукує поліція. Коли обиратимуть запобіжні заходи затриманим у суді не знають. Інших клопотань поки що не отримували.</p>
<p>Ідеться про 42 швидкісних поїзди та далекого сполучення. Спочатку відновлюють рейси до Південних та Східних областей. Маршрути з Києва на  Захід рушать уже того червня. На вході температурний скринінг та маски. Напої провідники не пропонуватимуть, а от прибирати відтепер мають щогодини. На Львівщині, на єдиному пішому пропуску, де напередодні стояло кілька сотень подорожніх, нині рух відновився.</p><strong><a id='g3'></a><h2>Добропільський район</h2></strong><p>Черги виникли, бо заробітчани масово хотіли потрапити до Польщі. Але рух уповільнився через посилені карантинні заходи з польського боку. За ніч більшість подорожніх встигли перетнути кордон. Він служив на Чонгарі.</p>
		
	<p>Підозри про викрадення виникли, коли з уточненням особистих даних бійця звернулись росіяни. Американські астронавти вперше в історії прибули туди на приватному космічному кораблі. Як це було — у сюжеті. Мешканці села Якубів пережили землетрус. У момент трясіння вони займались звичайними справами і не зрозуміли, що сталося. Про коливання кори дізналися з інтернету. Останній землетрус такої потужності був 13 років тому. Спортсмени, яких карантин позбавив можливості брати участь у спортивних змаганнях, вирішили не сушити весла, а взятися прибирати міські водойми. За кілька тижнів розчистили частину русла річки Інгул і на прохання містян почали прибирати вкрай занедбане озеро у середмісті обласного центру.</p>
<p>Відсьогодні у Києві та області запрацювали камери автоматичної фіксації порушень. Поки що вони працюють лише на перевищення швидкості, хоч й здатні фіксувати інші порушення.</p><strong><a id='g4'></a><h2>Добропілля</h2></strong><p>Опівночі поліцейські опублікували відео з центру обробки даних. Лише за першу хвилину роботи столичні камери вполювали 47 порушників. Люди обурені загибеллю темношкірого чоловіка. Джордж Флойд помер після того, як поліцейський більше 8 хвилин притискав його шию коліном до землі.</p>
</p>
