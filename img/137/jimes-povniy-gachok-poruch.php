<p>
<p>Процитовано 6 серпень  Процитовано  Бугогардівська паланка. Бузьке козацьке військо  Соколи з року — місто Вознесенськ. Адміністративно-територіальний устрій Миколаївської області. Міста і смт Миколаївської області. Історичні населені місця Миколаївської області. Південний Буг. Корабельна , М. Енциклопедія сучасної України · Britannica онлайн.</p>
<p>Простори назв Стаття Обговорення. Головна сторінка Поточні події Нові редагування Нові сторінки Випадкова стаття. Портал спільноти Кнайпа Довідка Пожертвувати.</p><a id='r1'></a><h3>Навігаційне меню</h3><p>Посилання сюди Пов'язані редагування Спеціальні сторінки Постійне посилання Інформація про сторінку Цитувати сторінку Елемент Вікіданих. Вікісховище Вікіджерела. Герб Вознесенська. Прапор Вознесенська. Альтанка у Вознесенську. Миколаївська область. Вознесенський район. Південний Буг р. Цей ліс був посаджений у х роках ХІХ століття графом В. Ліс складається з двох урочищ: "Василева пасіка" і "Лабіринт".</p>
<p>Обидва вони охороняються законом, а з року одержали категорію державних заповідних урочищ. Засаджена лісом територія перетинається рікою з різними відкидними і підвісними містками з одного берега річки на інший, тому урочище і назвали "Лабіринт". Зараз урочище займає площу   гектар.</p>
<p>Найбільша цінність урочища - старі могутні літні дуби, їх більше  Масив ділиться на 3 частини, котрі непомітно переходять одна в одну. У самому центрі його знаходиться ставок - улюблене місце рибалок. Поблизу Трикратського лісу розташований   Актовський каньйон   — справжня перлина цих місць. Це унікальний природний комплекс лісової та водної екосистем у поєднанні з комплексом скель та гранітних валунів.</p>
<p>Річка Арбузинка ділить каньйон навпіл, створюючи ландшафти, що мають особливу екологічну, естетичну та історичну цінність. Актовський каньйон займає площу понад га. Цей куточок землі славиться далеко за межами району не лише своїм вражаючим тваринним та рослинним світом, а й чудовими пейзажами, створеними природою. Гранітні скелі сягають подекуди метрів заввишки. Тихі, незаймані води річок Мертвоводу та Арбузинки населяють дикі качки, чирки.</p>
<p>Тут годується сіра чапля, що прилітає сюди з "Василевої пасіки". У річці багато риби: плітка, короп, карась, щука, тараня, окунь. Лісовий масив "Рацинська Дача" розташований поблизу села Трудове Вознесенського району і займає площу більше гектарів. Тут ростуть дуби, клени, берези, липи та інші дерева і кущі.</p>
<p>Коли цвіте липа, її медовий запах наповнює все навкруги. Вчені порахували, що в ясний день у розпал цвітіння бджоли з одного гектара збирають близько тисячі кілограмів меду. Прогулюючись лісом, можна зустріти зайця, лисицю, їжака, почути спів солов'я. Гордість "Рацинської Дачі" - березовий гай. У лісі є ще чимало-мальовничих куточків. Заповідне урочище «Лабіринт» , що знаходиться біля села Трикрати Вознесенського району, пов'язано з ім'ям Віктора Петровича Скаржинського.</p>
<p>Місцевий поміщик, людина досить прогресивних для того часу поглядів, він прославився під час Вітчизняної війни року.</p><strong><a id='r2'></a><h2>Робота Вознесенськ, Миколаївська обл.</h2></strong><p>Спочатку воно існувало як парк. Тут було насаджено багато квітів, особливо кущів бузку, близько 80 видів. Нині державне заповідне урочище "Лабіринт" займає площу гектарів. Річка Арбузинка поділяє його на дві частини. Арбузинка в'ється між могутніми дубами, створюючи невеликі водяні перешкоди. Розбігаючись в різні боки, вона згодом знов зливається в єдиний потік. Найбільша цінність урочища - старі могутні літні дуби, їх тут більше  Урочище ділиться на три частини, котрі непомітно переходять одна в іншу. Це "Василева пасіка", "Миронів сад" та "Виноградний сад".</p>
<p>У самому центрі урочища знаходиться ставок-улюблене місце рибалок. Мешкають тут і фазани, й ліси, й зайці, й кабани. Але мисливство тут заборонене. Недалеко від Трикрат по обох схилах безіменної балки простягнулося на п'ять кілометрів ще одне урочище. Темним півколом цей масив охоплює село, немовби захищаючи від суховіїв. Це - Василева Пасіка. Тут ростуть дуби, осини. У центрі масиву звертають на себе увагу кілька суховерхих осин, суцільно обліплених пташиними гніздами.</p>
<p>Це - єдина в наших степах колонія сірих чапель. Чому чаплі облюбували цей куточок, неважко здогадатися. Поруч ставок, та й по балці вода тече, місцевість болотяна, а відтак, гарна кормова база. Навколо розкинулися поля, і восени легко піймати полівку. Визначну особливість має «Миронів сад». Це єдина у Миколаївській області колонія сірих чапель.</p>
<p>На деревах, майже на самих верхівках, десятки гнізд: самички висиджують пташенят, а самці сидять поруч на гілках або повільно кружляють неподалік. Цікаво, що гнізда сірих чапель можна побачити тільки в цьому місці, на інших ділянках обох урочищ не зустрінеш жодного. Колонія чапель знаходиться під охороною держави і має орнітоло­гічне та пізнавальне значення. Свою визначну пам'ятку має і "Виноградний сад". Це - могутній дуб. Але знайти його непросто. Ліс, збігаючи на дно яру, стає майже непрохідним.</p>
<p>Густі хащі, нагромадження скель, джерела з холод­ною кришталево чистою водою роблять ці місця казково прекрасними. У самій низині над заростями височіє могутнє літнє дерево.</p>
		
			
