	<p>На південній околиці міста в районі цегельного заводу здійснюється розробка покладів глини в невеликих масштабах, яка використовується для виробництва керамічної цегли. В околицях міста виявили також напірні сірководневі води з температурою на поверхні 39 °C, які можуть використовуватися для бальнеологічних та термальних цілей. Станом на  р. Етнічний склад мешканців однорідний. Місто Жовква було закладене в  р. Перша згадка в літописах про село Винники датується  роком. Винники лежали на межі Звенигородського та Белзького князівств , через територію яких пролягли торгові шляхи з Галича та Львова на Белз , Холм.</p>
<p>Розташування поселення на торговій дорозі сприяло його швидкому розвитку. Винники перейшли у власність гетьмана Станіслава Жолкевського , який згодом одержав королівський привілей Зиґмунта III Вази на будівництво нового міста та на право назвати нове місто Жовквою  р. З наданням Жовкві 22 лютого  р.</p><strong><a id='l4'></a><h2>Знайомства Жовква</h2></strong><p>Урядування в місті здійснював магістрат, який обирався за згодою власника міста. Для будівництва міста Жовкви була вибрана вигідна в оборонному відношенні, вільна від житлової забудови підвищена платформа, де перетиналися важливі шляхи Червоної Русі. Припускають, що гетьман С. Жолкевський із дружиною хотіли бачити розпланування Жовкви за взірцем «Ідеальних міст» італійського архітектора П'єтро Катанео. Планування здійснив Павло Щасливий, який тоді зводив замок , а після нього костел св.</p>
<p>В першій половині XVII ст. Жовква перетворилася на укріплене місто-фортецю , оточене валами та ровами. Ринкову площу , розплановану перед замком, з північного та східного боків оточили житлові кам'яниці з відкритими галереями-підсінням. Припускають, що своєю красою Жовква має завдячувати дружині гетьмана Реґіні, котра дбала про місто, у той час коли чоловік перебував на королівській службі. У II пол. У Жовкві діяли п'ять церков , чотири костели , синагога.</p>
<p>Славилась високою художньою цінністю жовківська збірка живопису , графіки , різьблення , гобеленів , порцеляни , зброї. Дослідник історії Жовкви вірменин Садок Баронч висунув версію про народження тут українського державного діяча і полководця, гетьмана Богдана Хмельницького. Під час визвольної війни українського народу під проводом Богдана Хмельницького у Жовкві двічі побувало козацько-селянське військо.</p>
<p>Під час Північної війни — рр. Жовківський замок з грудня  р. До Жовкви у квітні року приїздив український гетьман Іван Мазепа. Кароль Юзеф Сопіга восени року пробував бути посередником в суперечці між гетьманом литовським Михайлом Казимиром Радзивіллом «Рибонькою» та сандомирським воєводою Яном Тарлом в їх суперечці через права на Жовкву.</p>
<p>У Жовкві була велика друкарня, де друкувалася більшість служебних книжок для греко-католицької церкви. Піднесенню Жовкви сприяло прокладення через неї залізниці Львів — Белжець , адміністрація якої з  р.</p>
