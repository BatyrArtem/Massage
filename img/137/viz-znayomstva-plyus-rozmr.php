<p>
<p>А вже у році на території повіту були проведені перші вибори Жовківської повітової ради.</p><strong><a id='l1'></a><h2>Жовківський район</h2></strong><p>Згідно з адміністративно-територіальним поділом і переписом у році у Жовківському повіті було 3 міських та 68 сільських громад, всього — населених пунктів. Жовківський повіт у своїх межах проіснував до 17 січня року. Після з'єднання західно-українських земель з Українською РСР був змінений і адміністративний поділ. Саме тоді на території Жовківського повіту організовано три райони: Великомостівський , Куликівський і Жовківський.</p><img src="https://lovesvit.com/sites/default/files/styles/wery/public/avse_f/ava/picture-17955-1453158629.jpg?itok=W1VTGT0n" alt="Інформація про аукціон" style="max-width: 480px; max-height: 480px;"/><p>У цей час до Жовківського району входило 16 сільських рад. Друга світова війна залишила слід в історії району, оскільки з цим періодом пов'язана діяльність осередків Організації Українських Націоналістів та боївок Української Повстанської Армії , що активно діяли в межах району до року. Зі зміною назви Жовкви на Нестеров на честь російського військового льотчика П. Нестерова в році, змінено також назву Жовківського району на Нестеровський район.</p>
<p>У році Нестеровський район в тодішніх межах охоплював 1 міську й 11 сільських рад. У році до його складу увійшла частина ліквідованого Куликівського району. У році Нестеровському району повернено колишню назву — Жовківський район, який на сьогодні охоплює 3 міські ради: Жовківську , Дублянську , Рава-Руську , 2 селищні та 40 сільських рад.</p><strong><a id='l2'></a><h2>Жовква — Вікіпедія</h2></strong><p>У межах Жовківського району було створено 91 виборчу дільницю. Решта кандидатів набрали меншу кількість голосів. Адміністративно-територіально район поділяється на 3 міські ради , 2 селищну раду та 40 сільських рад , які об'єднують 55 населених пунктів і підпорядковані Жовківській районній раді. Адміністративний центр — місто Жовква , яке є містом обласного значення та не входить до складу району [6]. Розподіл населення за віком та статтю [7] :. Національний склад району станом на  р.</p>
<p>Загальна чисельність населення Жовківського району становить ,3 тис. Територіально-господарський комплекс Жовківського району спеціалізується в основному на виробництві хлібобулочних та кондитерських виробів , масла тваринного, твердих сирів , меблів , взуття , поліграфічної продукції, запчастин для автомобілів , дорожньої техніки, обладнання для доріг, лісозаготівлі і переробці деревини , виробництві сільськогосподарської продукції та її переробці. У районі налічується 25 промислових підприємств, з них 9 харчопереробних, 5 деревообробних, 5 металообробних, 2 поліграфічних підприємства тощо.</p>
<p>Крім цього, в районі працює потужний приватний сектор понад 22,3 тис. Протягом останніх років активно розвивається сфера бізнесу. У районі здійснюють діяльність малих підприємств та приватних підприємців. Районом проходить низка важливих транспортних коридорів, серед них автошлях E40 та E У районі працює Рава-Руський ліцей, Рава-Руська гімназія, Львівський національний аграрний університет у Дублянах, де свого часу вчився Степан Бандера , 77 загальноосвітніх шкіл , в яких навчається до 16 тис. Функціонує 29 дошкільних закладів, в яких виховується близько дітей, Жовківський і Рава-Руський центри дитячої та юнацької творчості, дитячо-юнацька спортивна школа та районний дитячий оздоровчий табір «Росинка».</p>
<p>Медичну допомогу населенню району надають дві районні лікарні у м. Жовква та м.</p>
		
