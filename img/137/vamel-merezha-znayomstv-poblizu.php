	<p>Погрожували журналістам приватні особи, методом повідомлень через соцмережі або телефонний дзвінок. Заклики до розправи над журналісткою вони залишили під постом Андрія Князєва Позивний Княже. Андрій Князєв звинуватив Дарину Синицьку у розповсюдженні особистої інформації активістів. Крім того, ІМІ зафіксував по 1 випадку цензури, пошкодження майна журналістів, нападів на офіси ЗМІ та судового позову проти журналістів. Таким чином, з початку року ІМІ зафіксував 7 випадків побиття, 4 випадки цензури, 34 випадки перешкоджання, 16 — обмеження доступу до інформації та 15 випадків погроз.</p>
<p>Детальніше з барометром можна ознайомитися нижче:. На Одещині невідомі побили журналіста-розслідувача. Двоє невідомих близько ночі проникли через паркан у двір будинку, в якому проживає Буць з сім'єю. Почувши сторонні звуки, журналіст вийшов на подвір'я. Тут на нього напали і побили невідомі. Як пише інформагентство, журналіст перебуває в хірургічному відділенні районної лікарні.</p>
<p>У потерпілого черепно-мозкова травма, струс головного мозку.</p><strong><a id='q4'></a><h2>Пам'ятки України</h2></strong><p>Правоохоронці шукають зловмисників, які побили Буця, а також встановлюють їх мотиви. Сам журналіст пов'язує напад зі своєю професійною діяльністю. Проте припиняти роботу він не має наміру.</p>
		
	<p>Як зазначає агентство, Віктор Буць є автором багатьох резонансних розслідувань - в тому числі про зловживання владою з боку колишнього керівництва Іванівського району. Інцидент стався 25 травня близько , коли Олексій Леонтій повернувся з роботи додому. Як тільки він увійшов у двір свого будинку, його покликав молодий чоловік і попросив вийти. Як пише видання, з'ясувалося, що нападник - син однієї з членів Громадської ради при Зіньківській райдержадміністрації Максим Батрак, колишній працівник міліції. Відтоді, пише видання, головреду неодноразово погрожували фізичною розправою, а для залякування кілька разів розбивали вікна в приміщенні редакції.</p>
<p>Одеському журналісту погрожують після статті про психоневрологічний інтернат. За словами Дмитра Бакаєва, після виходу статті на його електронну скриньку, телефон та в соціальні мережі почали масово надходити погрози від невідомих осіб. Сам журналіст вважає, що керівництво інтернату обдзвонило родичів підопічних і натякнуло їм, що заклад можуть закрити, а хворих відправити по домівках.</p>
<p>Матюкаються в трубці. Що особливо неприємно, так це те, що це родичі хворих. Жителі сіл. Вони в більшості своїй і статті ніякої не читали. Їх просто обдурили. Після отримання погрози із зазначенням домашньої адреси та фото дверей квартири журналіста, Дмитро Бакаєв звернувся із заявою до поліції. Це перше повідомлення з цього акаунту, отримане Ольгою.</p>
<p>Журналістка каже, що оприлюднила погрозу, щоб показати суспільству з чим доводиться зіштовхуватися журналістам. Журналістка написала заяву в правоохоронні органи. Відомості внесено 14 травня до ЄРДР за ч. Я часто боюсь. Навчитись, навчити, зробити, змінити. Погроз отримувала різних. Але потім був Саакашвілі звинуватив журналістку у продажності — ред. Сотні повідомлень з образами і погрозами. Я певна, що їм навряд чи писали, але хто знає, чи завтра на якомусь мітингу мені не засадять ножа в спину. Мені чи будь-кому з нас. Про це повідомляється на сайті газети.</p>
<p>Крім того, зазначає видання, вже кілька місяців вони інформують читачів про роботу нового керівника Третьої міськлікарні Віктора Сичова. Як пише видання, Сичов 14 травня на своїй сторінці у Facebook висловив своє бачення ситуації щодо роботи моргу та оприлюднив скріншот інформаційного запиту Кременчуцької газети, який стосувався роботи міського моргу. А саме: щодо кількості трупів у лікарні в квітні , та років.</p>
<p>Булашев звернувся у поліцію і просить занести в Єдиний реєстр досудових розслідувань відомості про дану подію та відкрити кримінальне впровадження за статтею ч. Про це вона повідомила представниці ІМІ в Полтавській області. Інцидент стався 24 травня, розповіла Цирульник, на її мобільний телефон зателефонував невідомий. За словами Цирульник, заяву в поліцію вона написала за статтею перешкоджання журналістській професійній діяльності. Інцидент стався 24 травня, повідомляється на сайті агентства.</p><strong><a id='q5'></a><h2>Вікенд на Полтавщині – | Феєрверк мрій</h2></strong><p>В цей день редакція опублікувала відео затримання порушника правил дорожнього руху, який намагався перетнути Новий міст, де зараз ведуться ремонтні роботи та рух заборонено для будь-якого транспорту, окрім громадського. Порушник проігнорував вимогу поліцейських про зупинку та намагався втекти. І лише коли поліцейські перекрили йому дорогу, зупинився.</p>
<p>Цього ж дня, рішенням Соборного суду міста Дніпра, водія позбавили водійських прав строком на три місяці. Врешті-решт, унаслідок сварки, чоловік наблизився до операторки сайту Вікторії Машкіної та почав погрожувати. Інший оператор, Євген Вагнер, намагався заблокувати чоловіка захватом ззаду. Обидва впали на підлогу.</p><img src="https://mistaua.com/filesup/city_map/53uk.jpg" alt="Колоритна Полтавщина" style="max-width: 480px; max-height: 480px;"/><p>А чоловік почав битися та вкусив нашого оператора за руку. Полтава прихистила Дарину, пригріла і дала роботу. Але замість того щоб бути вдячною, Дарина стала шпигувати, пакостити і зливати інформацію шанувальникам федералізації і причаївся в Полтаві, своїм друзям Колорадо і сепаратистам! Інформацію Дарина виклала в мережу, після чого невідомі одразу ж почали тероризувати за телефонами активістів і їх сім'ї!</p>
<p>Оскільки Дарина є уродженкою Донецької області до речі, території, яка є підконтрольною саме Україні , Андрій Князєв вбачає у журналістських розслідуваннях Дарини дослівно - злив інформації та шпигунство. Також Лопушинський зазначив, що окремі колеги постять ці публікації і підтримують заклики звести рахунки із журналісткою. А чи, до речі, ви хоча б перевіряли факти, коли робили репости і писали свої домисли з приводу? Але мова зараз не про те.</p>
		
