<p>Однак, яка ціна потрібності? Він погоджується. Едді — 70, Луї — приблизно так само. Вони живуть за квартал одне від одного у маленькому місті і ніколи не були близькими. Втім, її чоловік помер, діти виросли, ноги мерзнуть, снодійні не допомагають і думає: чому ні? Зрештою, вона ж хоче просто говорити. Про те, як важливо не одружуватись у 20 і не зважати на плітки, коли тобі  Про ніжність, досвіди і самоту двох непримітних людей із невеликого американського міста, яке, здається, далі від Нью-Йорка ніж Париж, Лондон чи Прага.</p>
<p>Нью-Йорк, яким марять дівчата її віку, гучні вечірки, чоловіки, зрештою, материнство і це у их! Вона хоче стати письменницею. Однак, повернувшись із Нью-Йорка, Естер дізнається, що її не прийняли на письменницький курс, для того, щоб самостійно написати роман, їй бракує досвіду, а матір переконує, що краще стати стенографісткою і записувати за чоловіками.</p>
