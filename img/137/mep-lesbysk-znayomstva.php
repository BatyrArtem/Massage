<p>Втім, якщо вам остогидли сльозливі драми із весільно-білопарканчиковими гепі ендами, не проблема. Стрічки про холоднокровних самураїв, самотність та комунізм нічим не гірші для перегляду зі своєю половинкою чи без неї на День святого Валентина. Мельвіль її вигадав, але запевнив: вона з кодексу японських воїнів Бусідо.</p>
