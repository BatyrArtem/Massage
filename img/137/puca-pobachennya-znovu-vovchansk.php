<p>Ми всі  маємо  сьогодні визнати,  що чинний Закон про банки і банківську діяльність виконав  свою  роль  на  сучасному  етапі  - сформував  банківську  систему. Тому  розгляд  такого важливого і принципового закону,  проект якого ми  сьогодні  розглядаємо,  по- перше, обумовлений положеннями нової Конституції України, подруге, необхідністю правового регулювання найважливіших  правовідносин  у банківській сфері.</p>
