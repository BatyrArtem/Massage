<p>Дружківці Донецької області. Грідчина Наталя. З 1 липня року змінюється оподаткування пенсійних виплат!</p><strong><a id='w1'></a><h2></h2></strong><p>Дружківці Донецької області повідомляє, що з  З урахуванням внесених змін відповідно до пп. Тобто, нарахування податку здійснюється на суму, що перевищує 10 грн. Оподатковуються також пенсії, що отримуються з іноземних джерел за міжнародними договорами, якщо такі пенсії не оподатковуються в країні їх виплати.</p>
<p>Положення цього підпункту не застосовуються до пенсій, призначених учасникам бойових дій, інвалідам війни та особам, на яких поширюється ст. Згідно п. Начальник управління Пенсійного. Дружківці  Донецької області. Наталя Грідчина. С целью обеспечения большей доступности к получению услуг в сфере пенсионного обеспечения, а также для обеспечения конституционных прав на обращения маломобильных слоев населения с  Дружковке Донецкой области будут предоставляться консультации по вопросам пенсионного обеспечения гражданам с использованием программы «Skype».</p>
<p>Дружковка PensFondDruzhkovka. График предоставления руководством управления Пенсионного фонда Украины в г. Дружковке Донецкой области консультаций гражданам с использованием программы «Skype».</p><strong><a id='w2'></a><h2>Управління Пенсійного фонду України в місті Дружківка — Дружківська міська рада</h2></strong><p>Постоянно действующая «Горячая мобильная телефонная линия» -   Інформаційне повідомлення  про роботу  веб-порталу електронних послуг Пенсійного фонду України. До уваги пенсіонерів! Дружківці Донецької області роз'яснює особливості виплати пенсій за січень року.</p>
<p>Головна Влада Міська рада Виконавчий комітет Виконавчі органи.</p><strong><a id='w3'></a><h2>Управління Пенсійного фонду України в м Дружківка</h2></strong><p>Олексієво-Дружківка Райська. Ще в -му році тут стояли лише станційні будинки. Єдиним промис­ловим підприємством була невелика цукроварня графа Борисова, збудована в році. У році Донецьке товариство залізоробного та сталеливарного виробни­цтва, засноване французькими капіталістами, розпочало тут будівництво чавуноливар­ного та сталеливарного заводів. Перша домну було задуто в травні року. Уже наступного року завод виробив понад 2 млн пудів чавуну. А в — pp. У перші роки на заводі випускали лише бесемерівський метал. З року розпочалось виробництво мартенів­ського металу, яке, однак, не стало основним.</p>
<p>Головною продукцією заводу були рейки. В році тут працювало чоловік, а року —  Успіхи Донецького товариства привабили до Дружківки бельгійських промисловців. Навесні року вони створили Торецьке сталеливарне І механічне ано­німне товариство, яке збудувало на території колишньої Дружківської цукроварні Торецький сталеливарний і механічний завод, що виробляв обладнання для заліз­ниці: сталеві, чавунні та бронзові відливки, вагони, колісні скати, ресори та пружини для вагонів, вагонетки, стрілочні переводи. Після того як стало відомо про лютневий переворот у Дружківці було створено громадський комітет, видним членом комітету був купець Гераскін.</p>
<p>У раді було створено 8 комісій із чітко визначеними функціями міліційна, продовольча, культурницька, залізнична тощо. У дружківських комісіях брали участь особи, які не мали жодного стосунку до Ради, як, наприклад, священик Гонтаревський, що працював в культ-комісії та керував навіть спорудженнм нової школи. Блок обрав єдиний партійний центр і мав на увазі організовано виступити на виборах у волосне земство й місцеве самоврядування.</p>
<p>В ході Радянсько-української війни в грудні  р. Дружківка опиняється під контролем більшовицьких банд. У роки перших п'ятирічок побудовані найбільший в СРСР метизний завод , електростанція, нові цехи на металургійному заводі. Великим підприємством вугільного машинобудування став після реконструкції Торецький завод імені Ворошилова до році тут працювало особи.</p>
<p>У році Дружківка отримала статус міста. До  р. Працювали лікарня й 4 фельдшерських пункти, 8 загальноосвітніх шкіл, робітфак, вечірнє відділення машинобудівного технікуму, кінотеатр, два клуби, стадіон. В ході німецько-радянської війни була захоплена німецькими військами 22 жовтня року.</p>
<p>З міста було евакуйовано машинобудівний завод до м. Олександрівськ на Уралі та метизний завод на Кузбас. Німцями закатовано й відправлено до Німеччини осіб. Ескізний проект пам'ятника розробив дружківський художник Віктор Федоренко, а втілив у життя задум художника скульптор Юрій Тотуз. Навесні Дружківка постраждала від зазіхань з боку терористичної організації «Донецька народна республіка».</p>
		
	<p>Так, 12 квітня повідомлялося про захоплення сепаратистами будівлі районної адміністрації [7]. В той самий час, на відміну від сусідніх міст — Слов'янська та Краматорська, Дружківка уникла бойових зіткнень завдяки високій самоорганізації місцевих активістів [9]. З пізньої осені року в місті дислокується а десантно-штурмова бригада.</p>
<p>Дружківській міській раді підпорядковані такі населені пункти [10] :. Населення Дружківки на 1 червня року — 67 осіб [11]. Національний склад населення за переписом року [13]. З року трамвайний парк міста активно оновлюється.</p>
		
