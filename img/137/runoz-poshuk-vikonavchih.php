<p>
<p>Душе істини, що всюди єси і все наповняєш, скарбе дібр і життя подателю, прийди і вселися в нас, і очисти нас від усякої скверни, і спаси, благий, душі наші. Пресвята Тройце, помилуй нас; Господи, очисти гріхи наші; Владико, прости беззаконня наші; Святий, посіти і зціли немочі наші імени твого ради. Науки провадив отець Йосафат Хаймик, духовний провідник спільноти. Упродовж трьох днів ми говорили про внутрішній голос, який називаємо сумлінням. Саме такою і була тема наших великопісних реколекцій. Протягом цього часу ми також мали можливість разом молитися, брати участь у Богослужіннях, роздумувати над Словом Божим.</p>
<p>Якщо хтось не знає, сьогодні перше березня - неділя Прощення. А також зустріч 2-го етапу ШХА. У Василіанських аніматорів ІФ є традиція: кожного року у цей день ми стаємо в коло і просимо в один одного прощення. Тож ми повторили її разом зі своїми друзями, які відвідують ШХА. У нас було дві лекції, першу з яких проводила аніматор зі стажем Софія Герега. Ми говорили про медицину під час табору. Першу лекцію "Покликання до святості. Василіанські святі" для нас проводили аніматори Віктор Кланіца і Соломія Жаб'юк. Сьогодні 9 лютого р.</p>
<p>Ми не даємо про себе забути. І от знову 26 січня відбулась чергова зустріч, але вже першого етапу ШХА. Сьогодні ми говорили про Дона Боско. Це видатна людина, яка створила запобіжну методику виховання. Про це нам розповідав аніматор Діма Сенів. Ця виховна система є основою "Веселих канікул з Богом".</p>
<p>Сьогодні ми розбирали дві дуже важливі теми, як і для нашої спільноти, так і для особистих потреб кожного. Спершу ми говорили про Мас-Медіа та її особливості. Цю лекцію для нас проводив досвідчений у цій сфері аніматор Артур Пашник.</p><strong><a id='i1'></a><h2>Пошук судових рішень</h2></strong><p>Величним концертним дійством за участю різних колективів-учасників, які спільно — зведеним хором виконували різдвяні пісні і колядки, закінчився улюблений не лише в Івано-Франківську, на Прикарпатті, Україні. Протягом декількох днів перед глядачами виступило понад 30 колективів і виконавців. Кожного радо зустрічали і підтримували глядачі, яких величезна кількість зібралася у Храмі Царя Христа. І разом вони славили народження Христа.</p><strong><a id='i2'></a><h2>Надані гранти - Підтримані проєкти</h2></strong><p>Розпочався черговий день фестивалю «Коляда на Майзлях» з встановлення рекорду України — на святкову Різдвяну ходу з різних парафій Івано-Франківська і навіть з-закордону, зібралися сотні людей, які пройшли парадом зі спеціально виготовленими зірками. До центру міста — на Вічевий майдан, колони, під спів колядок, рушили від монастиря Царя Христа, храму Різдва Христового, Архікатедрального і Митрополичого собору Воскресіння Христового, монументу «Вознесіння Христового» де зустрілися і спільно - разом з зірковими гостями «Коляди» - Національною капелою бандуристів ім.</p>
<p>Майбороди, виконали коляду. Вокальний ансамбль «Співаночки» з Кривого Рогу, який його відкривав, вже не вперше у нас і знову з цікавою програмою — колядки дівчата виконують у притаманній лише їм оригінальній манері і аранжуванні. Нехай Господь надихає ваші молоді серця». Другий день фестивалю відзначився присутністю дорогих гостей-учасників з прифронтової Луганщини. І, як каже його керівник Аліна Лисовець — з превеликим задоволенням. Коляда у виконанні дівчаток з «Мелодії» особливо розчулює і викликає шквал емоцій у глядачів кожного разу. Вітаємо всіх його гостей і нехай Всемилостивий Господь благословить цей святий міжнародний фестиваль коляди», - наголосив владика.</p>
