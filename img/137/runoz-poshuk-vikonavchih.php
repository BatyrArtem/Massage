	<p>Мирна, лагідна та водночас зворушлива молитва під аконпонемент звуків скрипки допомагала кожному з присутніх стати ближче Господа і водночас один до одного, відчути себе одною родиною в цьому храмі. Віддати свої турботи Ісусові, щоб Він нами провадив. Великою радістю і святковим настроєм розпочався сьогодні, майстер клас по виготовленні Різдвяної Зірки, для діток віком років. Дорослі щиро раділи разом з дітьми, від неповторного і неймовірного процесу створення власного Різдва.</p>
<p>І вже дуже скоро, усі дітки нашої школи зможуть приєднатися до святкової ходи з колядою, потішаючи усіх своїми Зірочками. В кого що, а в нас сьогодні відбулася захоплююча зустріч ШХА 2 етапу. Почалася вона лекцією, яку провела нам аніматор Адріана. Якщо ви хоча б раз були на нашому таборі, то б мали догадатися,що розмова йшла про банси.</p>
<p>На нашу думку, це найулюбленіший момент на таборі багатьох дітей і, звичайно, аніматорів. До речі, той хто танцює банс "тричі молиться". І це не ми придумали, так говорив Св. А ви знали що для того щоб вибрати банс потрібно подивитися переклад? Якщо він на іноземній мові, то має бути християнська тематика і драйвова музика. А ще ми дізналися, що бувають різні види бансів. Немає кращого часу для зустрічі майбутніх аніматорів, як перед святом Миколая.</p><a id='i3'></a><h3>Ретрит для працівниць "гарячої лінії"</h3><p>Адже це свято обожнюють усі діти, та навіть дорослі. От і ми вирішили трішки поринути у зимову казкову атмосферу. Хоча говорили сьогодні про серйозні речі. Наш холодний листопад закінчується яскравими емоціями. Адже найкращим варіантом прощання з осінню є день, проведений на Школі Християнського Аніматора. Розпочинали ми сьогоднішній день з розмови про формування власного характеру. Уже знайома нам психолог Тимчій Іванна розказувала що таке характер, а також, як побороти свої страхи і вади. А ви знали, що темперамент і характер є незалежними один від одного? З темпераментом ми народжуємось, а характер набуваємо разом з життєвим досвідом.</p>
<p>Нарешті настала довгоочікувана зустріч 1 етапу ШХА. Сьогодні у нас була неймовірно цікава лекція про характер. Ми розбирали які риси мають бути у кожного християнського аніматора. Їх, звісно, є дуже багато, але на сьогоднішній зустрічі ми виділили тільки основні. Під такою однойменною назвою, вже третій рік поспіль, на парафії Царя Христа пройшов фестиваль дитячих талантів.</p>
<p>Відроджений, настоятелем монастиря оо. Василіян, та парохом парафії - отцем Йосафатом Хаймиком, ЧСВВ, цей фестиваль вже традиційно об'єднує навколо себе талановитих дітей та молодь нашого міста. Задум та основний меседж цього заходу дуже простий - дати можливість вступити на сцену, нехай поки маленьку та родинну, усім бажаючим діткам нашої парафії. Ранішню Святу Літургію очолив о. Співслужили владиці: отці-василіяни з парафії та ті, які завітали з близьких і далеких парафій, ігумени монастирів. На Святу Літургію також зібралися сотні івано-франківців та гостей міста.</p>
<p>Сьогодні, такого сонячного жовтневого дня, відбулася вже друга зустріч 1 етапу Школи Християнського Аніматора. На початку зустрічі у нас була невеличка самостійна робота по вивченому матеріалу за перше заняття. Кожен з присутніх описував християнського аніматора, та те, яким він повинен БУТИ.</p>
