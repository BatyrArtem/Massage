<p>Я дізнаюся, як створити соцмережу з доповненою реальністю для туристів у Копенгагені, як за хвилину визначити, що з тобою розмовляє бот, і чому турецький поет Кемаль Сурея назвав Стамбул "величезним звіром". В одній з таких розмов відбувається дивна хімія, здається, що ми знайомі сто років, розуміємо одне одного з півслова і можемо ділитися найпотаємнішими секретами.</p>
<p>Я знаю, що віртуальні стосунки оманливі, але хіба якісно створена ілюзія в наші дні вже давно не стала реальністю? Можливість знайти на Tinder свого справжнього соулмейта - п'ята й остання річ у моєму списку. Кілька днів я ширяю в хмарах, доки не лунає сакраментальне питання про серйозність стосунків.</p>
