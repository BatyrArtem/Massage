<p>
<p>Як на мене, саме вони створюють той самий гостинний карпатський колорит». В Одеській області є все, що потрібно людині для щастя — сонце, море, посмішки відкритих життєрадісних людей, смачна їжа і дух свободи. Вам в «Перлину біля моря». Одеса — це літні майданчики, архітектура старого міста, нескінченні парки і дворики, легендарний «Привоз», на якому ще можна купити дунайського оселедця і завжди — збагатити свій словниковий запас. Хочете тиші та усамітнення? Їдьте у бік Затоки в населений пункт Санжейка.</p>
<p>Хвилин 20 на авто — і ви на красивому пустельному пляжі, дорога до якого прокладена зі спуску з семиметрового обриву. Складно сказати, де крутіше пройтися — зверху, насолоджуючись панорамою і прозорістю моря, або внизу по кромці хвиль. Напевно, найкраще рішення — захопити намет, залишитися тут на якийсь час і випробувати обидва варіанти. Для тих, кому конче потрібні зручності, за метрів звідси пропонують апартаменти.</p>
<p>Вам в Роксолани. З села на лівому березі Дністровського лиману можна побачити Білгород-Дністровську фортецю. Сам ландшафт тут унікальний — це вкриті зеленню круті пагорби, в які врізається вода. Раніше тут існувало місто Ніконія, та сьогодні воно затоплене водами лиману.</p>
<p>Тут ми влаштували пікнік. Смак навіть найпростішої їжі в цьому місці змінюється дивовижних чином». Яким би казковим нам не здавався Львів, я все ж рекомендую виїхати за межі міста і відкрити для себе цілий регіон з його давньою історією і культурою. Навіть якщо є лише один день, його можна витратити на знайомство із замками «Золотої підкови Львівщини». До маршруту входить 5 замків: Олеський, Підгорецький, Золочівський, Свірзький та Старосільський.</p>
<p>Кожен з них розкаже свою історію, зачарує легендами, полонить краєвидами. Протяжність маршруту — близько км. Тож зручно буде добиратися як на власному авто, так і разом з туристичними групами.</p><strong><a id='j1'></a><h2>Катерина Бакай: Моє знайомство з Могилянкою</h2></strong><p>Не біда, якщо не запланували екскурсію заздалегідь. Її можна придбати чи не в кожному туристичному агентстві Львова. Виїзд на екскурсію відбувається рано-вранці з центру міста. Я теж мандрувала «Золотою підковою» з туристичною групою. Тому я вже точно знаю, куди відправлюся, коли наступного разу потраплю до Львова — до Свірзького та Старосільського замку. І я вже передчуваю, що це буде щось неповторне та захопливе».</p>
<p>Тож, перед мандрівкою сюди варто подивитись цей фільм, а також — «Заповідник Асканія» — документальну роботу Андрія Литвиненка. Тоді вирушаймо! Чим же тут зайнятись? Відразу ж після оголошення карантину я зловила себе на думці, що найкращий спосіб самоізолюватись — поїхати до моря.</p>
<p>Хіба це не мрія? Завжди хотіла пожити біля моря, в цьому є особлива романтика — дихати свіжим морським повітрям, насолоджуватися прекрасною панорамою і отримувати натхнення. Карантин можна проводити по-різному. Головне ставитися до нього розумно. Під час послаблення карантину я почала вивчати околиці і в черговий раз переконалася, що у нашій країні повно мальовничих місць, які своїм виглядом здатні зачарувати і закохати раз і назавжди.</p>
<p>Відкрила для себе з іншого боку узбережжя Коблево з нереальними схилами і краєвидами.</p><strong><a id='j2'></a><h2>Триває виставка «Моє місце» у Національній бібліотеці України для дітей | Громадський Простір</h2></strong><p>Тут хочеться постійно повторювати: «Зупинись, мить! Ти прекрасна!</p>
		
	<p>Красива Україна, правда? Тож не стримуйте себе та прямо зараз плануйте подорож рідною країною, а ми вам у цьому допоможемо і запропонуємо безліч варіантів відпочинку — вони вже чекають на вас! Звичайно, до України". Це не про сексуальні стосунки, в першу чергу, а про людські". Моє перше Tinder-побачення також з іноземцем - французом. Ми зустрічаємося в понеділок о дев'ятій ранку випити кави. Розмовляємо на дикій суміші англійської, французької і російської він чотири роки працював у Росії.</p>
<p>Це не одразу дозволяє мені помітити приголомшливу відсутність почуття гумору в мого співрозмовника. Щойно замовивши капучино і чорничний пай, роблю одразу два "камінг-аути".</p><strong><a id='j3'></a><h2>Гола правда про сайти знайомств та їх відвідувачів</h2></strong><p>Зізнаюся, що мені 41 з переляку і невпевненості спочатку написала в анкеті, що 37, але, як виявилося пізніше, додаток не дозволяє редагувати дані про ім'я і вік. На цю новину він реагує приблизно так, як герой комедії "Дехто любить гарячіше", той, що філософськи зауважував - "в кожного свої недоліки". А от інформація про те, що я насправді пишу статтю про Tinder, дається моєму тіндерфелла дещо складніше. Але і цей удар він витримує стоїчно і навіть в кінці пропонує заплатити за мене.</p>
<p>Я категорично відмовляюся, проте цікавлюся, чому він шукає наречену саме в тутешніх місцях. І отримую знайому вже відповідь - "тут дуже миловидні жінки, освічені і розумні, цікаві співрозмовниці Я не витримую і перебиваю: "І мріють звідси втекти? Моє друге побачення, мабуть, найкращий досвід, що стався зі мною впродовж поневірянь світом віртуальної романтики.</p>
<p>Відправляючи в небуття лівого свайпу чергових випускників Стенфорда на сноубордах, натрапляю на профіль, автор якого шукає моделей для фотосесії. Так, я знаю, що камера - старий як світ метод пікапу, але спокуса велика, до того ж фотограф пише, що в стосунках не зацікавлений.</p><a id='j4'></a><h3>Правило № 1. На хорошу аватарку і Леонардо клюне</h3><p>За годину ми домовляємось про час, місце і формат зйомки. Він розповідає, що в нього склалося з десяток пар, але я "єдина, хто адекватно спілкується", і додає, "сподіваюся, ти не мужик". Я пропускаю повз вуха відвертий сексизм цього жарту, він зізнається, що й хлопців в принципі теж готовий пофоткати, і ми вирішуємо зустрітись. Автор фото, Dmitry Larin. Ми гуляємо млосного літнього вечора, і я отримую першу в своєму житті професійну фотосесію.</p>
<p>Він - штатний фотограф одного ЗМІ, якому потрібно попрактикувати зйомку на пленері, я маю нарешті дописати статтю про Tinder. Без сумніву, це - ідеальний "метч". Це - четверта річ, яку я зробила на Tinder. Та її варіацій - безліч.</p>
		
