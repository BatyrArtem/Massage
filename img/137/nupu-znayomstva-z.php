<p>Під прицільним вогнем артилерії ще до початку наступу опинились і позиції в чистому полі під Гузіївом сотень II куреня. Але під час атаки стрільці цього куреня спинили наступ російських військ на залізничному шляху поблизу Болехова. Цьому допомогла і новоприбула з Коша сотня під проводом чотаря Івана Цяпки. До речі, під час війни І. Цяпка став улюбленцем стрільців за веселу вдачу, спокій, розсудливість, гумор.</p>
		
	<p>Саме йому пізніше була присвячена стрілецька пісня «Бо війна війною». В боях під Болеховом УСС зазнали великих втрат: 15 вбито, 50 поранено, потрапило в полон. Спроба перехопити ініціативу не привела до суттєвих змін, і вздовж загального фронту почався відступ російських військ. За місто боролася сотня Дмитра Вітовського та О. Серед хоробрих стрільців, у цьому бою себе виявив Григорій Трух , який був командиром чоти у сотні Дмитра Вітовського, провідником розвідувальних стеж у Карпатах. Поранений під Болеховом 30 травня року, неподалік залізничної платформи потрапив у полон.</p>
<p>Болехів окупований військами Польщі. у склад міста включені гміни Болехів Руський, Вавилон Новий, Соломонова Гірка і Волоське Село [4]. в результаті анексії Радянським Союзом західноукраїнських земель Болехів приєднаний до Української РСР. Місто стало відомим на Заході завдяки книзі американського письменника Даніеля Мендельсона «Загублені: Пошук шести із шести мільйонів» англ. The Lost: A Search for Six of Six Million , ISBN , яка розповідає про долю єврейської громади міста. До Другої світової війни близько половини мешканців міста були єврейського походження.</p><img src="https://f2.love.ua/b_7f_1BR0I_blXoQEYyDbfqA1.jpg" alt="" style="max-width: 480px; max-height: 480px;"/><p>Під час Голокосту з євреїв вижило лише  Болехів став центром однойменного району, який ліквідовано в  р. До Болехова приєднане село Довжка [6] тепер — південна частина міста, з'єднана вулицею Довжанською. З  р.</p><strong><a id='t1'></a><h2>Болехів — Вікіпедія</h2></strong><p>місту надано статус міста обласного підпорядкування. У Болехові працюють: ВАТ «Будматеріали», ТОВ «Елрун», Об'єднання промислових підприємств, Споживче товариство, житлово-комунальне підприємство, державне лісове господарство. Освіту в місті надають: дві середні школи, лісогосподарський ліцей-технікум, художня школа, музична школа, міжшкільний навчально-виробничий комбінат, два дошкільні заклади.</p>
