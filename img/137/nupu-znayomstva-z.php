<p>А гуртова торгівля худобою, вичиненими шкірами, сіллю, діжками, виробами зі шкіри — стала основним промислом місцевих ремісників та купців середньовіччя. Наприкінці ХХ століття за участю німецького та єврейського капіталу було збудовано: нафтову рафінерію, тартак, шкірзавод, цегельний завод, фабрику крісел, меблеву фабрику, оцтарню, сірникову фабрику, тарний завод, удосконалено солеваріння.</p>
