	<p>Говорити відразу прямо про свої наміри не годилося. Замість цього використовували образи полювання чи купівлі-продажу. Дівчина в такому випадку поставала здобиччю або товаром, парубок — купцем або вправним мисливцем. Деякі дослідники вбачають у цьому залишки архаїчних шлюбних звичаїв, коли наречену викрадали чи купували, проте до останнього часу такі розмови були нагодою для старостів повправлятися в дотепності та «забалакати» батьків дівчини, схилити їх на свій бік. Саме тому в старости запрошували дядьків на язик гострих, тямущих, а вдома у хлопця їх ще й частували горілочкою, щоб веселіше діло робилося.</p>
<p>Як заходили свати до хати, дівчина прудко ховалася та, затамувавши подих, уважно дослухалася до розмови, аж поки її не покличуть батьки. Або ж лишалася в кімнаті і мовчки та зосереджено «колупала комин, бо соромно було», доки не питали її думку. І додає: «А сама думаю: як же ж я не піду, я ж тоді остануся».</p>
<p>Коли дівчина була згодна вийти заміж і батьки не мали заперечень, з сіней кликали парубка, який мав привітатися та розповісти, хто він і звідки. Потому сватів запрошували до столу і обмінювали хліб: принесений старостами дівчина забирала його потім неодмінно треба було розрізати, щоб таким чином скріпити домовленість , а натомість віддавала свій. У Валківському районі у сватів хліб брав батько, цілував і говорив донці: «Давай сватам рушнички, а молодому — хустку». З самого дитинства дівчинка привчалася до жіночої роботи: вчилася прясти, ткати, вишивати, шити одежу. Щойно в її руці опинялася голка, майбутня наречена починала готувати собі придане.</p>
<p>Тож і зараз, коли дівчина, зашарівшись, виносила на тарелю кожному старості по рушнику в деяких селах — по два , а молодому — вишиту хустку, говорили повчально: «Молода не гуляла — рушників надбала». Рушниками дівка перев'язувала сватів через одне плече або хрест-навхрест — це був її подарунок і наочне свідчення про неї як добру господиню. Тож обирала довгі, щоб аж землі діставали, тонкі, багато орнаментовані. В пізніші часи замість рушників уживали платки чи навіть просто великі шматки матерії.</p>
<p>Хустину дівчина в'язала обранцю до правої руки. У селі Крисине сват рушником зв'язував дівчині та хлопцю руки, промовляючи: «Оце ви тепер молодий і молода». А Либа Катерина Парамонівна, мешканка села Знам'янка Нововодолазького району, так описує своє сватання: «Розстелили сіряк перед столом, заставили на коліна становитися, поклони бити. Тричі ми поклони били, кланялися до долівки. Тоді почали хліб переміняти, і той дядько, що прийшов сватати, віддав свою хлібину, а мама йому — свою.</p>
<p>Рушниками чи двома платками сватів перев'язували навхрест. Тоді посідали, по чарці випили».</p>
