<p>Як відрізнити реальні фотки від фейкових?</p>
		
	<p>Якщо в якоїсь дівчини в анкеті ти бачиш тільки одну фотографію, і фотографія дуже шикарна, то дуже ймовірно, що вона десь вкрадена. Або, якщо фоток декілька, але, насправді, це одна й та сама повторюється. Або є одна крупним планом, а інша — в дуже далекій перспективи. Той самий варіант. Це не її фотки. Раз в мене був такий випадок, коли познайомився з однією супер-гарною дівчиною, як я міг судити судити по фотографіям. Говорив з нею по телефону, буквально, годинами, упродовж багатьох днів.</p>
<p>Вже майже домовилися, що зустрінемося, вона розказала де працює, на якій вулиці. Я вже хотів  бігти туди, її шукати. І тут такий прікол. Вона просто зникла, перестала відповідати. Я розказав цю ситуацію другу, він якось там в інтернеті погуглив і присилає мені лінк — прошу, ось така-то і така-то у Вконтакті. Я захожу на її екаунт і пишу їй — "то це ти там на такому-то сайті знайомств? Вона відповідає — "який сайт, нічого такого не знаю, я живу в Харкові". Ну я їй відписую — "тоді знай, що під твоєю фоткою, на тому сайті якась дівчина дурить хлопців".</p>
<p>Я цій дівчині, яка зникла, ще пару раз писав і питав — "скільки ще будеш людей розводити! Вона ніяк не реагувала. Але адміністратору не став писати. Було раз, що я домовився про зустріч і в той же день зустрілися, в районі Привокзального ринку, було темно, така холодна пора. Приходить на зустріч кобіта, я на неї дивлюся і так згадую її фотку з сайту і настільки велика різниця, що я просто був в шоці. Довелося з нею таки йти в піцерію.</p>
