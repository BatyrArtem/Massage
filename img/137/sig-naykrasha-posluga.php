<p>І каже, що давно мріяла до Львова приїхати, всяке таке ліпить і дає номер телефону. Зідзвонилися, говорили  десь впродовж двох днів, а потім вона мені заявляє — "да, я развєдєна, дітей нема, я хочу поїхати до Львова. Якщо ти мене зустрінеш і покажеш мені Львів, то я приїду". У мене якраз вихідні, я можу взяти відпустку, коротше, все так співпадає, так красиво. Я, дурний, ведуся, ж фотку бачу, і вже собі уявляю: приїде така красуня з Харкова. Я вже забронював квартиру, в центрі міста коло Оперного. Вигляд прямо на готель "Опера". Добре, що без завдатку, мені повірили. Мав, одним словом, вже якісь плани.</p>
<p>І тут вона мені вже дзвонить і каже — "все, я сьогодні йду купляти квитки". Я думаю, все класно. А потім ще передзвонює — "солнишко, тут така ситуація, я забронювала квитки, мені треба їх купити, але ось зараз немає грошей, в мене кума вчора позичила, сьогодні ввечері поверне. Ти можеш мені позичити на квиток, а я, як приїду, то відразу віддам, вісімсот гривень, туда і назад". Я кажу — "я би радий, але в мене теж грошей зараз немає". У мене були, але я щось трохи запідозрив. Вона питає — "а коли будуть? Вона каже — "добре, я почекаю до обіду".</p>
		
