<strong><a id='d1'></a><h2>1 кімнатні квартири - Містечко Центральне</h2></strong><p>Ти можеш доказувати, що ти той самий, я навіть писав у службу підтримки, модератору: в мене, типу, злетів Windows, а їм пофіг, просто блокують. Навіть не відписали. Мене тоді В. Але прікол в тому, що на сайті воно автоматично написало мені Берлін — "моє ім'я, Берлін". І я постійно писав — "привіт, давай знайомитися", а мені відписували — "ти шо, дурак", а я їм пояснював, що я зі Львова, то в мене IP-адреса така. Є загальний ресурс, називається mamba.</p>
<p>Назв — тьма. А база даних та сама, фотки ті самі. Набираєш там, наприклад, "ищу девушку, львов". Заходиш на сайт і набираєш поиск, разширений, вік, від і до. На телефоні, я заходжу на Wambu, але це те саме. Раз навіть роботу вдалося знайти через сайт знайомств. Познайомився там з кобітою, виявилось, що їй треба відреставрувати вікна. Потім виявилося, що вона взагалі з Росії, з Чіти. Вона, як сама себе називала, содєржанка. Якийсь чувак з Франції її утримував. Якось вони проїжджали нашими місцями в Польщу, заїхали до Львова і вона, за її словами, закохалася в наше міст.</p><strong><a id='d2'></a><h2>Клуб знайомств</h2></strong><p>Ну, і той чувак купив їй тут квартиру. Купив їй теж піаніно і вона тут собі жила, займалася музикою. Причому страшна шовіністка російська, як виявилось. Перед Майданом втекла назад до себе в Росію, от як чуйку мала. Раз був кумедний випадок. Я написав "Привіт" і мені зразу приходить "Привіт, мене звати Аня". Але не пише ніяких доларів і зразу номер телефону, зразу, перше повідомлення, ще до того, як я попросив.</p>
<p>Я витягую свій МТС, дзвоню. Мужик піднімає трубку, я, опа, скинув, дивлюся, чи я правильно набрав. Ні, правильно. Дзвоню ще раз, там ще раз мужик. Я пишу — "ти що, прікалуєшся", а вона каже, — "нє, не прикалуюсь". Я кажу "стоп".</p>
		
			
