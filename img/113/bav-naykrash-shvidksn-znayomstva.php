	<p>Тепер там облаштовано нових ліжок, які зараз уже використовують для лікування хворих на ковід Там є ШВЛ, кисневі концентратори, тобто все необхідне, щоби пацієнти отримали якісні послуги. І таких проєктів є багато, й наші аграрії, коли потрібно, до них долучаються.</p><strong><a id='h1'></a><h2>Бердянськ | 7 чудес України</h2></strong><p>Думаю, що цей закон збалансований, адже була можливість внести стільки змін, скільки хто хотів. Тернопільщина і справді є аграрною областю, тому ми повинні готуватися, що ринок землі настане рано чи пізно і він буде працювати. Мабуть, не було такого губернатора, який би не обіцяв її добудувати, але справа так і не зрушилася з місця….</p>
<p>Але повірте, це я буду дурити людей і це буде неправда, тому що не можливо його здати цього року — чи до Дня Незалежності, чи до Нового року. По-перше, реконструкція займе доволі багато часу. По-друге, ми провели попередню експертизу, де виявилось, що приміщення книгозбірні знаходиться у жахливому стані. ДБНівські норми порушено, сама будівля починає руйнуватися. Це свого роду «чиряк» у центрі Тернополя. Зараз є декілька бачень, що з ним робити далі.</p>
<p>Однозначно, це приміщення повинно працювати для громади, для жителів області — чи в режимі хабу, чи величезного комплексу для надання адміністративних послуг. Зараз будемо спільно з мером Тернополя та головою обласної ради думати, які варіанти вирішення. Його можна все-таки реанімувати чи це мертвий проєкт? Я не хочу бути голослівним і казати: «Так, ми відновимо аеропорт!</p>
<p>Думаю, що рік буде присвячений проєктно-кошторисній документації, а потім намагатимемося добудувати злітну смугу, встановити огорожу і хоча б отримати сертифікат для внутрішніх авіарейсів. Якщо ці задуми вдасться втілити, то це буде вже велике досягнення для Тернопільщини, тому що на сьогодні аеропорт просто пустує. Упродовж останнього часу з Кабміном та інвесторами проводились перемовини, розглядаються різні варіанти. Тернопільський аеропорт цікавить багатьох перш за все логістично, адже ми знаходимося практично посередині чотирьох сусідніх обласних центрів — Івано-Франківськ, Львів, Чернівці, Хмельницький.</p>
<p>Паралельно із пасажирськими перевезеннями розглядаємо варіант розвитку аеропорту у вантажному плані. Але ми повинні зрозуміти, що цей проєкт точно не на рік, а мінімум на років. Головне, щоби цей маховик запустився,  аеропорт запрацював, а далі можна його удосконалювати. І завжди болюча для Тернопільщини тема — заробітні плати, адже останніх років ть область пасла задніх у країні щодо середньої зарплатні?</p>
<p>У нас немає надпотужної промисловості, серйозних виробників, які могли би піднімати середній рівень заробітної плати. Проте у нас є аграрії, з якими ми поспілкувалися, і в них уже зараз досить високий рівень заробітних плат.</p><strong><a id='h2'></a><h2>Енергоменеджмент - шлях до заощадження державного та місцевих бюджетів</h2></strong><p>У цьому плані потрібно працювати в комплексі, розвиваючи економіку, створюючи нові робочі місця. Щодо інвестицій, звичайно, намагаємося в цьому напрямку активно працювати. Кілька днів тому в мене відбулася зустріч із представниками китайського бізнесу. Розглядається декілька локацій на півдні Тернопільської області щодо будівництва заводу з переробки кукурудзи, а також зведення теплової електростанції потужністю 20 мегават для внутрішнього користування цього заводу.</p>
<p>У перший етап планується вкласти млн. доларів, а в загальному — до 1 млрд. Ця інвестиція на рівні України може стати не лише помітною, а й важливою. Тому за цей проєкт будемо боротися. Сподіваюся, китайська сторона прийме правильне рішення і збудує завод саме в нашому краї. Коли мене призначив президент, то я був єдиним представником центральної влади у сесійному залі серед 64 депутатів обласної ради минулого скликання. Звичайно, було важко, доводилося переконувати в елементарних речах. Упродовж року депутати схилялися тільки до виборів і ставили мені часто палиці в колеса, про що я неодноразово наголошував.</p>
<p>У нас були бюджетні баталії, суди і все решта.</p>
		
	<p>Теперішній склад обласної ради, до якої я входжу і як голова ТОДА перебуваю у президії, — конструктивний, робочий, практично немає політики, відповідно є можливість спокійно працювати та ухвалювати рішення на благо жителів області. Напевне, це зумовлено тим, що вдалося вийти з тієї політичної турбулентності, в якій ми перебували практично весь минулий рік. Коли і де народилися, ким працюють Ваші батьки?</p>
<p>Мама працює медсестрою у районній лікарні, батько — викладач СПТУ, а до того працював водієм автотранспортного підприємства. У мене був своєрідний симбіоз, тому що все літо проводив у селі то в однієї бабусі, то в іншої, а решту часу — в місті. На канікулах довелося пасти корову, залучали до городніх робіт, косив траву, працював у саду. У місті левову частку часу займала школа.</p>
<p>Подобалися інформатика, хімія, математика, непогано давалися англійська і латинська мови. Як не дивно, фізика не входила до переліку улюблених предметів. У молодших класах не любив малювання, а в старших — хіба що німецької мови. Запитують у річного хлопця: що ти робиш? Він відповідає, що відпочиває. А йому апелюють: а коли ж ти встиг втомитися?.. В Рогатині є одна з кращих шкіл із кульової стрільби, яка виховала не одного чемпіона світу.</p>
<p>Досить довго відвідував цю спортивну секцію, показував непогані результати. Також ганяв у футбол, з однолітками купалися у ставку. Тобто жив звичайним здоровим життям на свіжому повітрі, без мобільних телефонів та інтернету. В університеті паралельно здобував дві вищі освіти. Стаціонарно навчався на спеціальності «економіка, фінанси і кредит», а на вечірній формі вивчав іноземні мови. Брав актину участь у житті вузу, був головою студради. Як відпочивала студентська молодь у Львові? Я не відрізнявся від більшості з них, адже мешкав у гуртожитку. Ніхто днями й ночами не гриз граніт науки, недарма є така приказка: «Від сесії до сесії живуть студенти весело».</p>
<p>Ввечері було студентське дозвілля.</p><a id='h3'></a><h3>м. Нова Одеса, вул. Сеславінського, 12 (ТСЦ МВС № 4843)</h3><p>Це були надзвичайно великі кошти! І я наскладав їх. Але не надовго. Довелося вже на нього збирати. З го до років пройшов тернистий шлях від простого оперативного працівника до заступника начальника податкової міліції — начальника управління з викриття злочинів у базових галузях економіки Державної податкової адміністрації в Івано-Франківській області.</p>
<p>податкова міліція реформувалася і переформовувалась. У мене на той час була краща пропозиція в бізнесі — я зайшов співзасновником в одну із фірм і дотепер не жалкую, що тоді прийняв саме таке рішення. Займався бізнесом у лісовій галузі, наданням послуг, ресторанним бізнесом. Для прикладу, відкриття ресторану в Рогатині припало на 1 грудня року, коли відбувся Євромайдан. Половина моїх гостей сіли в автівки і наступного дня поїхали до Києва.</p>
